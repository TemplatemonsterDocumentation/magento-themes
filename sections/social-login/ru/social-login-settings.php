        <td>turn on to enable the module.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>Client id</strong></td>
        <td>-</td>
        <td>Google+ client id.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>Client secret</strong></td>
