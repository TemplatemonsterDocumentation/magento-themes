<h3>Mega menu</h3>
<p>Given module substitutes standard menu with the menu which allows to configure the display of two level subcategories, add static blocks to the dropdown menu, add labels, display products, etc.</p>
<strong>MegaMenu</strong> is included into the template by default, you can enable/disable it in <strong>System > Configuration > MEGAMENU > Mega Menu</strong>.

<ol class="index-list">
	<li>Display of each dropdown menu can be configured on the category settings page, which is located in <strong>Catalog > Manage Categories</strong>.
		<figure class="img-polaroid"><img src="img/magento/mega-1.jpg" alt="" /></figure>		
	</li>
	<li>
		In order to add a static block to a dropdown menu, you need to add a static block in <strong>CMS > Static Blocks</strong> and once it was created add it to the menu.
		<figure class="img-polaroid"><img src="img/magento/mega-2.jpg" alt="" /></figure>
	</li>
	<li>
		Module allows to add the label for any category. You can do this in categories menu <strong>Catalog > Manage Categories</strong>.
		<figure class="img-polaroid"><img src="img/magento/mega-3.jpg" alt="" /></figure>
	</li>		
	<li>
		In order to change the number of columns in subcategories you need to change the Level column Count value in the menu <strong>Catalog > Manage Categories</strong>.
		<figure class="img-polaroid"><img src="img/magento/mega-4.jpg" alt="" /></figure>
	</li>			
</ol>