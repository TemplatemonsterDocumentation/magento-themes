<h3>Olark Live Chat</h3>
<p>You need to register on the official <a href="http://special.olark.com/cDqdW" target="blank_">website</a> in order to use on-line chat built in the template, fill in all the required information and receive the code <strong>Olark Site-ID</strong>.</p>
<div class="alert alert-info"><span>You can get a free trial 6-month bronze package. In order to get it follow the <a href="http://olark.com/signup/create_new_account/18981?utm_source=templatemonster&utm_medium=offer&utm_campaign=default" target="blank_">link</a>, fill in your registration data.</span></div>
In order to configure the on-line chat, you need to perform the following steps:
<ol class="index-list">
	<li>
		Open Magento admin panel. Navigate to the CMS menu item and click it, in the drop-down menu navigate to the <strong>Widgets</strong> item.
		Once the new page is opened, click the <strong>Add new widget instance</strong> button.
		<figure class="img-polaroid"><img src="img/magento/olark-1.jpg" alt="" /></figure>
	</li>
	<li>
		In the drop-down menu <strong>Type</strong> select the Olark Live Chat, in the menu <strong>Design Package/Theme</strong> select your store skin to display the chat. Click the <strong>Continue</strong> button.
		<figure class="img-polaroid"><img src="img/magento/olark-2.jpg" alt="" /></figure>
	</li>
	<li>
		Now you need to configure the display of the on-line chat. As a first step, in the field <strong>Widget Instance Title</strong> type in the name of the widget, which will be displayed in the admin panel. In the field <strong>Assign to Store Views</strong> you need to select the store view, which will display the chat. In the block <strong>Layout Updates</strong> click the <strong>Add Layout Update</strong> button. In the field <strong>Display On</strong> you have to select <strong>All pages</strong>. For the field <strong>Block Reference</strong> select the <strong>Main Content Area</strong>.
		<figure class="img-polaroid"><img src="img/magento/olark-3.jpg" alt="" /></figure>
	</li>
	<li>
		Follow the link <strong>Widget Options</strong> in the left sidebar. On the right side you will see the field in which you need to insert the <strong>Olark Site-ID</strong>. 
		Once your <strong>Olark Site-ID</strong> is filled in, click the <strong>Save</strong> button.
		<figure class="img-polaroid"><img src="img/magento/olark-4.jpg" alt="" /></figure>		
	</li>
	<li>
		In the new window navigate to <strong>System -> Cache Management</strong> menu item. In the block <strong>Cache Storage Management</strong> check <strong>Layouts</strong> and <strong>Blocks HTML output</strong> items. Make sure, that in the field <strong>Actions</strong> the <strong>Refresh</strong> item is selected, and click <strong>Submit</strong>.
		<figure class="img-polaroid"><img src="img/magento/olark-5.jpg" alt="" /></figure>
	</li>
</ol>						
<p><strong>All done!</strong> Now you can visit your website and see the widget for the on-line chat.</p>
