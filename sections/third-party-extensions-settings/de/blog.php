<h3>Blog</h3>			
Sie haben die Möglichkeit den Blog(<a href="http://ecommerce.aheadworks.com/?cmid=SVhUd01JTTdDZ1k9&afid=Y0ZHc2wwaWlRYkU9&ats=YnJLMTBpcnFJ­N2M9">AHEADWORKS EXTENSIONS</a>) zu Ihrer Webseite hinzuzufügen.
<ol class="index-list">
	<li>
		Die Einstellungen für den Blog befinden sich im Menü <strong>Blog > Settings</strong>, oder <strong>System > Configuration > AHEADWORKS EXTENSIONS > Blog</strong>.
		<figure class="img-polaroid"><img src="img/magento/blog-1.jpg" alt="" /></figure>
	</li>
	<li>
		In dem ersten Tab, <strong>Blog > Settings</strong>, befinden sich die Grundeinstellungen für den Blog.
		<figure class="img-polaroid"><img src="img/magento/blog-2.jpg" alt="" /></figure>
	</li>
	<li>
		Andere Tabs ermöglichen die folgenden Funktion einzustellen: Beitragsansicht, Datumsanzeige, Anzeige von Kommentaren, Captcha und Nachrichten.
		<figure class="img-polaroid"><img src="img/magento/blog-3.jpg" alt="" /></figure>
	</li>				
</ol>					