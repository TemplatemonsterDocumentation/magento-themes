<h3>Mega menu</h3>
<p>Das jeweilige Modul ersetzt ein Standardmenü durch ein Menü, in dem man die Darstellung von Unterkategorien bis zu zwei Ebenen einstellen, statische Blöcke zum DropDownMenü, Labels (Labels) hinzufügen, Produkte anzeigen usw. kann.</p>
<strong>MegaMenu</strong> ist in Vorlagen standardmäßig aktiviert, aber es kan im Menü <strong>System > Configuration > CMSMART MEGAMENU > Mega Menu</strong> aktiviert oder deaktiviert werden.

