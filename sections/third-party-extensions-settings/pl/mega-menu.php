<h3>Mega menu</h3>
<p>Ten moduł zamienia standardowe menu, co pozwala na konfigurowanie wyświetlania dwóch poziomów podkategorii, dodawanie statycznych bloków do menu rozwijanego, dodawania etykiet, wyświetlania produktów, itp.</p>
<strong>MegaMenu</strong> jest zawarte w szablonie domyślnie, możesz je włączyć/wyłączyć w <strong>System > Configuration > CMSMART MEGAMENU > Mega Menu</strong>.

<ol class="index-list">
	<li>Sposób wyswietlania każdego rozwijanego menu może być konfigurowane na stronie ustawień kategorii, znajdujących się w <strong>Catalog > Manage Categories</strong>.
		<figure class="img-polaroid"><img src="img/magento/mega-1.jpg" alt="" /></figure>
		
		<figure class="img-polaroid"><img src="img/magento/mega-2.jpg" alt="" /></figure>
	</li>
	<li>
		Aby dodać statyczny blok do rozwijanego menu, musisz dodać statyczny blok w <strong>CMS > Static Blocks</strong> i po utworzeniu, dodać go do menu.
		<figure class="img-polaroid"><img src="img/magento/mega-3.jpg" alt="" /></figure>
	</li>
	<li>
		Moduł pozwala na dodawanie etykiet do jakiejkolwiek kategorii.
		<figure class="img-polaroid"><img src="img/magento/mega-5.jpg" alt="" /></figure>
	</li>		
	<li>
		Można tego dokonać w menu kategorii <strong>Catalog > Manage Categories</strong>.
		<figure class="img-polaroid"><img src="img/magento/mega-4.jpg" alt="" /></figure>
	</li>	
