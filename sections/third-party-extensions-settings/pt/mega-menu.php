<h3>Mega menu</h3>
<p>Este módulo substitui o menu padrão por um menu que permite configurar dois níveis de subcategorias, incluir blocos estáticos ao menu, incluir etiquetas, mostrar produtos, etc.</p>
<strong>MegaMenu</strong> é incluído no template por padrão, você pode ativar/desativar este módulo em <strong>System > Configuration > MEGAMENU > Mega Menu</strong>.

<ol class="index-list">
	<li>A aparência de cada menu pode ser configurada na página de definições da categoria, localizada em <strong>Catalog > Manage Categories</strong>.
		<figure class="img-polaroid"><img src="img/magento/mega-1.jpg" alt="" /></figure>	
	</li>
	<li>
		Para adicionar blocos estáticos ao menu, você precisa adicionar blocos estáticos em <strong>CMS > Static Blocks</strong> e, após criados, adicioná-los ao menu.
		<figure class="img-polaroid"><img src="img/magento/mega-2.jpg" alt="" /></figure>
	</li>	
	<li>
		O módule permite incluir uma etiqueta para qualquer categoria. Você pode realizar esta inclusão no menu de categorias <strong>Catalog > Manage Categories</strong>.
		<figure class="img-polaroid"><img src="img/magento/mega-3.jpg" alt="" /></figure>
	</li>	
	<li>
		Para mudar a quantidade de colunas em subcategorias você precisa mudar o valor de "Level column Count" no menu <strong>Catalog > Manage Categories</strong>.
		<figure class="img-polaroid"><img src="img/magento/mega-4.jpg" alt="" /></figure>
	</li>			
</ol>