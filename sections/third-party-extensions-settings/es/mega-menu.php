<h3>Mega menu</h3>
<p>El módulo sustituye el menú estándar por el menú que permite configurar la visualización en dos niveles de subcategorías, añadir bloques estáticos para el menú desplegable, añadir etiquetas, visualización de productos, etc.</p>
<strong>MegaMenu</strong> está incluido en la plantilla por defecto, se puede activar/desactivar en <strong>System > Configuration > CMSMART MEGAMENU > Mega Menu</strong>.

<ol class="index-list">
	<li>La visualización de cada menú desplegable se puede configurar en la página de configuración de las categorías, que se encuentra en <strong>Catalog > Manage Categories</strong>.
		<figure class="img-polaroid"><img src="img/magento/mega-1.jpg" alt="" /></figure>
		
		<figure class="img-polaroid"><img src="img/magento/mega-2.jpg" alt="" /></figure>
	</li>
	<li>
		Para añadir un bloque estático a un menú desplegable, es necesario añadir el bloque estático en <strong>CMS > Static Blocks</strong> y una vez que se ha creado añadirlo al menú.
		<figure class="img-polaroid"><img src="img/magento/mega-3.jpg" alt="" /></figure>
	</li>
	<li>
		El módulo le permite añadir etiquetas a cualquier categoría.
		<figure class="img-polaroid"><img src="img/magento/mega-5.jpg" alt="" /></figure>
	</li>		
	<li>
		Puede hacerlo en el menú de categorías <strong>Catalog > Manage Categories</strong>.
		<figure class="img-polaroid"><img src="img/magento/mega-4.jpg" alt="" /></figure>
	</li>	
	<li>
		Para cambiar la cantidad de columnas en subcategorías 
		<figure class="img-polaroid"><img src="img/magento/mega-7.jpg" alt="" /></figure>
		<p>necesita cambiar el valor del número de la columna de nivel en el menú <strong>Catalog > Manage Categories</strong>.</p>
		<figure class="img-polaroid"><img src="img/magento/mega-6.jpg" alt="" /></figure>
	</li>			
</ol>