<h3>Blog</h3>			
Hay una oportunidad para añadir un Blog(<a href="http://ecommerce.aheadworks.com/?cmid=SVhUd01JTTdDZ1k9&afid=Y0ZHc2wwaWlRYkU9&ats=YnJLMTBpcnFJ­N2M9">AHEADWORKS EXTENSIONS</a>) a su sitio web.
<ol class="index-list">
	<li>
		Los ajustes del blog se pueden encontrar en <strong>Blog > Settings</strong>, o <strong>System > Configuration > AHEADWORKS EXTENSIONS > Blog</strong>.
		<figure class="img-polaroid"><img src="img/magento/blog-1.jpg" alt="" /></figure>
	</li>
	<li>
		La primera pestaña <strong>Blog > Settings</strong>, incluye los ajustes básicos del blog.
		<figure class="img-polaroid"><img src="img/magento/blog-2.jpg" alt="" /></figure>
	</li>
	<li>
		Las otras pestañas le permiten configurar: tipo de registro, mostrar fecha, mostrar comentarios, captcha y activar fuentes de noticias.
		<figure class="img-polaroid"><img src="img/magento/blog-3.jpg" alt="" /></figure>
	</li>				
</ol>					