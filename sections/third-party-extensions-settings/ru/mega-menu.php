	<li>Настроить отображение каждого выпадающего меню можно на странице настройки категории, которую вы можете найти в <strong>Catalog > Manage Categories</strong>.
		<figure class="img-polaroid"><img src="img/magento/mega-1.jpg" alt="" /></figure>		
		<figure class="img-polaroid"><img src="img/magento/mega-2.jpg" alt="" /></figure>
	</li>
	<li>
		Чтобы добавить статик блок в выпадающее меню, вам необходимо добавить статик блок в <strong>CMS > Static Blocks</strong> и только после этого добавить его в меню.
		<figure class="img-polaroid"><img src="img/magento/mega-3.jpg" alt="" /></figure>
	</li>
	<li>
		В модуле есть возможность добавить бэйдж для любой категории.
		<figure class="img-polaroid"><img src="img/magento/mega-5.jpg" alt="" /></figure>
	</li>		
	<li>
		Сделать это вы можете в меню категорий <strong>Catalog > Manage Categories</strong>.
