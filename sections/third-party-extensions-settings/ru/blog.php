<h3>Blog</h3>			
<p>Вы имеете возможность добавить блог(<a href="http://ecommerce.aheadworks.com/?cmid=SVhUd01JTTdDZ1k9&afid=Y0ZHc2wwaWlRYkU9&ats=YnJLMTBpcnFJ­N2M9">AHEADWORKS EXTENSIONS</a>) на Ваш сайт.</p>
<ol class="index-list">
	<li>
		Настройки блога находятся в меню <strong>Blog > Settings</strong>, или <strong>System > Configuration > AHEADWORKS EXTENSIONS > Blog</strong>.
		<figure class="img-polaroid"><img src="img/magento/blog-1.jpg" alt="" /></figure>
	</li>
	<li>
		На первой вкладке, <strong>Blog > Settings</strong>, находятся основные настройки блога.
		<figure class="img-polaroid"><img src="img/magento/blog-2.jpg" alt="" /></figure>
	</li>
	<li>
		Остальные вкладки позволяют настроить: вид записей, вывод даты, вывод комментариев, включение капчи и ленты новостей.
		<figure class="img-polaroid"><img src="img/magento/blog-3.jpg" alt="" /></figure>
	</li>				
