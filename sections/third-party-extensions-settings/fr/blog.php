<h3>Blog</h3>			
Il y a la possibilité d'ajouter Blog  à votre site Web.
<ol class="index-list">
	<li>
		Les paramètres de blog peuvent être trouvés dans <strong>Blog > Settings</strong>, ou <strong>System > Configuration > AHEADWORKS EXTENSIONS > Blog</strong>.
		<figure class="img-polaroid"><img src="img/magento/blog-1.jpg" alt="" /></figure>
	</li>
	<li>
		Le premier onglet  <strong>Blog > Settings</strong>, comprend les paramètres de base du blog.
		<figure class="img-polaroid"><img src="img/magento/blog-2.jpg" alt="" /></figure>
	</li>
	<li>
		Le reste des onglets permettent de configurer:  le type d'enregistrement, l'affichage de la date, les commentaires d'affichage,  un texte CAPTCHA et  une fonction de diffusion de nouvelles (RSS).
		<figure class="img-polaroid"><img src="img/magento/blog-3.jpg" alt="" /></figure>
	</li>				
</ol>					