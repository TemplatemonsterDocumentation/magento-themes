<h3>Mega menu</h3>
<p>Le module donné substitue le menu standard au menu qui permet de configurer l'affichage des deux sous-catégories à ce niveau, d'ajouter des blocs statiques au menu déroulant, d'ajouter des étiquettes, d'afficher les produits, etc.</p>
<strong>MegaMenu</strong> est inclus en modèle par défaut, vous pouvez activer / désactiver dans <strong>System > Configuration > CMSMART MEGAMENU > Mega Menu</strong>.

<ol class="index-list">
	<li>L'affichage de chaque menu déroulant peut être configuré sur la page de paramètres d'une catégorie, qui est situé dans <strong>Catalog > Manage Categories</strong>.
		<figure class="img-polaroid"><img src="img/magento/mega-1.jpg" alt="" /></figure>
		
		<figure class="img-polaroid"><img src="img/magento/mega-2.jpg" alt="" /></figure>
	</li>
	<li>
		Pour ajouter un bloc statique au menu déroulant, vous devez l'ajouter dans  <strong>CMS > Static Blocks</strong> et une fois qu'il a été créé, ajouter ce bloc au menu.
		<figure class="img-polaroid"><img src="img/magento/mega-3.jpg" alt="" /></figure>
	</li>
	<li>
		Le module permet d'ajouter l'étiquette pour n'importe quelle catégorie.
		<figure class="img-polaroid"><img src="img/magento/mega-5.jpg" alt="" /></figure>
	</li>		
	<li>
		Vous pouvez le faire dans le menu des catégories <strong>Catalog > Manage Categories</strong>.
		<figure class="img-polaroid"><img src="img/magento/mega-4.jpg" alt="" /></figure>
	</li>	
	<li>
		Afin de changer la quantité de colonnes dans les sous-catégories
		<figure class="img-polaroid"><img src="img/magento/mega-7.jpg" alt="" /></figure>
		<p>vous devez changer la Valeur du nombre de colonnes dans le menu <strong>Catalog > Manage Categories</strong>.</p>
		<figure class="img-polaroid"><img src="img/magento/mega-6.jpg" alt="" /></figure>
	</li>			
</ol>