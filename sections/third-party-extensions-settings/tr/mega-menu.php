<h3>Mega menü</h3>
<p>Bu modül, iki aşamalı alt kategorilerin ayarlanmasını sağlayan menüyü temsil eder. Buradan, açılır menüye sabit kutular ve seviyeler ekleyebilir, ürünlerin nasıl görüntüleneceğini ayarlayabilirsiniz.</p>
<strong>MegaMenu</strong> şablonun içinde hali hazırda yer almaktadır. Açıp kapatmak için <strong>Sistem > Ayarlar > MEGAMENU > Mega Menu</strong> kısmına girebilirsiniz.

<ol class="index-list">
	<li>Tüm açılan menüleri <strong>Katalog > Kategorileri Düzenle</strong> kısmında yer alan, kategori ayar sayfasından düzenleyebilirsiniz.
		<figure class="img-polaroid"><img src="img/magento/mega-1.jpg" alt="" /></figure>
	</li>
	<li>
		Açılır menüye sabit kutu eklemek için <strong>CMS > Static Blocks</strong> kısmından kutu oluşturup, ardından menüye eklemeniz gerekir.
		<figure class="img-polaroid"><img src="img/magento/mega-2.jpg" alt="" /></figure>
	</li>
	<li>
		Modül herhangi bir kategoriye seviye eklemenize izin verir. Bunu <strong>Katalog > Kategorileri Düzenle</strong> kısmındaki kategori menüsünden yapabilirsiniz.
		<figure class="img-polaroid"><img src="img/magento/mega-3.jpg" alt="" /></figure>
	</li>		
	<li>
		Alt kategorilerdeki sütun sayısını değiştirmek için seviye alanındaki Sayı kısmını <strong>Katalog > Kategorileri Düzenle</strong> bölümünden düzenleyebilirsiniz.
		<figure class="img-polaroid"><img src="img/magento/mega-4.jpg" alt="" /></figure>
	</li>			
</ol>