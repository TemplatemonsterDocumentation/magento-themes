<h3>Background Video</h3>
Arkaplanda video oynatmak için, Youtube videolarının gösterilmesini sağlayan <a href="https://github.com/pupunzi/jquery.mb.YTPlayer/wiki" target="_blank">jquery.mb.YTPlayer</a> plug-inini kullanmalısınız.
Video <strong>jquery.mb.YTPlayer.js</strong> dosyası ve ilgili nitelikler eklenerek, sabit bir kutuya yerleştirilebilir.
<figure class="img-polaroid"><img src="img/magento/video.jpg" alt="" /></figure>
<p><strong>Data-property</strong> (veri-özelliği) için temel parametreler şu şekildedir:</p>
<table  class="options-table">
	<tr>
		<td class="col-1"><strong>videoURL</strong></td>
		<td>-</td>
		<td>arkaplan videosu olarak kullanmak istediğiniz videonun bağlantı adresi.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>quality</strong></td>
		<td>-</td>
		<td>videonun görüntü kalitesi (‘default’ veya “small”, “medium”, “large”, “hd720”, “hd1080”, “highres”).</td>
	</tr>
	<tr>
		<td class="col-1"><strong>containment</strong></td>
		<td>-</td>
		<td>varsayılan olarak, eğer scriptte ilklendirilmişse video gösterilir ve kutu için “self” seçili durumdadır.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>opacity</strong></td>
		<td>-</td>
		<td>videonun transaranlığı için 0 ile 1 arası değer seçilir.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>loop</strong></td>
		<td>-</td>
		<td>videonun tekrar oynatılıp oynatılmayacağı buradan ayarlanır.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>showControls</strong></td>
		<td>-</td>
		<td>kontrol tuşlarının gösterilip gösterilmeyeceği buradan ayarlanır.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>mute</strong></td>
		<td>-</td>
		<td>sessiz video gösterimi.</td>
	</tr>			
	<tr>
		<td class="col-1"><strong>startAt</strong></td>
		<td>-</td>
		<td>videonun başlatılacağı saniye buradan ayarlanır.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>stopAt</strong></td>
		<td>-</td>
		<td>videonun durdurulacağı saniye buradan ayarlanır.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>autoplay</strong></td>
		<td>-</td>
		<td>videonun otomatik olarak başlatılıp başlatılmayacağı buradan ayarlanır.</td>
	</tr>
</table>		