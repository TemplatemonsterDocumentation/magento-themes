<h3>Blog</h3>			
Hai la possibilità di aggiungere un blog(<a href="http://ecommerce.aheadworks.com/?cmid=SVhUd01JTTdDZ1k9&afid=Y0ZHc2wwaWlRYkU9&ats=YnJLMTBpcnFJ­N2M9">AHEADWORKS EXTENSIONS</a>) al tuo sito web.
<ol class="index-list">
	<li>
		Le impostazioni per il blog si trovano su <strong>Blog > Settings</strong>, oppure su <strong>System > Configuration > AHEADWORKS EXTENSIONS > Blog</strong>.
		<figure class="img-polaroid"><img src="img/magento/blog-1.jpg" alt="" /></figure>
	</li>
	<li>
		La prima scheda <strong>Blog > Settings</strong>, include le impostazioni di base del blog.
		<figure class="img-polaroid"><img src="img/magento/blog-2.jpg" alt="" /></figure>
	</li>
	<li>
		Le altre schede permettono di configurare: tipo di record, visualizzazione dati, visualizzazione commenti, captcha e abilitazione newsfeed.
		<figure class="img-polaroid"><img src="img/magento/blog-3.jpg" alt="" /></figure>
	</li>				
</ol>					