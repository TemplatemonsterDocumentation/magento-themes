<h2 class="item1">Extension settings</h2>
<p>In order to configure this module, you should navigate to Magento admin panel and choose the <strong>Stores > Configuration</strong> section in the left side menu.</p>
<figure class="img-polaroid"><img src="img/magento/search-1.png" alt="" /></figure>
<p>Then choose the <strong> TemplateMonster > Catalog Search Ajax item </strong> in the left side menu to go to the module settings interface.</p>
 <figure class="img-polaroid"><img src="img/magento/search.png" alt="" /></figure>


<p>This module has 6 options available for editing if needed.<br>
    Let’s review each option separately:</p>            
<table class="options-table">
    <tr>
        <td class="col-1"><strong>Default Search Ajax</strong></td>
        <td>-</td>
        <td>enables/disables search by keywords,  specified in the “Search Terms” shop section.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>Product Search Ajax</strong></td>
        <td>-</td>
        <td>enables/disables search by products’ titles, assigned to all of active site categories.</td>
    </tr>                   
    <tr>
        <td class="col-1"><strong>Category Search Ajax</strong></td>
        <td>-</td>
        <td>enables/disables search by all of active site categories titles.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>Category Search Ajax Num. Result</strong></td>
        <td>-</td>
        <td>defines the number of categories to show in the search results list.</td>
    </tr>   
    <tr>
        <td class="col-1"><strong>Default Search Ajax Num. Result</strong></td>
        <td>-</td>
        <td>defines the number of products to show in the search results list while searching by a keyword.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>Product Search Ajax Num. Result</strong></td>
        <td>-</td>
        <td>defines the number of products to show in the search results list.</td>
    </tr>   
</table>

<div class="alert alert-danger">Attention!!! <br>
If you decide to enable Category Search Ajax and Product Search Ajax options, you’ll also need to enable <strong>Use Flat Catalog Category</strong> and <strong>Use Flat Catalog Product</strong> options respectively. You can enable these options  at the <strong>Catalog - > Catalog -> Storefront tab</strong>.
<figure class="img-polaroid"><img src="img/magento/search-2.png" alt="" /></figure></div>
<p>Feel free to use all the options at once or just some of them, it’s up to you. In case there are any suggestions or errors detected, feel free to contact us using the link provided on the module page!</p>
<p>Enjoy using!</p>