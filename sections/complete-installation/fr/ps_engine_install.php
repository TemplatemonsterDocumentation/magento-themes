<h3>Installation de Magento</h3>
<p>Lorsque vous avez terminé avec le téléchargement de fichiers, vous pouvez démarrer le processus d'installation du moteur Magento. Ouvrez votre navigateur et dans la barre d'adresse tapez votre <strong>'domain name/path to your Magento directory'</strong>. Vous devriez voir l'écran d'accueil. Veuillez suivre les instructions ci-dessous pour installer Magento. </p>
<div class="alert alert-warning"><span>NOTEZ: le fichier <strong>dump.sql.gz</strong> devrait être importé dans la base de données vide AVANT d'installer Magento.</span></div>

<h4>Étape 1: Assistant d'installation</h4>
<p>Veuillez lire l'accord de licence et cliquez sur la case "Agree and Setup Magento" si vous acceptez les termes. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-1.jpg" alt="" /></figure>
<h5>Vérification de serveur</h5>
<p>Cliquez sur le bouton "Start Readiness Check" pour vérifier la compatibilité de vos paramètres du serveur avec les exigences de moteur Magento 2. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-2.jpg" alt="" /></figure>
<p>Lorsque la procédure de vérification est terminée, cliquez sur le bouton '<strong>Next</strong>'. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-3.jpg" alt="" /></figure>

<h4>Étape 2: Connexion de base de données</h4>
<p>Ici, vous devez saisir les détails de la base de données et des paramètres initiaux. 
<strong>Add a Database</strong> - entrez votre nom d'hôte, nom de base de données, l'utilisateur et le mot de passe base de données. Contactez votre fournisseur d'hébergement, si vous ne possédez pas les détails nécessaires.</p>
<p>Assurez-vous que vous avez une base de données créée pour l'installation Magento. Et assurez-vous qu'elle est vide, sans tables. 
<figure class="img-polaroid"><img src="img/magento/how-to-install-4.jpg" alt="" /></figure>
<p>Si tout est correct, cliquez sur le bouton '<strong>Next</strong>'. </p>

<h4>Étape 3: Web Configuration</h4>
<p>Here you can change Magento admin panel URL and the main store address. Other settings you can leave unchanged. </p>
<div class="alert alert-warning">Make a point of the admin panel path. Note it or insert your specific admin panel URL to remember the admin panel access.</span></div>
<figure class="img-polaroid"><img src="img/magento/how-to-install-5.jpg" alt="" /></figure>
<p>When you are done, click '<strong>Next</strong>'. </p>

<h4>Étape 4: Localisation</h4>
<p>Sélectionnez le fuseau horaire requis, devise par défaut et la langue du magasin. Dans la section <strong>Advanced Modules Configurations</strong> vous pouvez également sélectionner des modules par défaut qui doivent/ne doivent pas être installés. Nous vous recommandons de ne pas effectuer des changements dans cette section. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-6.jpg" alt="" /></figure>
<p>Lorsque vous avez terminé, cliquez sur le bouton '<strong>Next</strong>'.</p>

<h4>Étape 5: Créer un compte Administrateur</h4>
<p>Ici, vous devez entrer des données personnelles pour le propriétaire du magasin et l'administrateur. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-7.jpg" alt="" /></figure>

<h4>Étape 6: Installer</h4>
<p>La configuration de magasin est effectuée. Appuyez sur le bouton <strong>Install Now</strong> pour démarrer le processus d'installation du magasin. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-8.jpg" alt="" /></figure>

<h4>Étape 7: Succès</h4>
<p>C'est une étape finale de l'installation. Copiez la clé de chiffrement générée et la garder en sécurité. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-9.jpg" alt="" /></figure>