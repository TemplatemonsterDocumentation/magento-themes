		
<h3>Création d'une base de données</h3>
<p>D'abord, vous devez préparer une nouvelle base de données pour votre boutique Magento. Vous pouvez créer la nouvelle base de données à l'aide de l'outil de gestion de base de données du panneau de votre hébergeur (<strong>généralement PhpMyAdmin</strong>).</p>
<p>Les données suivantes seront reçues pendant la création d'une base de données: nom de la base de données, nom de l'utilisateur de la base de données, mot de passe et nom du serveur. Vous devez conserver ces données car vous en aurez besoin plus tard lors de l'installation de Magento. </p>

<p>Avec l'outil phpMyAdmin, vous pourrez créer une nouvelle base de données en 3 étapes simple (voir les slides ci-dessous): </p>

<ol class="index-list">
	<li>
		Ouvrez l'onglet <strong>"Database"</strong>, vous verrez une liste de bases de données.
		<figure class="img-polaroid"><img src="img/magento/database-create-1.jpg" alt="" /></figure>
	</li>
	<li>
		Entrez le nom de votre base de données. Cliquez sur le bouton <strong>"Create"</strong>.
		<figure class="img-polaroid"><img src="img/magento/database-create-2.jpg" alt="" /></figure>
	</li>
	<li>
		<p>Votre base de données apparait dans la liste. Pour entrer dans la base de données, cliquez sur son nom dans la liste.</p>
		<figure class="img-polaroid"><img src="img/magento/database-create-3.jpg" alt="" /></figure>
	</li>
</ol>

<p>Vous pouvez également consulter le tutoriel vidéo détaillé sur <a href="/help/how-to-create-a-database.html" target="_blank"> Comment créer une base de données</a>.</p>

<div class="alert alert-warning"><span>Si vous avez des problèmes lors de la création de la base de données, veuillez contacter votre hébergeur pour demander de l'assistance.</span></div>		