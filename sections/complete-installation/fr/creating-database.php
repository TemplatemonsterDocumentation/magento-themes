		
<h3>Création d'une base de données</h3>
<p>D'abord, vous devez préparer une nouvelle base de données pour votre boutique Magento. Vous pouvez créer la nouvelle base de données à l'aide de l'outil de gestion de base de données du panneau de votre hébergeur (<i>généralement PhpMyAdmin</i>).</p>
<p>Les données suivantes seront reçues pendant la création d'une base de données: nom de la base de données, nom de l'utilisateur de la base de données, mot de passe et nom du serveur. Vous devez conserver ces données car vous en aurez besoin plus tard lors de l'installation de Magento. </p>

<p>Avec l'outil phpMyAdmin, vous pourrez créer une nouvelle base de données en 3 étapes simple (voir les slides ci-dessous): </p>

<ol class="index-list">
	<li>Cliquez sur le bouton Database du menu de navigation du haut pour accéder à la liste de bases de données.</li>
	<li>Entrez le nom de la base de données dans le champ <strong>"Create new database"</strong>.</li>
	<li>Cliquez sur le bouton <strong>Create</strong> sur le côté droit.</li>
</ol>

<p>Votre base de données est maintenant visible dans la liste des bases de données. Cliquez sur son nom pour accéder à la base de données:</p>
<p>Ouvrez l'onglet <b>"Database"</b>, vous verrez une liste de bases de données.</p>
<figure class="img-polaroid"><img src="img/magento/database-create-1.jpg" alt="" /></figure>	
<p>Entrez le nom de votre base de données. Cliquez sur le bouton <b>"Create"</b>.</p>
<figure class="img-polaroid"><img src="img/magento/database-create-2.jpg" alt="" /></figure>
<p>Votre base de données apparait dans la liste. Pour entrer dans la base de données, cliquez sur son nom dans la liste.</p>
<figure class="img-polaroid"><img src="img/magento/database-create-3.jpg" alt="" /></figure>
			

