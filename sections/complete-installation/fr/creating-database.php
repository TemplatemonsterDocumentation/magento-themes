<h3>La création d'une base de données</h3>
<p>Tout d'abord, vous devez préparer une nouvelle base de données pour votre magasin Magento. Vous pouvez créer la nouvelle base de données en utilisant l'outil de gestion de base de données de votre panneau de contrôle d'hébergement (<strong>habituellement PhpMyAdmin</strong>).</p>
<p>Les données suivantes seront reçues lors de la création d'une base de données: nom de base de données, nom d'utilisateur de base de données, mot de passe et nom de votre serveur. Vous devez enregistrer ces données parce que vous en aurez besoin pour installation de Magento. </p>

<p>Avec l'outil phpMyAdmin, vous serez en mesure de créer une nouvelle base de données en 3 étapes simples (voir les diapositives ci-dessous): </p>

<ol class="index-list">
	<li>
		<p>Ouvrez l'onglet <strong>"Database"</strong>, vous verrez la liste des bases de données.</p>
		<figure class="img-polaroid"><img src="img/magento/database-create-1.jpg" alt="" /></figure>
	</li>
	<li>
		<p>Entrez <strong>nom de base de données</strong>. Cliquez le bouton <strong>"Create"</strong>.</p>
		<figure class="img-polaroid"><img src="img/magento/database-create-2.jpg" alt="" /></figure>
	</li>
	<li>
		<p>Votre base de données est apparu dans la liste.  Pour entrer dans la base de données, cliquez sur son nom dans la liste.</p>
		<figure class="img-polaroid"><img src="img/magento/database-create-3.jpg" alt="" /></figure>
	</li>
</ol>

<p>Vous pouvez également consulter le tutoriel vidéo détaillé sur <a href="/help/how-to-create-a-database.html" target="_blank">Comment créer une base de données (How to create a database)</a></p>

<div class="alert alert-warning"><span>Si vous avez des problèmes avec la création d'une base de données, veuillez contacter hébergeur pour obtenir l'aide et l'assistance.</span></div>
		