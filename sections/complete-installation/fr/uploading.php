<h3>Téléchargement et décompression</h3>

<p>Pour commencer à travailler avec Magento vous avez besoin de télécharger des fichiers sur votre serveur d'hébergement. Cela peut être fait à l'aide de votre gestionnaire de fichiers d'hébergement ou d'un gestionnaire FTP tierce.</p>
            
<ol class="index-list">
  <li>Selectionnez les fichiers <strong>'unzip.php'</strong> et <strong>'fullpackage.zip'</strong>  et téléchargez les sur votre serveur (
    <a href="/help/how-upload-files-server-2.html" target="_blank">How to upload files to server</a>).</li>
  <li>Tapez le chemin vers le fichier <strong>'unzip.php'</strong> sur votre serveur (http://your_domain_name/unzip.php) dans votre <abbr title="Internet Explorer, Google Chrome, Mozilla Firefox, Opera, Safari etc.">navigateur</abbr>.</li>
  <li>
  	Vous devriez voir l'écran suivant:
  	<figure class="img-polaroid"><img src="img/magento/complete-install-unzip-php.jpg" alt="Unzip.php initial screen."></figure>
  </li>
  <li>Dans la zone de sélection <strong>'Choose your zip file'</strong> veuillez sélectionner le fichier <strong>fullpackage.zip</strong>.</li>
  <li>Dans le champ <strong>'Unzip to'</strong> spécifiez le répertoire dans lequel vous souhaitez extraire les fichiers.</li>
  <li>Cliquez sur le bouton <strong>'Unzip'</strong> pour continuer.</li>
</ol>
          
<p class="alert alert-warning"><span>Assurez-vous de mettre les permissions correctes pour le répertoire dans lequel vous voulez extraire les fichiers. Permissions doivent être <strong>CHMOD 755 ou 777</strong> selon la configuration de votre serveur. </span></p>
		
