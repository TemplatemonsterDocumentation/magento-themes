		
<h3>Téléchargement et décompression</h3>

<p>Pour commencer à travailler avec Magento, vous devez envoyer les fichiers sur votre serveur d'hébergement. Ceci se fait en utilisant le gestionnaire de fichiers de l'hébergeur, ou avec un gestionnaire FTP de tierce partie.</p>
            
<ol>
  <li>Sélectionnez les fichiers <b>'unzip.php'</b> et <b>'fullpackage.zip'</b> et envoyez-les sur votre serveur (<i class="icon-film"> </i> <a href="/help/how-upload-files-server-2.html" target="_blank">Comment envoyer des fichiers sur un serveur.</a>).</li>
  <li>Pointez votre <abbr title="Internet Explorer, Google Chrome, Mozilla Firefox, Opera, Safari etc.">navigateur</abbr> sur l'adresse du fichier « unzip.php » sur votre serveur (<i class="muted">http://votre_nom_de_domaine/unzip.php</i>).</li>
  <li>Vous devriez voir la page suivante :</li>
</ol>
          
<figure class="img-polaroid"><img src="img/magento/complete-install-unzip-php.jpg" alt="Unzip.php initial screen."></figure>
<p class="muted text-center">Img 1. Page principale de Unzip.php.</p>
          
<ol start="5">
  <li>Sélectionnez le fichier <strong>fullpackage.zip</strong> que vous avez envoyé dans la boîte <strong>« Choisissez votre fichier zip » (« Choose your zip file »)</strong>.</li>
  <li>Dans le champ <strong>« Décompresser vers » (« Unzip to »)</strong>, spécifiez le répertoir où vous voulez extraire les fichiers.</li>
  <li>Cliquez sur le bouton <strong>« Décompresser » (« Unzip »)</strong> pour continuer.</li>
</ol>
          
<p class="alert alert-warning"><span>Assurez-vous d'avoir les permissions nécessaires dans le répertoire où vous voulez extraire les fichiers. Les permissions devraient être <strong>CHMOD 755 ou 777</strong> en fonction de la configuration de votre serveur.</span></p>