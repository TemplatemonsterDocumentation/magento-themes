<h2> Instalación completa </h2>
<p>El método de instalación completa se puede utilizar si desea configurar una tienda Magento utilizando una muestra del contenido. Suponemos que ha completado todos los pasos de preparación.</p>
<p>El proceso de instalación consta de 3 etapas:</p>
<ol class="index-list">
    <li>La creación de una base de datos.</li>
    <li>Subir todos los archivos necesarios en el servidor.</li>
    <li>La instalación del motor de Magento Commerce junto con la plantilla que ha comprado.</li>
</ol>
<p>Vamos a comenzar!</p>