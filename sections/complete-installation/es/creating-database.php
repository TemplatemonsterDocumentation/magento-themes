		
<h3>Creaci&oacute;n de una base de datos</h3>
<p>En primer lugar, es necesario preparar una nueva base de datos para su tienda de Magento. Puede crear la nueva base de datos utilizando la herramienta de gesti&oacute;n de base de datos desde el panel de control del hosting (<i>normalmente PhpMyAdmin</i>).</p>
<p>Los siguientes datos ser&aacute;n recibidos durante la creaci&oacute;n de una base de datos: nombre de la base de datos, nombre de usuario, contrase&ntilde;a y nombre de servidor. Tendr&aacute; que guardar estos datos, ya que lo necesitar&aacute; para su posterior instalaci&oacute;n de Magento. </p>

<p>Con la herramienta phpMyAdmin usted ser&aacute; capaz de crear una nueva base de datos en 3 sencillos pasos (vea las siguientes diapositivas): </p>

<ol class="list">
	<li>Haga clic en el bot&oacute;n Database en el men&uacute; de navegaci&oacute;n superior para acceder a la lista de bases de datos.</li>
	<li>Introduzca el nombre de la base de datos en el campo "Create new database".</li>
	<li>Haga clic en el bot&oacute;n Create en el lado derecho.</li>
</ol>

<p>Ahora, su base de datos puede verse en la lista de bases de datos. Haga clic en su nombre para acceder a la base de datos:</p>
<p>Abra la pesta&ntilde;a <b>"Database"</b> , ver&aacute; la lista de bases de datos.</p>
<figure class="img-polaroid"><img src="img/magento/database-create-1.jpg" alt="" /></figure>	
<p>Introduzca el nombre de la base de datos. Haga clic en el bot&oacute;n <b>"Create"</b>.</p>
<figure class="img-polaroid"><img src="img/magento/database-create-2.jpg" alt="" /></figure>
<p>Su base de datos ha aparecido en la lista. Con el fin de entrar en la base de datos, haga clic en su nombre en la lista.</p>
<figure class="img-polaroid"><img src="img/magento/database-create-3.jpg" alt="" /></figure>
			

