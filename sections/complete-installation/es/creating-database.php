<h3>La creación de una base de datos</h3>
<p>En primer lugar, es necesario preparar una nueva base de datos para su tienda de Magento. Puede crear la nueva base de datos utilizando la herramienta de gestión de base de datos desde su panel de control de hosting (<strong>generalmente PhpMyAdmin</strong>).</p>
<p>Los siguientes datos se recibirán durante la creación de una base de datos: nombre de la base de datos, nombre de usuario de base de datos, contraseña y nombre de servidor. Tendrá que guardar estos datos como que los necesitará para su futura instalación de Magento. </p>

<p>Con la herramienta phpMyAdmin usted será capaz de crear una nueva base de datos en 3 sencillos pasos (consulte las diapositivas a continuación): </p>

<ol class="index-list">
	<li>
		<p>Abra la pestaña <strong>"Database"</strong>, verá la lista de bases de datos .</p>
		<figure class="img-polaroid"><img src="img/magento/database-create-1.jpg" alt="" /></figure>
	</li>
	<li>
		<p>Ingrese su <strong>nombre de la base de datos</strong>. Haga clic en el botón <strong>"Crear" ("Create")</strong>.</p>
		<figure class="img-polaroid"><img src="img/magento/database-create-2.jpg" alt="" /></figure>
	</li>
	<li>
		<p>La base de datos ha aparecido en la lista.  Para entrar en la base de datos, haga clic sobre su nombre en la lista.</p>
		<figure class="img-polaroid"><img src="img/magento/database-create-3.jpg" alt="" /></figure>
	</li>
</ol>

<p>También puede revisar el video tutorial detallado sobre <a href="/help/how-to-create-a-database.html" target="_blank"> Cómo crear una base de datos</a></p>

