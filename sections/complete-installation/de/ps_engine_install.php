<h3>Magento Installation</h3>
<p>Wenn Sie mit dem Hochladen von Dateien fertig sind, können Sie mit dem Magento Engine Installationsprozess starten. Öffnen Sie Ihren Browser und tippen Sie in die Adressleiste Ihren <strong>'Domainname/Pfad zu Ihrem Magento-Verzeichnis'</strong>. Sie sollten den Begrüßungsbildschirm sehen. Bitte befolgen Sie den folgenden Anweisungen um Magento zu installieren. </p>
<div class="alert alert-warning"><span>HINWEIS: die <strong>dump.sql.gz</strong> Datei soll in eine leere Datenbank importiert werde, BEVOR Sie Magneto installieren.</span></div>

<h4>Schritt 1: Installationsassistent </h4>
<p>Bitte lesen Sie die Lizenzvereinbarung und klicken Sie auf "Zustimmen und Magento kofigurieren", wenn Sie mit den Bedingungen einverstanden sind. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-1.jpg" alt="" /></figure>
<h5>Server-Bereitschaftsprüfung </h5>
<p>Klicken Sie den " Bereitschaftsprüfung starten(Start Readiness Check)" Knopf um Ihre Servereinstellungen Kompatibilität mit den Magento 2 Engineanforderungen zu überprüfen. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-2.jpg" alt="" /></figure>
<p>Wenn das Prüfungsverfahren abgeschlossen ist, klicken Sie den '<strong>Next</strong>'. Knopf. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-3.jpg" alt="" /></figure>

<h4>Schritt 2: Datenbankverbindung </h4>
<p>Hier müssen Ihre die Datenbank Details und Basiseinstellungen eigeben. 
<strong>Datenbank hinzufügen</strong> - geben Sie Ihren Hostnamen , Datenbanknamen , Datenbankbenutzer und Passwort ein. Kontaktieren Sie Ihren Hosting-Anbieter, wenn Sie nicht die erforderliche Information haben.</p>
<p>Stellen Sie sicher, dass Sie eine Datenbank für die Magento-Installation erstellt haben. Vergewissern Sie sich dass Ihre Datenbank leer und ohne Tabellen ist. 
<figure class="img-polaroid"><img src="img/magento/how-to-install-4.jpg" alt="" /></figure>
<p>Wenn alles korrekt ist, klicken Sie den '<strong>Next</strong>'. Knopf. </p>

<h4>Schritt 3: Web-Konfiguration</h4>
<p>Hier können Sie Magento Admin-Panel-URL und die Hauptladenadresse ändern . Weitere Einstellungen können Sie unverändert lassen. </p>
<div class="alert alert-warning">Legen Sie großen Wert auf den Admin-Panel-Pfad. Notieren Sie sich Ihre spezielle Admin-Panel URL , damit Sie sich den Admin-Panel Zugang merken.</span></div>
<figure class="img-polaroid"><img src="img/magento/how-to-install-5.jpg" alt="" /></figure>
<p>Wenn Sie fertig sind, klicken Sie den '<strong>Next</strong>'. Knopf. </p>

<h4>Schritt 4: Lokalisierung</h4>
<p>Wählen Sie die gewünschte Zeitzone , Währung und Laden Sprache. In der <strong>Advanced Modules Configurations</strong> Sektion können Sie auch Standardmodule auswählen, die installiert/ nicht installiert werden sollen.Wir empfeheln Ihnen keine Änderungen in dieser Sektion vorzunehmen. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-6.jpg" alt="" /></figure>
<p>Wenn Sie fertig sind, klicken Sie den '<strong>Next</strong>'. Knopf.</p>

<h4>Schritt 5: Admin Account erstellen</h4>
<p>Hier müssen Sie persönliche Daten für den Ladenbesitzer und Administrator eingeben. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-7.jpg" alt="" /></figure>

<h4>Schritt 6: Installierung</h4>
<p>Die Laden Konfiguration ist beendet. Klicken Sie den <strong>Install Now</strong> Knopf um den Laden Installationsprozess zu starten. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-8.jpg" alt="" /></figure>

<h4>Schritt 7: Erfolg</h4>
<p>Dies ist der letzte Installationsschritt. Kopieren Sie den generierten Codierungsschlüssel und bewahren Sie es sicher. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-9.jpg" alt="" /></figure>