		
<h3>Erstellung der Datenbank</h3>
<p>Zuerst sollen Sie die neue Datenbank für den Online-Shop von Magento vorbereiten. Sie können sie über das Datenbank-Verwaltungswerkzeug im Control Panel des Hosting (<i>normalerweise PhpMyAdmin</i>) erstellen.</p>
<p>Beim Erstellen der Datenbank erhalten Sie solche Daten: Namen der Datenbank, Benutzernamen, Passwort und Adresse des Datenbankservers. Speichern Sie diese Daten, weil Sie sie bei der Installation Magento brauchen. </p>

<p>Anhand phpMyAdmin ist es möglich, die Datenbank in 3 einfachen Schritten zu erstellen: (schauen Sie die folgenden Dias an)</p>

<ol class="index-list">
	<li>Klicken Sie den Button Database (Datenbank ) in dem oberen Navigationsmenü, um zur Liste der Datenbanken zu gelangen</li>
	<li>Geben Sie den Namen der Datenbank ins Feld "Create new database (Neue Datenbank erstellen)" ein</li>
	<li>Drücken Sie auf den Button rechts Create (Erstellen)</li>
</ol>

<p>Die Datenbank kann man jetzt in der Liste sehen. Klicken Sie auf ihren Namen, um den Zugang zur Datenbank zu erhalten:</p>
<p>Öffnen Sie den Tab <b>"Database (Datenbank)"</b>, um zur Liste der Datenbanken zu gelangen.</p>
<figure class="img-polaroid"><img src="img/magento/database-create-1.jpg" alt="" /></figure>	
<p>Geben Sie den Namen der Datenbank ein. Klicken Sie den Button <b>"Erstellen"</b>.</p>
<figure class="img-polaroid"><img src="img/magento/database-create-2.jpg" alt="" /></figure>
<p>Man kann jetzt die Datenbank in der Liste sehen.  Klicken Sie auf den Namen, um den Zugang zur Datenbank zu erhalten.</p>
<figure class="img-polaroid"><img src="img/magento/database-create-3.jpg" alt="" /></figure>
			

