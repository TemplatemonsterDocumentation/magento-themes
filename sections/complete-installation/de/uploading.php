		
<h3>Hochladen und entpacken</h3>

<p>Damit Sie mit Magento arbeiten können, müssen Sie zuerst Dateien auf Ihrem Hosting Server hochaden. Dafür verwenden Sie Ihren Hosting- Dateimanager oder einen FTP-Manager eines Drittanbieters.</p>
            
<ol class="index-list">
  <li>Wählen  <strong>'unzip.php'</strong> und <strong>'fullpackage.zip'</strong> Dateien und laden Sie die auf Ihrem Server hoch (
   <a href="/help/de/how-upload-files-server-2.html" target="_blank">Wie laden man Dateien auf einem Server hoch.</a>
    )</li>
  <li> Im <abbr title="Internet Explorer, Google Chrome, Mozilla Firefox, Opera, Safari etc."> Browser </abbr> tippen Sie den Pfad zur <strong>'unzip.php'</strong> Datei von Ihrem Server (http://your_domain_name/unzip.php).</li>
  <li>Es sollte das folgende Bildschirm erscheinen:
    <figure class="img-polaroid"><img src="img/magento/complete-install-unzip-php.jpg" alt="Unzip.php initial screen."></figure>
  </li>
  <li> Im Auswahlfeld <strong>'Wählen Ihre zip Datei aus'</strong> (eng. Choose your zip file) wählen Sie die herunterdeladene <strong>fullpackage.zip</strong> Datei aus. </li>
  <li>Im Feld <strong>'Entpacken in'</strong> (eng. Unzip to) geben Sie das Verzeichnis ein, wohin Sie Dateien extrahieren möchten.</li>
  <li>klicken Sie auf <strong>"Auspacken"</strong> (eng. 'Unzip'), um fortzufahren.</li>
</ol>
          
        
<p class="alert alert-warning"><span>Bitte achten Sie darauf, die  korrekten Zugriffsrechte das Verzeichnis, wohin Dateien entpackt werden sollen, zu setzen. Je nach der Serverkonfiguration  sollten die Zugriffsrechte <strong>CHMOD 755 oder 777</strong> sein.</span></p>
		
