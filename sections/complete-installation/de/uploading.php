<h3>Hochladen und Entpacken </h3>

<p>Um mit Magento zu arbeiten, müssen Sie Dateien auf Ihrem Hosting-Server hochladen.Dafür können Sie Ihren Hosting-Dateimanager oder einen FTP-Manager verwenden.</p>
            
<ol class="index-list">
  <li>Wählen Sie die <strong>'unzip.php'</strong> und <strong>'fullpackage.zip'</strong> Dateien und laden Sie diese zum Server hoch (
    <a href="/help/how-upload-files-server-2.html" target="_blank"> Wie man Dateien zum Server hochlädt </a>).</li>
  <li>Tippen Sie den Pfad zur <strong>'unzip.php'</strong> Datei auf Ihrem Server (http://your_domain_name/unzip.php) in Ihren <abbr title="Internet Explorer, Google Chrome, Mozilla Firefox, Opera, Safari etc.">Browser</abbr>ein.</li>
  <li>
  	Sie sollten den folgenden Bildschirm sehen:
  	<figure class="img-polaroid"><img src="img/magento/complete-install-unzip-php.jpg" alt="Unzip.php initial screen."></figure>
  </li>
  <li>In der <strong>'Choose your zip file'</strong> Wahlbox wählen Sie die hochgeladene <strong>fullpackage.zip Datei</strong>.</li>
  <li>Im <strong>'Unzip to'</strong> Feld geben Sie das Verzeichnis ein, in dem Sie die Dateien extrahieren möchten.</li>
  <li>Klicken SIe den <strong>'Unzip'</strong> Knopf um fortzufahren.</li>
</ol>
          
<p class="alert alert-warning"><span>Bitte stellen Sie sicher, dass Sie die richtigen Berechtigungen für das Verzeichnis haben, wo Sie die Dateien auspacken wollen . Berechtigungen sollten <strong>CHMOD 755 oder 777</strong> sein, abhängig von der Serverkonfiguration . </span></p>
		
