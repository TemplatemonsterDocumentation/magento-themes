		
<h3>Sample data import</h3>
<p>Magento template package contains the sample data file (<b>dump.sql</b> or <b>dump.gz</b>). This file is located under the <b>/sources/sample_data</b> folder of your template. It allows you to load sample data, so that your website looks exactly like our demo template.</p>
<p class="alert alert-danger"><span>Do not import <b>dump.sql.gz</b> if you already have the data on your site. By importing <b>dump.sql.gz</b> all the products will be erased from your store. If you do not want to install sample data, use clean database while installing Magento. Skip the sample data installation step. </span></p>	
<p>In order to install the dump file, follow the steps below:</p>
<ol class="index-list">
	<li>Log into PhpMyAdmin and the select the database that you will use for Magento Commerce (It was created by you with the help of the steps provided earlier).</li>
	<li>Navigate to <b>"Import"</b> tab and click <b>"Browse"</b> button.</li>
	<li>Find the <i><b>"/sources/sample_data"</b></i> folder of your template and select the <b>dump.sql.gz.</b>file.</li>
	<li>Click. <b>"GO"</b>. The import process can take a few minutes.</li>
</ol>
