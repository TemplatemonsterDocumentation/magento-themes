<h3>Magento Installation</h3>
<p>When you are done with uploading files, you can start Magento engine installation process. Open your browser and in the address bar type your <strong>'domain name/path to your Magento directory'</strong>. You should see the welcome screen. Please follow the instructions below to install Magento. </p>
<div class="alert alert-warning"><span>NOTE: the <strong>dump.sql.gz</strong> file should be imported into the clear database BEFORE you install Magento.</span></div>

<h4>Step 1: Installation Assistant</h4>
<p>Please read the license agreement and click the "Agree and Setup Magento" checkbox if you agree with the terms. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-1.jpg" alt="" /></figure>
<h5>Server Readiness Check</h5>
<p>Click the "Start Readiness Check" button to check your server settings compatibility with the Magento 2 engine requirements. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-2.jpg" alt="" /></figure>
<p>When the checking procedure is complete, click '<strong>Next</strong>' button. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-3.jpg" alt="" /></figure>

<h4>Step 2: Database connection</h4>
<p>Here you need to input the database details and some initial settings. 
<strong>Add a Database</strong> â€“ input your host name, database name, database user and password. Contact your hosting provider, if you don't have the necessary details.</p>
<p>Make sure you have a database created for the Magento installation. And also make sure it is clear, without any tables. 
<figure class="img-polaroid"><img src="img/magento/how-to-install-4.jpg" alt="" /></figure>
<p>If everything is correct, click '<strong>Next</strong>' button. </p>

<h4>Step 3: Web Configuration</h4>
<p>Here you can change Magento admin panel URL and the main store address. Other settings you can leave unchanged. </p>
<div class="alert alert-warning">Make a point of the admin panel path. Note it or insert your specific admin panel URL to remember the admin panel access.</span></div>
<figure class="img-polaroid"><img src="img/magento/how-to-install-5.jpg" alt="" /></figure>
<p>When you are done, click '<strong>Next</strong>' button. </p>

<h4>Step 4: Localization</h4>
<p>Select the required time zone, default currency and store language. In the <strong>Advanced Modules Configurations</strong> section you can also select default modules that should/shouldn't be installed. We recommend that you do not perform changes in this section. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-6.jpg" alt="" /></figure>
<p>When you are done, click '<strong>Next</strong>' button.</p>

<h4>Step 5: Create Admin Account</h4>
<p>Here you need to input personal details for the store owner and administrator. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-7.jpg" alt="" /></figure>

<h4>Step 6: Install</h4>
<p>The store configuration is done. Press the <strong>Install Now</strong> button to start the store installation process. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-8.jpg" alt="" /></figure>

<h4>Step 7: Success</h4>
<p>This is the final installation step. Copy the generated encryption key and keep it safe. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-9.jpg" alt="" /></figure>
