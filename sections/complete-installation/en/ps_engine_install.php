		
<h3>Magento engine installation</h3>
<p>When you are done with uploading files, you can start with Magento engine installation.  Open your browser and in the address bar type your <b>'domain name/path to your Magento directory'</b>. You should see the welcome screen. Please follow the instructions below to install Magento. </p>
<div class="alert alert-warning"><span>Please note: the <b>dump.sql.gz</b> file should be imported into the clear database BEFORE you install Magento.</span></div>
<h4>Step 1: Installation Assistant</h4>
<p>Please read the license agreement and click the "I agree to the above terms and conditions" checkbox if you agree with the terms.  </p>

<h4>Step 2: Installation Assistant (Localization)</h4>
<p>Select the Locale, Time Zone and default currency. The Locale option defines your Magento store backend language. If you want to change the frontend language, you need to download and install Magento frontend locales. All the options can be easily changed later using the Magento admin panel. </p>
<figure class="img-polaroid"><img class="presta_scr1" src="img/magento/how-to-install-1.jpg" alt="" /></figure>

<h4>Step 3: Configuration</h4>
<p>Here you need to input the store database details and some initial settings.
<b>Database Connection</b> – input the database host name, user name, password and name. Contact your hosting provider if you don't have the necessary details. </p>
<p>Make sure you have a database created for the Magento installation. And also make sure it is clear, without any tables.
<b>Web access options</b> and <b>Session Storage Options</b> – please leave the options without any changes if you are not sure. The default settings usually work fine.</p>
<figure class="img-polaroid"><img class="presta_scr1" src="img/magento/how-to-install-3.jpg" alt="" /></figure>
<p>If everything is correct, please click '<b>Continue</b>' button.</p>

<h4>Step 4: Create Admin Account</h4>
<p>Here you need to input personal details for the store owner and administrator.  </p>
<figure class="img-polaroid"><img class="presta_scr1" src="img/magento/how-to-install-4.jpg" alt="" /></figure>
<p>When you are done, click <b>'Continue'</b> button.</p>

<h4>Step 5: You are All Set</h4>
<p>This is the final step of installation. Copy the generated encryption key and keep it safe. You can use the buttons in the bottom right corner to view the store front end and the admin panel. </p>
<figure class="img-polaroid"><img class="presta_scr1" src="img/magento/how-to-install-5.jpg" alt="" /></figure>
		