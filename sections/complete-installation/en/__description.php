<h2 align="justify"> Complete Installation </h2>
<h5>This kind of installation can be used in case, you do not have a functional Magento store and want to use the Live Demo store as the template to build your actual store not starting from scratch.</h5>
<p>The archive you've downloaded contains a fullpackage archive with Magento engine, Magento theme you've purchased and sample data (including banners, sliders, categories and products) you can see on the Template Live Demo.</p>
<p>To install the template you need to upload zipped <strong>fullpackage</strong> folder content to your server and run the Web Setup Wizard.</p>
<p align="justify">The installation process consists of 3 steps:</p>
<div align="justify">
  <ol class="index-list">
    <li><a href="https://zemez.io/magento/support/knowledge-base/create-new-database/">Create the database.</a></li>
    <li><a href="https://zemez.io/magento/support/knowledge-base/uploading-and-unzipping/">Upload all the required files to the server.</a></li>
    <li><a href="https://zemez.io/magento/support/knowledge-base/magento-installation/">Install a Magento Commerce engine together with the template you have purchased.</a></li>
  </ol>
