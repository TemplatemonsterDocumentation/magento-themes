		
<h3>Uploading and unzipping</h3>

<p>To start working with Magento you need to upload files to your hosting server. This can be done using your hosting file manager or some third party FTP manager.</p>
            
<ol class="index-list">
  <li>Select <b>'unzip.php'</b> and <b>'fullpackage.zip'</b> files and upload them to your server (
    <i class="icon-film"> </i> <a href="/help/how-upload-files-server-2.html" target="_blank">How to upload files to server</a>).</li>
  <li>Type the path to the <b>'unzip.php'</b> file on your server (<i class="muted">http://your_domain_name/unzip.php</i>) in your <abbr title="Internet Explorer, Google Chrome, Mozilla Firefox, Opera, Safari etc.">browser</abbr>.</li>
  <li>
  	You should see the following screen:
  	<figure class="img-polaroid"><img src="img/magento/complete-install-unzip-php.jpg" alt="Unzip.php initial screen."></figure>
  </li>
  <li>In the <b>'Choose your zip file'</b> select box please select the uploaded <b>fullpackage.zip file</b>.</li>
  <li>In the <b>'Unzip to'</b> field specify the directory where you want to extract the files.</li>
  <li>Click the <b>'Unzip'</b> button to proceed.</li>
</ol>
          
<p class="alert alert-warning"><span>Please make sure to set the correct permissions for the directory where you are planning to extract the files. Permissions should be <b>CHMOD 755 or 777</b> depending on your server configuration. </span></p>
		
