		
<h3>Creating a database</h3>
<p>First, you need to prepare a new database for your Magento store. You can create the new database using the database management tool from your hosting control panel (<i>usually PhpMyAdmin</i>).</p>
<p>The following data will be received while creating a database: database name, database username, password and you server name. You need to save this data as you will need it for further Magento installation. </p>

<p>With the phpMyAdmin tool you will be able to create a new database in 3 simple steps(check the slides below): </p>

<ol class="index-list">
	<li>Click the Database button from the top navigation menu to access the database listing.</li>
	<li>Input the database name into the "Create new database" field.</li>
	<li>Click the Create button on the right side.</li>
</ol>

<p>Your database can be seen in the database listing now. Click on its name to get access to the database:</p>
<p>Open the <b>"Database"</b> tab, you will see the list of databases.</p>
<figure class="img-polaroid"><img src="img/magento/database-create-1.jpg" alt="" /></figure>	
<p>Input your <b>database name</b>. Click <b>"Create"</b> button.</p>
<figure class="img-polaroid"><img src="img/magento/database-create-2.jpg" alt="" /></figure>
<p>Your database has appeared in the list.  In order to enter the database, click on its name in the list.</p>
<figure class="img-polaroid"><img src="img/magento/database-create-3.jpg" alt="" /></figure>
			

