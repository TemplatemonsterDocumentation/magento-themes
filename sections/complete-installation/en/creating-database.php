		
<h3 align="justify">Create the database</h3>
                                <p align="justify">Prepare a new database for your Magento store.  It should be created with the help of the database management tool in your hosting cPanel (PhpMyAdmin typically).</p>
                                <p align="justify">While creating a database, you will receive the database name, the database username, the password and your server name. Please, save these data. You will need it for further Magento installation. </p>
<p align="justify">With the phpMyAdmin tool you will be able to create a new database in 3 simple steps (check the slides below): </p>
                                <p align="justify">Open the <strong>"Database"</strong> tab.</p>
                                <figure class="img-polaroid">
                                    <div><img src="img/magento/database-create-1.jpg" alt="" /></div>
                                </figure>
                                <p align="justify">Enter your <strong>database name</strong>. Click the <strong>"Create"</strong> button.</p>
                                <figure class="img-polaroid">
                                    <div><img src="img/magento/database-create-2.jpg" alt="" /></div>
                                </figure>
                                <p align="justify">Your database will appear in the list on the left. In order to get the access to the database, click its name.</p>
                                <figure class="img-polaroid">
                                    <div align="justify"><img src="img/magento/database-create-3.jpg" alt="" /></div>
                                </figure>
            
