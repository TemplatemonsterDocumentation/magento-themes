		
<h3>Installazione motore Magento</h3>
<p>Una volta terminato il caricamento dei file, potrai iniziare l'installazione del motore Magento.  Apri il tuo browser, e nella barra degli indirizzi, digita il tuo <b>'nome di dominio/percorso verso la tua directory Magento'</b>. A questo punto dovresti vedere la schermata di benvenuto. Per installare Magento, segui queste istruzioni. </p>
<div class="alert alert-danger"><span>Nota: il file <b>dump.sql.gz</b> andrebbe importato nel database vuoto PRIMA di installare Magento.</span></div>
<h4>Fase 1: Assistente Installazione</h4>
<p>Leggi il contratto di licenza e clicca sulla casella "I agree to the above terms and conditions" se sei d'accordo con i termini del contratto.</p>

<h4>Fase 2: Assistente Installazione (Localizzazione)</h4>
<p>Seleziona località, fuso orario e valuta predefinita. L'opzione località definisce la lingua di backend del tuo negozio Magento. Se vuoi modificare la lingua di frontend, devi scaricare e installare le rispettive località frontend di Magento. Tutte le opzioni potranno comunque essere facilmente modificate in seguito utilizzando il pannello admin di Magento.</p>
<figure class="img-polaroid"><img class="presta_scr1" src="img/magento/how-to-install-1.jpg" alt="" /></figure>

<h4>Fase 3: Configurazione</h4>
<p>In questa fase dovrai inserire i dettagli di database del tuo negozio e definire alcune impostazioni iniziali.
<b>Database Connection </b> – inserisci il nome dell'host del database, il nome utente, nome e password. Se non hai tutte le informazioni necessarie, contatta il tuo provider. </p>
<p>Assicurati di avere un database creato per l'installazione di Magento. Assicurati anche che sia vuoto, senza tabelle.
<b>Web Access Options</b> e <b>Session Storage Options </b> – se non sei sicuro, lascia queste opzioni invariate. Di solito le impostazioni predefinite funzionano bene.</p>
<figure class="img-polaroid"><img class="presta_scr1" src="img/magento/how-to-install-3.jpg" alt="" /></figure>
<p>Se tutto è in ordine, clicca sul pulsante '<b>Continue</b>'.</p>

<h4>Fase 4: Crea Account Admin</h4>
<p>In questa fase dovrai inserire alcune informazioni personali del proprietario e dell'amministratore dello store.</p>
<figure class="img-polaroid"><img class="presta_scr1" src="img/magento/how-to-install-4.jpg" alt="" /></figure>
<p>Quando hai finito, clicca su <b>'Continue'</b>.</p>

<h4>Fase 5: Tutto pronto</h4>
<p>Questa è la fase finale dell'installazione. Copia la chiave di crittaggio generata e tienila al sicuro. Puoi usare i pulsanti all'angolo in basso a destra per vedere il frontend del tuo negozio e per il pannello di amministrazione.</p>
