<h3>Installazione Magento</h3>
<p>Quando avete finito con il caricamento dei file, è possibile iniziare il processo di installazione del motore di Magento. Aprire il browser e nella barra degli indirizzi digitare il proprio <strong>nome di dominio/percorso della directory di Magento ('domain name/path to your Magento directory')</strong>. Si dovrebbe vedere la schermata di benvenuto. Installare Magento e si prega di seguire le istruzioni riportate di seguito. </p>
<div class="alert alert-warning"><span>NOTA: il <strong>dump.sql.gz</strong> file deve essere importato nel database pulito prima di installare Magento.</span></div>

<h4>Passo 1: Installazione Assistente</h4>
<p>Si prega di leggere il contratto di licenza e selezionare la casella "Accetto la configurazione di Magento" ("Agree and Setup Magento"), e si accettano i termini. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-1.jpg" alt="" /></figure>
<h5>Controllo conformità Server</h5>
<p>Clicca il "Inizia Controllo conformità" ("Start Readiness Check") pulsante per controllare la compatibilità delle impostazioni del server con le esigenze del motore Magento 2. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-2.jpg" alt="" /></figure>
<p>Per la procedura di controllo, fare clic su '<strong>Prossimo (Next)</strong>'. pulsante. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-3.jpg" alt="" /></figure>

<h4>Passo 2: Database connessione</h4>
<p>Qui è necessario inserire i dettagli del database e alcune impostazioni iniziali. 
<strong>Aggiungere al Database (Add a Database</strong> - inserire il nome host, il nome del database, utente del database e la password. Contattare il fornitore di hosting, se non si hanno i dettagli necessari.</p>
<p>Assicurarsi di avere un database creato per l'installazione di Magento. E fare in modo che sia pulita e senza tabelle.
<figure class="img-polaroid"><img src="img/magento/how-to-install-4.jpg" alt="" /></figure>
<p>Se è tutto corretto, fare clic su '<strong>Prossimo (Next)</strong>'. pulsante. </p>

<h4>Passo 3: Configurazione Web</h4>
<p>Qui è possibile modificare Magento URL pannello di amministrazione e l'indirizzo del negozio principale. Altre impostazioni è possibile lasciarle invariate. </p>
<div class="alert alert-warning">Il punto del percorso pannello di amministrazione. Nota che puoi inserire l'URL del pannello di amministrazione con specifiche per ricordare l'accesso al pannello di amministrazione.</span></div>
<figure class="img-polaroid"><img src="img/magento/how-to-install-5.jpg" alt="" /></figure>
<p>Quando hai finito, fai clic su '<strong>Prossimo (Next)</strong>'. pulsante. </p>

<h4>Passo 4: Localizzazione</h4>
<p>Selezionare la lingua per il fuso orario, valuta e negozio predefinito richiesto. Nel <strong>Moduli Configurazioni Avanzate (Advanced Modules Configurations)</strong> nella sezione è anche possibile selezionare i moduli predefiniti che non devono/devono essere installati. Si consiglia di non eseguire modifiche in questa sezione.</p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-6.jpg" alt="" /></figure>
<p>Quando hai finito, fai clic su '<strong>Prossimo (Next)</strong>'. pulsante.</p>

<h4>Passo 5: Crea Admin Account</h4>
<p>Qui è necessario inserire i dati personali per il proprietario del negozio e amministratore. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-7.jpg" alt="" /></figure>

<h4>Passo 6: Installa</h4>
<p>La configurazione della memorizzazione è stata eseguita. Premi il <strong>Installa Adesso (Install Now)</strong> pulsante per avviare il processo di installazione negozio. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-8.jpg" alt="" /></figure>

<h4>Passo 7: Successo</h4>
<p>Questa è la fase di installazione finale. Copiare la chiave di crittografia generata e tenerla al sicuro.</p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-9.jpg" alt="" /></figure>