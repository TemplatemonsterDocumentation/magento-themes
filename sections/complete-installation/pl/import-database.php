		
<h3>Import przykładowych danych</h3>
<p>Pakiet szablonu Magento zawiera przykładowy plik danych (dump.sql lub dump.gz). Ten plik znajduje się w folderze<b>/sources/sample_data</b> Twojego szablonu. Pozwala to załadować przykładowe dane w taki sposób, że witryna wygląda dokładnie tak jak nasze demo szablonu.</p>
<p class="alert alert-danger"><span>Nie importuj dump.sql.gz, jeśli masz już dane na swojej stronie. Importując dump.sql.gz, wszystkie produkty zostaną usunięte z Twojego sklepu. Jeśli nie chcesz instalować przykładowych danych, należy użyć czystej bazy danych podczas instalacji Magento. Pomiń krok instalacji przykładowych danych. </span></p>	
<p>Aby zainstalować plik zrzutu (dump file), wykonaj następujące czynności:</p>
<ol>
	<li>Zaloguj się do phpMyAdmin, wybierz bazę danych, która ma zostać użyta do Magento Commerce (Został on stworzony przez Ciebie przy pomocy kroków wskazanych wcześniej).</li>
	<li>Przejdź do zakładki<b>"Import"</b> i kliknij przycisk <b>"Browse"</b>.</li>
	<li>Znajdż folder<i>"/sources/sample_data"</i> Twojego szablonu i wybierz plik <b>dump.sql.gz.</b>.</li>
	<li>Naciśnij <b>"GO"</b>. Proces importowania może potrwać kilka minut.</li>
</ol>
<p>Wybierz bazę danych, której będziesz używał do Magento Commerce. Wybierz <b>"IMPORT"</b>. Naciśnij przycisk <b>"Browse"</b>.</p>
