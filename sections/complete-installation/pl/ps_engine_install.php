<h3>Instalacja Magento</h3>
<p>Po zakończeniu przesłania plików, można rozpocząć proces instalacji platformy Magento. Otwórz przeglądarkę, i w pasku adresowym wprowadź <strong>'nazwę domenu/ścieżkę do twojego katalogu Magento'</strong>. Powinien pojawić się ekran powitalny. Teraz należy postępować według poniższych instrukcji w celu zainstalowania Magento. </p>
<div class="alert alert-warning"><span>UWAGA: <strong>dump.sql.gz</strong> powinien zostać importowany do pustej bazy danych PRZED zainstalowaniem Magento.</span></div>

<h4>Krok 1: Asystent Programu Instalacyjnego</h4>
<p>Prosimy o zapoznanie się z umową licencyjną i zaznaczenie pola wyboru "Zgadzam się i Zainstaluj Magento", jeżeli warunki zostały zaakceptowane. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-1.jpg" alt="" /></figure>
<h5>Sprawdzenie Gotowości Serweru</h5>
<p>Kliknij przycisk "Start Sprawdzania Gotowości", w celu sprawdzenia kompatybilności ustawień serwera z wymaganiami platformy Magento 2. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-2.jpg" alt="" /></figure>
<p>Gdy procedura sprawdzania jest zakończona, kliknij przycisk '<strong>Dalej</strong>'. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-3.jpg" alt="" /></figure>

<h4>Krok 2: Połączenie z bazą danych</h4>
<p>Tu należy wprowadzić szczegółowe dane baz danych i niektóre wstępne ustawienia. 
<strong>Dodaj bazę danych</strong> – wprowadź swoją nazwę hosta, nazwę bazy danych, użytkownika bazy danych oraz hasło. Skontaktuj się z dostawcą usług hostingowych, jeżeli nie dysponujesz potrzebnymi szczegółami.</p>
<p>Upewnij się, że baza danych dla instalacji Magento została stworzona. A także upewnij się, że jest pusta, bez żadnych tabel. 
<figure class="img-polaroid"><img src="img/magento/how-to-install-4.jpg" alt="" /></figure>
<p>Jeżeli wszystko się zgadza, kliknij przycisk '<strong>Dalej</strong>'. </p>

<h4>Krok 3: Konfiguracja sieci Web</h4>
<p>Tutaj można zmienić adres URL panelu administracyjnego Magento i główny adres sklepu. Pozostałe ustawienia można zostawić bez zmian. </p>
<div class="alert alert-warning">Należy zapamiętać ścieżkę panelu administracyjnego. Zapisz ją lub wprowadź konkretny adres URL panelu administracyjnego, żeby zapamiętać dostęp do panelu.</span></div>
<figure class="img-polaroid"><img src="img/magento/how-to-install-5.jpg" alt="" /></figure>
<p>Po zakończeniu, kliknij przycisk '<strong>Dalej</strong>'. </p>

<h4>Krok 4: Lokalizacja</h4>
<p>Wybierz odpowiednią strefę czasową, domyślną walutę i język sklepu. W sekcji <strong>Konfiguracje Zaawansowanych Modułów</strong> można również wybrać domyślne moduły, które należy/nie należy instalować. Zaleca się nie wykonywanie zmian w tej sekcji. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-6.jpg" alt="" /></figure>
<p>Po zakończeniu, kliknij przycisk '<strong>Dalej</strong>'. </p>

<h4>Krok 5: Tworzenie Konta Admina</h4>
<p>Tutaj musisz wprowadzić dane osobowe dla właściciela i administratora sklepu. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-7.jpg" alt="" /></figure>

<h4>Krok 6: Instalacja</h4>
<p>Konfiguracja sklepu jest wykonana. Kliknij przycisk <strong>Zainstaluj Teraz</strong>, aby rozpocząć proces instalowania sklepu. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-8.jpg" alt="" /></figure>

<h4>Krok 7: Sukces</h4>
<p>Jest to ostatni etap instalacji. Skopiuj wygenerowany klucz szyfrowania i zachowaj go w bezpiecznym miejscu. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-9.jpg" alt="" /></figure>