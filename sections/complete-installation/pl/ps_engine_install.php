		
<h3>Instalacja silnika Magento</h3>
<p>Kiedy zakończysz przesyłanie plików, można rozpocząć instalację silnika Magento. Otwórz przeglądarkę i w pasku adresu wpisz Twoją <b>'Nazwę domeny / ścieżkę do katalogu Magento'</b>. Powinieneś zobaczyć ekran powitalny. Prosimy postępować zgodnie z poniższymi instrukcjami, aby zainstalować Magento. </p>
<div class="alert alert-danger"><span>Uwaga: pliki <b>dump.sql.gz</b> powinny być importowane do czystej bazy danych ZANIM zainstalujesz Magento.</span></div>

<h4>Krok 1: Asystent instalacji</h4>
<p>Proszę przeczytaj umowę licencyjną i kliknij przycisk pola wyboru "Zgadzam się z powyższymi warunkami", jeśli zgadzasz się z warunkami. </p>

<h4>Krok 2: Asystent instalacji (Lokalizacja)</h4>
<p>Wybierz Ustawienia regionalne, strefę czasową oraz domyślną walutę. Opcja Ustawień lokalnych określa język backendu Twojego sklepu Magento. Jeśli chcesz zmienić język frontendu, musisz pobrać i zainstalować pakiety językowe Magento. Wszystkie opcje można łatwo zmienić później przy użyciu panelu administratora Magento. </p>
<figure class="img-polaroid"><img class="presta_scr1" src="img/magento/how-to-install-1.jpg" alt="" /></figure>

<h4>Krok 3: Konfiguracja</h4>
<p>Tutaj musisz wpisać szczegóły bazy danych sklepów i kilka ustawień początkowych.
<b>Łączenie bazy danych</b> - uzupełnij nazwę bazy danych hostingu, nazwę użytkownika, hasło oraz nazwę. Skontaktuj się z dostawcą usług hostingowych, jeśli nie posiadasz niezbędnych danych. </p>
<p>Upewnij się, że posiadasz bazę danych stworzoną do instalacji . Upewnij się również, że jest ona czysta, bez żadnych tabel.
<b>Opcje dostępu do sieci Web</b> oraz <b>Opcje przechowywania sesji</b> - zostaw opcje bez zmian, jeśli nie jesteś pewien. Ustawienia domyślne zwykle działają.</p>
<figure class="img-polaroid"><img class="presta_scr1" src="img/magento/how-to-install-3.jpg" alt="" /></figure>
<p>Jeśli wszystko jest w porządku, kliknij przycisk '<b>Continue</b>'.</p>


<h4>Krok 4: Zakładanie konta administratora</h4>
<p>Tutaj należy uzupełnić dane osobowe właściciela sklepu oraz administratora.  </p>
<figure class="img-polaroid"><img class="presta_scr1" src="img/magento/how-to-install-4.jpg" alt="" /></figure>
<p>Kiedy skończysz, kliknij przycisk <b>'Continue'</b> .</p>

<h4>Krok 5: Masz wszystkie ustawienia</h4>
	<p>Jest to ostatni etap instalacji. Skopiuj wygenerowany klucz szyfrowania i przechowuj go w bezpiecznym miejscu. Możesz użyć przycisków w prawym dolnym rogu, aby zobaczyć front sklepu oraz panel administratora. </p>
<figure class="img-polaroid"><img class="presta_scr1" src="img/magento/how-to-install-5.jpg" alt="" /></figure>
		