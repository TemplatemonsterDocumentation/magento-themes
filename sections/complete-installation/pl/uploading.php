		
<h3>Przesyłanie i rozpakowanie</h3>

<p>Aby zacząć pracę z Magento, musisz przesłać pliki do swojego serwera hostingowego. Można tego dokonać przy użyciu menadżera plików w hostingu lub osobnego FTP managera.</p>
            
<ol class="index-list">
  <li>Wybierz <strong>'unzip.php'</strong> oraz <strong>'fullpackage.zip'</strong> i prześlij je do swojego serwera(
    <i class="icon-film"> </i> <a href="/help/how-upload-files-server-2.html" target="_blank">Jak przesłać pliki do serwera.</a>
    )</li>
  <li>Podaj scieżkę do pliku 'unzip.php' na Twoim serwerze (<i class="muted">http://your_domain_name/unzip.php</i>) w <abbr title="Internet Explorer, Google Chrome, Mozilla Firefox, Opera, Safari etc.">przeglądarce</abbr>.</li>
  <li>Powinieneś zobaczyć następujący widok:<figure class="img-polaroid"><img src="img/magento/complete-install-unzip-php.jpg" alt="Unzip.php initial screen."></figure></li>
  <li>W oknie <strong>'Wybierz plik zip'</strong> (ang. Choose your zip file) wybierz przesłany plik fullpackage.zip.</li>
  <li>W <strong>'Wypakuj do'</strong> (ang. Unzip to) wybierz folder, do którego chcesz wypakować pliki.</li>
  <li>Kliknij <strong>'Wypakuj'</strong> (ang. Unzip), aby kontynuować.</li>
</ol>
          

          
<p class="alert alert-warning"><span>Upewnij się, że ustaliłeś odpowiednie uprawnienia w folderze, do którego wypakowujesz pliki. Uprawnienia powinny być <strong>CHMOD 755 lub 777</strong>, w zależności od ustawień serwera.</span></p>
		
