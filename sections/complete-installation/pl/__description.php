<h2 class="item5"><i class="icon-cogs"></i> Pełna instalacja </h2>
<p>Metoda pełnej instalacji może być używana, jeśli chcesz założyć sklep Magento z wykorzystaniem przykładowych treści. Zakładamy, że ukończyłeś wszystkie <a href="#preparations">etapy przygotowania</a>.</p>
<p>Proces instalacji składa się z 3 etapów:</p>
<ol class="list">
	<li>Tworzenie bazy danych.</li>
	<li>Przesyłanie wszystkich wymaganych plików na serwer.</li>
	<li>Instalacja silnika Magento Commerce wraz z zakupionym szablonem.</li>
</ol>
<p>Zaczynajmy!</p>