<h2> Kompletne instalowanie </h2>
<p>Metoda kompletnego instalowania może być stosowana, jeżeli chcesz założyć sklep Magento za pomocą przykładowej treści. Zakładamy, że wszystkie czynności przygotowawcze zostały dokonane.</p>
<p>Proces instalacyjny składa się z 3 etapów:</p>
<ol class="index-list">
    <li>Tworzenie bazy danych.</li>
    <li>Przesłanie wszystkich wymaganych plików na serwer.</li>
    <li>Instalowanie silnika Magento Commerce wraz z zakupionym szablonem.</li>
</ol>
<p>Zaczynajmy!</p>