		
<h3>Tworzenie bazy danych</h3>
<p>Najpierw należy przygotować nową bazę danych dla sklepu Magento. Możesz utworzyć nową bazę danych za pomocą narzędzia do zarządzania bazą danych z panelu sterowania hostingu (<i>zazwyczaj PhpMyAdmin</i>).</p>
<p>Następujące dane uzyskamy podczas tworzenia bazy danych: Nazwa bazy danych, nazwa użytkownika bazy danych, hasło i Twoją nazwę serwera. Musisz zapisać te dane, gdyż będą one potrzebne do dalszej instalacji Magento.</p>

<p>Przy użyciu narzędzia phpMyAdmin, będziesz w stanie stworzyć nową bazę danych w 3 prostych krokach (sprawdź slajdy poniżej): </p>

<ol class="list">
	<li>Kliknij na przycisk "Database" z górnego menu nawigacyjnego, aby przejść do listy baz danych.</li>
	<li>Wprowadź nazwę bazy danych w polu "Create new database".</li>
	<li>Kliknij przycisk "Create" po prawej stronie.</li>
</ol>

<p>Twoja baza danych znajduje się teraz na liście. Kliknij na jej nazwę, aby uzyskać dostęp do bazy danych:</p>
<p>Otwórz zakładkę <b>"Database"</b>, pojawi się lista baz danych.</p>
<figure class="img-polaroid"><img src="img/magento/database-create-1.jpg" alt="" /></figure>	
<p>Wprowadź Twoją nazwę bazy danych. Kliknij przycisk <b>"Create"</b>.</p>
<figure class="img-polaroid"><img src="img/magento/database-create-2.jpg" alt="" /></figure>
<p>Twoja baza danych pojawiła się na liście. Aby wejść do bazy danych, należy kliknąć na jej nazwę na liście.</p>
