<h3>Tworzenie bazy danych</h3>
<p>Po pierwsze, trzeba przygotować nową bazę danych dla sklepu Magento. Można utworzyć nową bazę danych za pomocą narzędzia do zarządzania bazą danych z panelu sterowania hostingu (<strong>zwykle to PhpMyAdmin</strong>).</p>
<p>Następujące dane będą odbierane podczas tworzenia bazy danych: nazwa bazy danych, nazwa użytkownika bazy danych, hasło oraz nazwa serwera. Należy zapisać te dane, o ile będą potrzebne do dalszej instalacji Magento. </p>

<p>Będziesz miał możliwość tworzenia nowej bazy danych za pomocą narzędzia phpMyAdmin w 3 prostych krokach (sprawdź poniższe slajdy): </p>

<ol class="index-list">
	<li>
		<p>Otwórz kartę <strong>"Baza danych"</strong>, pojawi się lista baz danych.</p>
		<figure class="img-polaroid"><img src="img/magento/database-create-1.jpg" alt="" /></figure>
	</li>
	<li>
		<p>Wprowadź swoją <strong>nazwę bazy danych</strong>. Kliknij przycisk <strong>"Utwórz"</strong>.</p>
		<figure class="img-polaroid"><img src="img/magento/database-create-2.jpg" alt="" /></figure>
	</li>
	<li>
		<p>Baza danych pojawiła się na liście. W celu wejścia do bazy danych, kliknij jej nazwę na liście.</p>
		<figure class="img-polaroid"><img src="img/magento/database-create-3.jpg" alt="" /></figure>
	</li>
</ol>

<p>Można również sprawdzić szczegółowy tutorial wideo na <a href="/help/how-to-create-a-database.html" target="_blank"> Jak stworzyć bazę danych</a></p>

