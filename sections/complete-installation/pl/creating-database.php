		
<h3>Tworzenie bazy danych</h3>
<p>Najpierw należy przygotować nową bazę danych dla sklepu Magento. Możesz utworzyć nową bazę danych za pomocą narzędzia do zarządzania bazą danych z panelu sterowania hostingu (<strong>zazwyczaj PhpMyAdmin</strong>).</p>
<p>Następujące dane uzyskamy podczas tworzenia bazy danych: Nazwa bazy danych, nazwa użytkownika bazy danych, hasło i Twoją nazwę serwera. Musisz zapisać te dane, gdyż będą one potrzebne do dalszej instalacji Magento.</p>

<p>Przy użyciu narzędzia phpMyAdmin, będziesz w stanie stworzyć nową bazę danych w 3 prostych krokach (sprawdź slajdy poniżej): </p>

<ol class="index-list">
	<li>
		Otwórz zakładkę <strong>"Database"</strong>, pojawi się lista baz danych.
		<figure class="img-polaroid"><img src="img/magento/database-create-1.jpg" alt="" /></figure>
	</li>
	<li>
		Wprowadź Twoją nazwę bazy danych. Kliknij przycisk <strong>"Create"</strong>.
		<figure class="img-polaroid"><img src="img/magento/database-create-2.jpg" alt="" /></figure>
	</li>
	<li>
		<p>Twoja baza danych pojawiła się na liście. Aby wejść do bazy danych, należy kliknąć na jej nazwę na liście.</p>
		<figure class="img-polaroid"><img src="img/magento/database-create-3.jpg" alt="" /></figure>
	</li>
</ol>

<p>Możesz sprawdzić szczegółowy video tutorial na <a href="/help/how-to-create-a-database.html" target="_blank"> Jak stworzyć bazę danych</a>.</p>

<div class="alert alert-warning"><span>Jeśli masz problemy ze stworzeniem bazy danych, skontaktuj się ze swoim dostawcą hostingu w celu uzyskania pomocy i wsparcia.</span></div>		