		
<h3>Criar um banco de dados</h3>
<p>Primeiro, você precisa preparar um novo banco de dados para sua loja Magento. Você pode criar um novo banco de dados usando uma ferramenta de gerencimaneto de bancos de dados (<i>normalmente, o PhpMyAdmin</i>).</p>
<p>Os seguintes dados serão recebidos ao criar seu banco de dados: o nome do banco, nome de usuário, senha e o nome do seu servidor. Você precisa guardar esses dados para continuar a instalação do Magento.</p>

<p>Com a ferramenta phpMyAdmin, você pode criar um novo banco de dados em 3 passos simples: </p>

<ol class="list">
	<li>Clique no botão do banco de dados na navegação superior do menu para acessar a lista de bancos de dados</li>
	<li>Insira o nome do banco em "Create new database".</li>
	<li>Clique no botão Criar à direita do campo que você preencheu.</li>
</ol>

<p>Seu banco de dados agora pode ser visto na listagem. Clique no nome dele para conseguir acesso ao banco:</p>
<p>Abra a aba <b>"Database"</b>, você verá uma lista de bancos de dados.</p>
<figure class="img-polaroid"><img src="img/magento/database-create-1.jpg" alt="" /></figure>	
<p>Insira o nome do seu banco de dados. Clique em <b>"Create"</b>.</p>
<figure class="img-polaroid"><img src="img/magento/database-create-2.jpg" alt="" /></figure>
<p>Seu banco apareceu na lista. Para entrar no banco, clique no nome na lista.</p>
<figure class="img-polaroid"><img src="img/magento/database-create-3.jpg" alt="" /></figure>
			

