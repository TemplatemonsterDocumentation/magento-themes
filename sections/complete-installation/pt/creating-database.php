<h3>Criar banco de dados</h3>
<p>Primeiro, você precisa preparar um novo banco de dados para sua loja Magento. Você pode criar um novo banco usando a ferramenta de gerenciamento de banco de dados no seu painel de hospedagem (<strong>normalmente usam o PhpMyAdmin</strong>).</p>
<p>Os dados a seguir serão recebidos ao criar o banco: nome do banco de dados, usuário do banco, senha e o nome do servidor. Você precisa guardar esses dados, pois serão necessários para terminar de instalar o Magento. </p>

<p>Com a ferramenta do phpMyAdmin você vai poder criar seu banco de dados em 3 passos simples(veja slides abaixo): </p>

<ol class="index-list">
	<li>
		<p>Abra a a aba <strong>"Database"</strong>, você verá uma lista de bancos de dados.</p>
		<figure class="img-polaroid"><img src="img/magento/database-create-1.jpg" alt="" /></figure>
	</li>
	<li>
		<p>Inclua seu <strong>nome do banco</strong>. Clique no botão <strong>"Create"</strong>.</p>
		<figure class="img-polaroid"><img src="img/magento/database-create-2.jpg" alt="" /></figure>
	</li>
	<li>
		<p>Seu banco de dados apareceu na lista. Para entrar no banco, clique no nome na lista.</p>
		<figure class="img-polaroid"><img src="img/magento/database-create-3.jpg" alt="" /></figure>
	</li>
</ol>

<p>Você também pode assistir um tutorial em vídeo sobre o assunto em <a href="/help/how-to-create-a-database.html" target="_blank"> How to create a database</a></p>

<div class="alert alert-warning"><span>Se tiver qualquer problemas ao criar o banco de dados, entre em contato com seu provedor de hospedagem e peça ajuda a assistência deles.</span></div>
		