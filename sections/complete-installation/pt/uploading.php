<h3>Enviar os arquivos necessários e descompactá-los</h3>

<p>Para começar a trabalhar com Magento, você precisa enviar arquivos para o seu servidor de hospedagem. Isso pode ser feito através de um gerenciador de arquivos da sua hospedagem ou com um programa de FTP de terceiros.</p>
            
<ol class="index-list">
  <li>Escolha os arquivos <strong>'unzip.php'</strong> e <strong>'fullpackage.zip'</strong> e envie-os para seu servidor (
    <a href="/help/how-upload-files-server-2.html" target="_blank">Como enviar arquivos para o servidor</a>).</li>
  <li>Escreva o caminho para o arquivo <strong>'unzip.php'</strong> no seu servidor (http://seu_dominio/unzip.php) no seu <abbr title="Internet Explorer, Google Chrome, Mozilla Firefox, Opera, Safari etc.">navegador</abbr>.</li>
  <li>
  	Você deve ver a seguinte tela:
  	<figure class="img-polaroid"><img src="img/magento/complete-install-unzip-php.jpg" alt="Unzip.php initial screen."></figure>
  </li>
  <li>Na caixa de seleção <strong>'Choose your zip file'</strong> escolha o arquivo <strong>fullpackage.zip file</strong> que você enviou.</li>
  <li>No campo <strong>'Unzip to'</strong> defina o diretório onde você quer extrair os arquivos.</li>
  <li>Clique no botão <strong>'Unzip'</strong> para prosseguir.</li>
</ol>
          
<p class="alert alert-warning"><span>Certifique-se de que as permissões corretas estão definidas para a pasta onde deseja extrair os arquivos. Permissões deveriam ser <strong>CHMOD 755 ou 777</strong> dependendo das configurações do seu servidor. </span></p>
		
