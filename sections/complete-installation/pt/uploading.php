		
<h3>Fazendo upload e descompactando</h3>

<p>Para começar a mexer com o Magento você precisa enviar os arquivos ao seu servidor de hospedagem. Isso pode ser feito usando o gerenciador de sua hospedagem ou algum gerenciador terceirizado de FTP.</p>
            
<ol>
  <li>Selecione os arquivos 'unzip.php' e 'fullpackage.zip' e os envie ao seu servidor(
    <i class="icon-film"> </i> <a href="/help/how-upload-files-server-2.html" target="_blank">Como enviar arquivos ao servidor.</a>
    )</li>
  <li>Digite o caminho para o arquivo 'unzip.php' no seu servidor (<i class="muted">http://seu_domínio/unzip.php</i>) no seu <abbr title="Internet Explorer, Google Chrome, Mozilla Firefox, Opera, Safari etc.">navegador</abbr>.</li>
  <li>Você deve ver a seguinte tela:</li>
</ol>
          
<figure class="img-polaroid"><img src="img/magento/complete-install-unzip-php.jpg" alt="Unzip.php initial screen."></figure>
<p class="muted text-center">Img 1. Unzip.php tela inicial.</p>
          
<ol start="5">
  <li>No campo suspenso <strong>'Escolha seu arquivo zip'</strong> (em Inglês, 'Choose your zip file') por favor selecione o arquivo enviado <strong>fullpackage.zip</strong>.</li>
  <li>No campo <strong>'Descompactar para'</strong> (em Inglês, 'Unzip to') especifique o diretório pra onde você quer extrair os arquivos.</li>
  <li>Clique no botão <strong>&quot;Descompactar&quot;</strong> (em Inglês, 'Unzip') para confirmar.</li>
</ol>
          
<p class="alert alert-warning"><span>Por favor, certifique-se de colocar as permissões corretas para o diretório onde você está planejando em extrair os arquivos. As permissões devem ser <strong>CHMOD 755 ou 777</strong>, depende da configuração do seu servidor.</span></p>
		
