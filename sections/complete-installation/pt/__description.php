<h2 class="item5"><i class="icon-cogs"></i> Instalação completa </h2>
<p>O método de instalação completa pode ser usado se você precisar montar sua loja Magento usando o conteúdo de amostra. Assumimos que você já <a href="#preparations">se preparou</a>.</p>
<p>O processo de instalação consiste de 3 fases:</p>
<ol class="index-list">
    <li>Criar um banco de dados.</li>
		<li>Subir todos os arquivos necessários para o servidor.</li>
		<li>Instalar o Magento junto com o template que você comprou.</li>
</ol>
