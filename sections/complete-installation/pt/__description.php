<h2> Instalação completa </h2>
<p>O método de instalação completa pode ser usado se você deseja montar sua loja Magento com o conteúdo de exemplo. Assumimos que você já preparou o ambiente todo conforme instruções.</p>
<p>A instalação consiste em 3 estágios:</p>
<ol class="index-list">
    <li>Criar banco de dados.</li>
    <li>Enviar todo os arquivos necessários para o servidor.</li>
    <li>Instalar o Magento Commerce e o template que você comprou.</li>
</ol>
<p>Vamos começar!</p>