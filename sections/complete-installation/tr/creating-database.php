		
<h3>Veritabanı oluşturmak</h3>
<p>Öncelikle Magento sanal mağazanız için yeni bir veritabanı hazırlamalısınız. Veritabanını, hosting hesabınızın kontrol panelinde bulunan (<i>PhpMyAdmin</i>) aracılığıyla oluşturabilirsiniz.</p>
<p>Veritabanını oluştururken; veritabanı adı, veritabanı kullanıcı adı, şifre ve sunucu adı bilgileri de oluşturulacaktır. Bu bilgileri, ilerki aşamalarda kullanmak üzere bir yere kaydetmelisiniz.</p>

<p>phpMyAdmin ile 3 kolay adımda yeni bir veritabanı oluşturacaksınız (aşağıdaki listeye bakınız):</p>

<ol class="list">
	<li>Veritabanlarını listelemek için üst menüden Veritabanı yazısına tıklayın.</li>
	<li>"Yeni bir veritabanı oluştur" kısmına, veritabanı adını yazın.</li>
	<li>Sağ taraftaki Oluştur yazısına tıklayın.</li>
</ol>

<p>Veritabanınız artık listede gözükecektir. Ona tıklayın.</p>
<p><b>"Veritabanı"</b> sekmesini açın, veritabanı listesini görüntüleyin.</p>
<figure class="img-polaroid"><img src="img/magento/database-create-1.jpg" alt="" /></figure>	
<p>Veritabanı ismini yazın. <b>"Oluştur"</b> yazısına tıklayın.</p>
<figure class="img-polaroid"><img src="img/magento/database-create-2.jpg" alt="" /></figure>
<p>Veritabanınız artık listede yerini aldı. Veritabanı bilgilerinizi görmek için, ismine tıklayın.</p>
<figure class="img-polaroid"><img src="img/magento/database-create-3.jpg" alt="" /></figure>
			

<p>Detaylı anlatım için <i class="icon-film"> </i><a href="/help/how-to-create-a-database.html" target="_blank"> Veritabanı nasıl oluşturulur</a> konulu videoyu izleyebilirsiniz.</p>

