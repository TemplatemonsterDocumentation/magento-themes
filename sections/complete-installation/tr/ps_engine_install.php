<h3>Magento kurulumu</h3>
<p>Tüm gerekli dosyaları yükledikten sonra Magento'yu kurmak için hazır hale gelirsiniz. Tarayıcınızı açın ve adres çubuğuna <b>'site adresiniz/Magento'nun bulunduğu dizin adı'</b> nı yazın. Şimdi karşılama ekranını göreceksiniz. Magento'yu kurmak için gereken talimatları takip edin.</p>
<div class="alert alert-warning"><span>Dikkat: <b>dump.sql.gz</b> dosyası, Magento'yu kurmadan ÖNCE boş bir veritabanında içe aktarılmalıdır.</span></div>
<h4>Step 1: Kurulum Yardımcısı</h4>
<p>Lütfen lisans sözleşmesini okuyun ve eğer kabul ediyorsanız, "Aşağıdaki şartları ve koşulları kabul ediyorum" kutusunu işaretleyin.</p>

<h4>Step 2: Kurulum Yardımcısı (Yerelleştirme)</h4>
<p>Konumunuzu, Saat Diliminizi ve Para Biriminizi seçin. Seçeceğiniz konum, Magento mağazanızın dilini belirler. Eğer başlangıç dilini değiştirmek isterseniz, Magento'nun ek dosyalar yüklemeniz gerekebilir. Tüm değişiklikler kurulum sonrası, yönetici panelinden ayarlanabilir.</p>
<figure class="img-polaroid"><img class="presta_scr1" src="img/magento/how-to-install-1.jpg" alt="" /></figure>

<h4>Step 3: Ayarlamalar</h4>
<p>Burada mağazanın veritabanı detaylarını ve bazı ayarları girmeniz gerekir. <b>Veritabanı Bağlantısı</b> – veritabanı host adı, kullanıcı adı, şifre ve veritabanı adını girin. Gerekli bilgilere sahip değilseniz, hosting sağlayacınızla iletişime geçin.</p>
<p>Magento kurulumu için bir veritabanı oluşturmuş olduğunuza emin olun. Ayrıca bu veritabanı boş olmalı ve herhangi bir tablo içermemelidir.<b>Web erişim seçenekleri</b> ve <b>Depo oturum seçenekleri</b> – emin değilseniz bu iki seçeneği boş bırakın. Varolan ayarlar sorunsuz çalışacaktır.</p>
<figure class="img-polaroid"><img class="presta_scr1" src="img/magento/how-to-install-3.jpg" alt="" /></figure>
<p>Bu aşama bittikten sonra '<b>Devam</b>' butonuna tıklayın.</p>

<h4>Step 4: Yönetici Hesabı Oluşturmak</h4>
<p>Mağaza sahibi ve yöneticisi için kişisel detaylar yazmanız gereken alandır.</p>
<figure class="img-polaroid"><img class="presta_scr1" src="img/magento/how-to-install-4.jpg" alt="" /></figure>
<p>Bu aşama bittikten sonra '<b>Devam</b>' butonuna tıklayın.</p>

<h4>Step 5: Hazırsınız</h4>
<p>Bu, kurulumun son aşamasıdır. Oluşturulan şifreli anahtarı kopyalayın ve kaydedin. Alt sağ köşede bulunan butonları kullanarak artık mağazanıza ve yönetici panelinize erişebilirsiniz.</p>
