<h3>Установка Magento</h3>
<p>После окончания загрузки файлов можно приступать к установке Magento. Откройте ваш браузер и в адресной строке браузера напишите<strong>'доменное имя/путь к вашей папке Magento'</strong>. Вы должны увидеть экран приветствия. Следуйте инструкциям ниже для установки Magento. </p>
<div class="alert alert-warning">Внимание: файл <strong>dump.sql.gz</strong> должен быть импортирован в чистую базу данных ПЕРЕД установкой Magento.</span></div>

<h4>Шаг 1: Installation Assistant</h4>
<p>Прочтите, пожалуйста, лицензионное соглашение и нажмите на кнопку "Agree and Setup Magento", подтверждающую ваше с ним согласие. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-1.jpg" alt="" /></figure>
<h5>Проверка совместимости сервера</h5>
<p>Нажмите на кнопку "Start Readiness Check" для проверки соответствия настроек вашего сервера к требованиям движка Magento 2. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-2.jpg" alt="" /></figure>
<p>После окончания проверки нажмите "Next". </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-3.jpg" alt="" /></figure>

<h4>Шаг 2: Настройка базы данных</h4>
<p>Здесь вам необходимо указать данные для доступа к базе данных и некоторые базовые настройки.
<strong>Add a Database</strong> – укажите адрес сервера, название базы, имя пользователя и пароль. Свяжитесь с вашим хостером, если у вас нет необходимых данных. </p>
<p>Убедитесь, что вы создали базу данных для установки Magento. Также убедитесь, что она пустая и в ней нет никаких таблиц.
<figure class="img-polaroid"><img src="img/magento/how-to-install-4.jpg" alt="" /></figure>
<p>Если все правильно, нажмите кнопку '<strong>Next</strong>'.</p>

<h4>Шаг 3: Web Configuration</h4>
<p>Здесь вы можете изменить url админ панели и основной адрес сайта. Остальные настройки вы можете оставить без изменений. </p>
<div class="alert alert-warning">Обратите внимание на путь к админ панели. Запишите или установите свой, чтобы не потерять доступ.</span></div>
<figure class="img-polaroid"><img src="img/magento/how-to-install-5.jpg" alt="" /></figure>
Когда данные будут введены, нажмите кнопку <strong>'Next'</strong>.

<h4>Шаг 4: Локализация</h4>
<p>Укажите часовой пояс, дефолтную валюту и язык вашего магазина. Вы можете выбрать, какие дефолтные модули устанавливать, а какие нет в пункте <strong>Advanced Modules Configurations</strong>. Мы вам рекомендуем ничего не менять.</p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-6.jpg" alt="" /></figure>
Когда данные будут введены, нажмите кнопку <strong>'Next'</strong>.

<h4>Шаг 5: Create Admin Account</h4>
<p>Укажите личные данные владельца магазина или администратора</p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-7.jpg" alt="" /></figure>

<h4>Шаг 6: Установка</h4>
<p>Настройка магазина закончена. Нажмите <strong>Install Now</strong> для установки магазина.</p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-8.jpg" alt="" /></figure>

<h4>Шаг 7: Success</h4>
<p>Это завершающий этап установки. Скопируйте зашифрованный ключ и сохраните его в надежном месте. </p>
<figure class="img-polaroid"><img src="img/magento/how-to-install-9.jpg" alt="" /></figure>