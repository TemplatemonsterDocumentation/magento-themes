<h3>Загрузка и разархивация</h3>

<p>Для начала работы с Magento, требуется загрузить файлы на хостинг сервер. Это можно сделать при помощи файлового менеджера хостинга или альтернативного FTP менеджера.</p>
            
<ol class="index-list">
  <li>Выделите файлы <strong>'unzip.php'</strong> и <strong>'fullpackage.zip'</strong> и загрузите на сервер (
      <a href="/help/how-upload-files-server-2.html" target="_blank"> Как загрузить файлы на сервер.</a>
      )</li>
  <li>Введите путь к файлу  <strong>'unzip.php'</strong> на сервере (http://your_domain_name/unzip.php) в адресной строке <abbr title="Internet Explorer, Google Chrome, Mozilla Firefox, Opera, Safari etc.">браузера</abbr>.</li>
  <li>Должно появиться следующее окно:<figure class="img-polaroid"><img src="img/magento/complete-install-unzip-php.jpg" alt="Unzip.php initial screen."></figure></li>
  <li>В поле <strong>'Choose your zip file (Выберите файл zip)'</strong> выберите загруженный файл <strong>fullpackage.zip file</strong>.</li>
  <li>В поле <strong>'Unzip to (Распаковать в)'</strong> укажите директорию, куда нужно распаковать файлы.</li>
  <li>Нажмите кнопку <strong>'Unzip  (Распаковать)'</strong>, чтобы распаковать файлы.</li>
</ol>

          
<p class="alert alert-warning"><span>Задайте правильные разрешения на папку, куда нужно разархивировать файлы. Разрешения должны быть  CHMOD 755 или 777 в зависимости от конфигурации сервера. </span></p>
		
