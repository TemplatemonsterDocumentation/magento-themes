<h3>Создание базы данных</h3>
<p>Для начала необходимо подготовить новую базу данных для интернет магазина Magento. Ее можно создать через инструмент управления базами данных в контрольной панели хостинга (<strong>обычно PhpMyAdmin</strong>).</p>
<p>При создании базы данных вы получите такие данные: название базы данных, имя пользователя, пароль и адрес сервера баз данных. Сохраните эти данные, так как они понадобятся вам при установке Magento. </p>

<p>При помощи phpMyAdmin возможно создать новую базу данных в 3 простых шага: (см. следующие слайды)</p>

<ol class="index-list">
	<li>
		Отктройте вкладку <strong>"Database (База данных)"</strong>, чтобы перейти в список баз данных.
		<figure class="img-polaroid"><img src="img/magento/database-create-1.jpg" alt="" /></figure>
	</li>
	<li>
		Введите название <strong>базы данных</strong>. Щелкните кнопку <strong>"Create (Создать)"</strong>.
		<figure class="img-polaroid"><img src="img/magento/database-create-2.jpg" alt="" /></figure>
	</li>
	<li>
		<p>База данных появилась  в списке. Кликните по названию, чтобы получить доступ в базу данных.</p>
		<figure class="img-polaroid"><img src="img/magento/database-create-3.jpg" alt="" /></figure>
	</li>
</ol>

<p>Вы также можете воспользоваться детальным видео туториалом <a href="/help/how-to-create-a-database.html" target="_blank"> Как создать базу данных</a>.</p>

<div class="alert alert-warning"><span>При возникновении сложностей при создании базы данных, необходимо обратиться к хостинг провайдеру за поддержкой. </span></div>