<h3>Galeria produktów</h3>
<p class="indent">Moduł ten umożliwia pokazywanie miniaturek obrazów produktów wraz z ich głównym obrazem. Standardowo miniaturki wyświetlane są pod obrazem głównego produktu, ale w naszym motywie miniaturki są wyświetlane wedle układu PSD.</p>
<p>Ten moduł może zostać włączony/wyłączony w panelu administracyjnym Magento pod: <strong>System > Configuration > Templatemonster > Product List Gallery</strong>.</p>
<figure class="img-polaroid"><img src="img/magento/productgallery.jpg" alt="" /></figure>
<p>Moduł zawiera cztery sekcje ustawień: <strong>List View</strong>, <strong>Grid View</strong>, <strong>Homepage List View</strong> i <strong>Homepage Grid View</strong>.<br>
W <strong>Homepage List View</strong> i <strong>Homepage Grid View</strong> można konfigurować wyświetlanie galerii na stronie głównej sklepu.</p>
<p>Wszystkie sekcje zawierają te same ustawienia:</p>			
<table class="options-table">
	<tr>
		<td class="col-1"><strong>Enable/Disable</strong></td>
		<td>-</td>
		<td>ta opcja włącza/wyłącza moduł. </td>
	</tr>
	<tr>
		<td class="col-1"><strong>Image width</strong></td>
		<td>-</td>
		<td>ta opcja kontroluje szerokość głównego obrazu produktu. </td>
	</tr>
	<tr>
		<td class="col-1"><strong>Image height</strong></td>
		<td>-</td>
		<td>ta opcja kontroluje wysokość głównego obrazu produktu. </td>
	</tr>
	<tr>
		<td class="col-1"><strong>Thumbnail width</strong></td>
		<td>-</td>
		<td>ta opcja kontroluje szerokość miniaturki produktu. </td>
	</tr>
	<tr>
		<td class="col-1"><strong>Thumbnail height</strong></td>
		<td>-</td>
		<td>ta opcja kontroluje wysokość miniaturki produktu. </td>
	</tr>
</table>
<p>Warto zauważyć! Maksymalna szerokość obrazu jest równa szerokości kolumny, w którym jest on umieszczony.</p>
		