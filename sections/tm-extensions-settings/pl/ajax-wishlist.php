<h3>Ajax Wishlist</h3>
<p>Lista życzeń produktów jest jedną ze standardowych funkcji handlu elektronicznego (e-commerce). Stworzyliśmy specjalny moduł dla wygodnego korzystania z tej funkcji. Jest oparty na technologii AJAX, a także umożliwia dodawanie produktów do listy życzeń bez konieczności przechodzenia na osobną stronę.</p>
<p>W celu konfigurowania modułu, należy przejść do panelu administracyjnego Magento i w menu po lewej stronie wybrać sekcję <strong>Sklepy > Konfiguracja (Stores > Configuration)</strong>.</p>
<figure class="img-polaroid"><img src="img/magento/compare.jpg" alt="" /></figure>
<p>Zatem na nowej stronie należy wybrać element <strong> TemplateMonster > Product Compare Ajax </strong> po lewej stronie.</p>
 <figure class="img-polaroid"><img src="img/magento/wishlist.jpg" alt="" /></figure>


<p>Przyjrzyjmy się do konfiguracji modułu:</p>			
<table class="options-table">
	<tr>
		<td class="col-1"><strong>Włącz (Enable)</strong></td>
		<td>-</td>
		<td>ta opcja umożliwia włączanie lub wyłączanie modułu.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Pokaż spinner (Show spinner)</strong></td>
		<td>-</td>
		<td>ta opcja pozwala włączyć wyświetlanie spinner'a podczas doawania elementu do swojej listy życzeń.</td>
	</tr>					
	<tr>
		<td class="col-1"><strong>Pokaż wiadomość powodzenia (Show Success Message)</strong></td>
		<td>-</td>
		<td>ta opcja pozwala włączyć wyświetlanie wyskakującego komunikatu po dodaniu elementu do swojej listy życzeń.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Tekst wiadomości powodzenia (Success Message Text)</strong></td>
		<td>-</td>
		<td>Tutaj można ustawić wiadomość tekstową, która pojawi się w wyskakującym komunikacie po dodaniu elementu do swojej listy życzeń.</td>
	</tr>		
</table>