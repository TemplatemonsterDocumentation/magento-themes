<h3>Moduł Instagram</h3>
<p>Moduł Instagram pozwala wyświetlić galerię Instagram w Twoim sklepie.<br>
Ten moduł pozwala wyświetlić dwa rodzaje galerii: galerię użytkownika oraz galerię tagu.</p>						
<p>Wszystkie opcje konfiguracji są dostępne w panelu administracyjnym w sekcji <strong>System > Configuration > Templatemonster > Instagram</strong>.</p>
<figure class="img-polaroid"><img src="img/magento/instagram.jpg" alt="" /></figure>			
<p>Moduł posiada następujące opcje:</p>			
<table  class="options-table">
	<tr>
		<td class="col-1"><strong>Client ID</strong></td>
		<td>-</td>
		<td>Identyfikator aplikacji Instagram. Aby otrzymać identyfikator, należy zarejestrować Twoją aplikację korzystając z poniższego linka <a href="http://instagram.com/developer/" target="_blank"></a></td>
	</tr>
	<tr>
		<td class="col-1"><strong>Username</strong></td>
		<td>-</td>
		<td>nazwa użytkownika Instagram, którego galerię chcesz wyświetlić.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Sample tag</strong></td>
		<td>-</td>
		<td>tagi używane do wyświetlania obrazów.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Product tag</strong></td>
		<td>-</td>
		<td>jest stosowany do wyszukiwania podobnych obrazów, których początek tagu składa się z wybranych parametrów. Np.: <strong>#storetitleproductname</strong>, <strong>#productsku</strong>, <strong>#storetitleproductsku</strong>, itp.</td>
	</tr>				
</table>			
		