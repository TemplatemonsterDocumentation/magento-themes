<h3>Mapy Google</h3>
<p>Ten moduł pozwala na osadzenie mapy Google na Twojej stronie Magento. Domyślnie moduł oferuje dwie mapy: mapę na stronie kontaktowej oraz mapę w stopce innych stron. <br>Obie mapy można skonfigurować za pomocą panelu administracyjnego Magento w sekcji <strong>System > Configuration > Templatemonster > Google Map</strong>.</p>

<ol class="index-list">
	<li>
		<strong>Ustawienia osadzonej Mapy</strong> blok, który oznacza wyświetlanie mapy w stopce wszystkich stron oprócz strony kontaktowej.
	</li>
	<li>
		<strong>Ustawienia Mapy na stronie kontaktowej</strong> blok, który oznacza wyświetlanie mapy bezpośrednio na stronie kontaktowej.
		<figure class="img-polaroid"><img src="img/magento/googlemap.jpg" alt="" /></figure>
	</li>
	<li>
		Obie sekcje mają takie same możliwości konfigurowania wyglądu mapy.<figure class="img-polaroid"><img src="img/magento/googlemap-1.jpg" alt="" /></figure>
	</li>
</ol>
<p>Moduł posiada następujące opcje:</p>

<table class="options-table">
	<tr>
		<td class="col-1"><strong>API key</strong></td>
		<td>-</td>
		<td>Google Api Key jest niezbędne do poprawnego działania mapy. Możesz otrzymać i aktywować Google Api Key klikając <a href="https://code.google.com/apis/console/" target="_blank">link</a></td>
	</tr>
	<tr>
		<td class="col-1"><strong>Coordinates</strong></td>
		<td>-</td>
		<td>dane dotyczące lokalizacji będą pokazywane w centrum wyświetlanej mapy. Możesz uzyskać te dane przy użyciu strony internetowej <a href="http://maps.google.com/" target="_blank">Mapy Google</a> lub <a href="http://www.mapcoordinates.net/" target="_blank" rel="nofollow">używając</a></td>
	</tr>
	<tr>
		<td class="col-1"><strong>Sensor</strong></td>
		<td>-</td>
		<td>włączanie i wyłączanie geolokalizacji.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Zoom</strong></td>
		<td>-</td>
		<td>wartość rozszerzenia (nie powinna przekraczać 8).</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Map Type</strong></td>
		<td>-</td>
		<td>rodzaj wyświetlanej mapy (mapa drogowa, satelitarna, hybrydowa, itp.).</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Map width</strong></td>
		<td>-</td>
		<td>szerokość mapy.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Map height</strong></td>
		<td>-</td>
		<td>wysokość mapy.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Styles</strong></td>
		<td>-</td>
		<td>wyświetla style mapy. Aby zmienić wygląd mapy, skorzystaj ze stylów znajdujących się na stronie internetowej <a href="https://snazzymaps.com/" target="_blank" rel="nofollow">snazzymaps</a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Disable UI</strong></td>
		<td>-</td>
		<td>ukrywa elementy, które kontrolują mapę.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Scrollwheel</strong></td>
		<td>-</td>
		<td>wyłącza opcję przewijania myszką.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Draggable</strong></td>
		<td>-</td>
		<td>pozwala przesuwać mapę za pomocą lewego przycisku myszki lub używając palców dla urządzeń dotykowych (z czujnikiem).</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Add marker</strong></td>
		<td>-</td>
		<td>pozwala dodać marker do mapy.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Marker coordinates</strong></td>
		<td>-</td>
		<td>ustawia współrzędne markera.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Marker title</strong></td>
		<td>-</td>
		<td>tekst, który będzie wyświetlany w dymku, gdy skierujesz wskaźnik myszki na niego.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Marker image</strong></td>
		<td>-</td>
		<td>ścieżka do obrazu markera, jeśli chcesz zastąpić go domyślnym. Obraz zostanie zapisany w folderze instalacji Twojego Magento <strong>skin/frontend/default/themeXXX/images/</strong>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Infowindow</strong></td>
		<td>-</td>
		<td>dodatkowe informacje, które będą wyświetlane po kliknięciu markera.</td>
	</tr>
</table>
