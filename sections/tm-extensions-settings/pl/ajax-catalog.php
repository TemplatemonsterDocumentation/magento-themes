<h3>Ajax Catalog</h3>
<p>Szybki i łatwy wybór spośród najróżniejszych produktów przyczynia się do tworzenia pozytywnej decyzji dotyczącej zakupów w sklepie internetowym. Moduł Katalog Ajax, który zapewnia taką możliwość, jest najbardziej popularnym modułem pośród sklepów internetowych opartych na różnych platformach. On umożliwia szybkie i łatwe wyszukiwanie oraz filtrowanie potrzebnych produktów w zależności od wybranych parametrów, bez przeładowania strony.</p>						
<p>W celu skonfigurowania tego modułu, należy przejść do sekcji <strong>Sklepy > Ustawienia > Konfiguracja (Stores -> Settings -> Configuration)</strong>.</p>
<figure class="img-polaroid"><img src="img/magento/compare.jpg" alt="" /></figure>			
<p>Zaten należy przejść do menu <strong>TemplateMonster -> Catalog Ajax</strong> po lewej stronie, aby przejść do interfejsu ustawień modułu.</p>
<figure class="img-polaroid"><img src="img/magento/ajaxcatalog.jpg" alt="" /></figure>		
<p>Przyjrzyjmy się do opcji modułu:</p>			
<table  class="options-table">
	<tr>
		<td class="col-1"><strong>Paginacja (Pagination) </strong></td>
		<td>-</td>
		<td>paginacja wykazu. Jeśli wykaz zawiera więcej niż jedną stronę z produktami, ta opcja umożliwia przełączanie pomiędzy wewnętrznymi stronami, bez konieczności przeładowywania strony głównej.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Pokaż liczbę elementów (Show number items)</strong></td>
		<td>-</td>
		<td>wyświetlana liczba produktów na stronie. Ta opcja umożliwia zmianę liczby produktów wyświetlanych na stronie pojedynczego wykazu bez przeładowywania.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Sortuj według (malejąco/rosnąco) (Sort by (desc/asc) )</strong></td>
		<td>-</td>
		<td>uporządkowanie produktów. Elementy można sortować bez konieczności przeładowywania strony głównej.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Kolejność na liście (cena, pozycja, nazwa) (List order (price, position, name) )</strong></td>
		<td>-</td>
		<td>parametry uporządkowania. To pozwala zdefiniować parametry sortowania bez przeładowania strony.</td>
	</tr>		
	<tr>
		<td class="col-1"><strong>Tryb podglądu (siatka/wykaz) (View mode (grid/list) )</strong></td>
		<td>-</td>
		<td>układ wyświetlania produktów. Ta opcja umożliwia określenie układu produktów na stronach wykazu bez ich przeładowywania.</td>
	</tr>		
	<tr>
		<td class="col-1"><strong>Filtr warstwy (Layer filter)</strong></td>
		<td>-</td>
		<td>filtrowanie według atrybutów. To jest opcja głównego modułu. Umożliwia filtrowanie AJAX według atrybutów i parametrów znajdujących się w kolumnie standardowych filtrów sklepów.</td>
	</tr>		
</table>			
<p>Zapraszamy do korzystania ze wszystkich opcji jednocześnie lub tylko niektórych z nich, to już zależy od Ciebie.</p>	