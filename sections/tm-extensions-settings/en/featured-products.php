<h3 class="item1">Featured Products</h3>
<h4>General</h4>
<p align="justify">The widget is displayed as any other Magento widget. To set up the widget, navigate to <strong>Content > Widgets</strong> and press the <strong>Add widget</strong> button.</p>
<figure class="img-polaroid"><img src="img/magento/featured-2.jpg" alt="" /></figure>
<p align="justify">
In the <strong>Storefront Properties</strong> section in the <strong>Type</strong> field, select <strong>Featured products</strong>. Press<strong> Continue</strong>.</p>
<figure class="img-polaroid"><img src="img/magento/featured-15.jpg" alt="" /></figure>
<p align="justify">Then, fill in all the other fields and specify the place, where you want to display the module in the <strong>Layout Updates</strong> section.
</p>
<figure class="img-polaroid"><img src="img/magento/featured-3.jpg" alt="" /></figure>
<p>To set up the widget, go to <strong>Widget Options</strong> section on the left.</p>
<figure class="img-polaroid"><img src="img/magento/featured-4.jpg" alt="" /></figure>
<p align="justify">In this section you should select the product type, which you want to display, in the <strong>Product types</strong> option. All the types have the same settings.</p>
<figure class="img-polaroid"><img src="img/magento/featured-5.jpg" alt="" /></figure>

