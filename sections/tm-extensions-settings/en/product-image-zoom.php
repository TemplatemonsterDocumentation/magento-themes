<h3>Product image zoom</h3>
<p>
	In order to configure Image zoom, navigate to <strong>System -> Configuration</strong> tab.<br> In <strong>Product image zoom</strong> section you will notice a lot of settings. This module allows you to choose two ways to display product image on the product page: default and improved (Unite gallery). You can also disable the module.
</p>
<p>
	The default display type isn't customizable. You can simply turn it on or off. <br>
	The second type is a <strong>Unite gallery</strong> that has a lot of different settings.
</p>
