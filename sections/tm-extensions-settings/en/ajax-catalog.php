<h3>Ajax Catalog</h3>
<p>Quick and easy selection among the variety of products contributes to making a positive decision on purchasing in your online shop. Ajax Catalog module that provides this possibility is the most popular module among the online shops based on various platforms. It allows you to find and filter the products you need according to the selected parameters quickly and easily without reloading the page.</p>						
<p>In order to configure this module, navigate to <strong>Stores > Settings > Configuration</strong> section.</p>
<figure class="img-polaroid"><img src="img/magento/compare.jpg" alt="" /></figure>			
<p>Then navigate to <strong>TemplateMonster > Catalog Ajax</strong> in the left side menu to go to the module settings interface.</p>
<figure class="img-polaroid"><img src="img/magento/ajaxcatalog.jpg" alt="" /></figure>		
<p>Let’s review module options:</p>			
<table  class="options-table">
	<tr>
		<td class="col-1"><strong>Pagination </strong></td>
		<td>-</td>
		<td>listing pagination. If your listing includes more than one page with products, this option allows you to switch between the inner pages without reloading the main page.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Show number items</strong></td>
		<td>-</td>
		<td>number of products displayed on the page. This option allows you to change the number of products displayed on the single listing page without reloading.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Sort by (desc/asc)</strong></td>
		<td>-</td>
		<td>products ordering. You can sort items without reloading the main page.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>List order (price, position, name)</strong></td>
		<td>-</td>
		<td>sorting parameters. It allows you to define the sorting parameter without reloading the page.</td>
	</tr>		
	<tr>
		<td class="col-1"><strong>View mode (grid/list)</strong></td>
		<td>-</td>
		<td>products display layout. This option allows you to specify products layout for listing pages without reloading them.</td>
	</tr>		
	<tr>
		<td class="col-1"><strong>Layer filter</strong></td>
		<td>-</td>
		<td>filter by attributes. That is the main module option. It enables AJAX filter by attributes and parameters located in the standard shop filters column.</td>
	</tr>		
</table>			
<p>Feel free to use all the options at once or just a few of them, it’s up to you.</p>	