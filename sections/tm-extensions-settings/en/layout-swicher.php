<h3 class="item1">Layout Switcher</h3>
<p>Using this module you can change the layout of: header, footer, layout product grid listing and product page listing</p>
<h3>General</h3>
<p align="justify">To activate the module, navigate to <strong>Store > Configuration >  Templatemonster > <span class="item1">Layout Switcher</span></strong>. Here you'll find option to enable module.</p>
<figure class="img-polaroid"><img src="img/magento/layout-swicher.jpg" alt="Layout Switcher" /></figure>

<table  class="options-table">
    <tr>
        <td class="col-1"><strong>Enabled</strong></td>
        <td>-</td>
        <td>in this field, you turn on/off module.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>Default theme</strong></td>
        <td>-</td>
        <td>theme to use by default.</td>
    </tr>

        
</table>
<div class="alert alert-warning">
  <p>You can change <span class="col-1"><strong>Default theme</strong></span> either via<span class="item1"> <strong>Layout Switcher</strong></span> module or in the Stores settings [<strong>Stores</strong> &gt; <strong>All Stores</strong>].</p>
  <p>If you've changed <span class="col-1"><strong>Default theme</strong></span>  via<span class="item1"> <strong>Layout Switcher </strong>module you don't need to change it in the </span>Stores settings.</p>
</div>