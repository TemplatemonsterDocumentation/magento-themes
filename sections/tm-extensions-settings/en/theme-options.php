<p align="justify">Find the <strong>&lt;website code=&quot;clothes_store&quot;&gt;</strong> line (in our case) and replace &quot;<strong>clothes_store</strong>&quot; (the  previous code) with the new one &lt;<strong>website code=&quot;your code&quot;&gt;</strong>. </p>
<p>
  <figure class="img-polaroid"><img src="img/magento/to-29.jpg" alt="parallax" /></figure>
</p>
