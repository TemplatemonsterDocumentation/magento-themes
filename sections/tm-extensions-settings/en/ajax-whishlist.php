<h3>Ajax Wishlist</h3>

                                <p align="justify">Products Wishlist is one of the standard e-commerce features. We have created the special module for the convenient use of this feature, it is based on AJAX technology and allows you to add products to the wishlist without having to navigate to the separate page.</p>
                                <p align="justify">In order to configure this module, you should navigate to Magento admin panel and choose the <strong>Stores > Configuration</strong> section in the left side menu.</p>
                                <figure class="img-polaroid">
                                    <div align=""><img src="img/magento/compare.jpg" alt="" /></div>
                                </figure>
                                <p align="justify">Then choose <strong> TemplateMonster > Ajax Wishlist </strong> item on the new page on the left side.</p>
                                <figure class="img-polaroid">
                                    <div align=""><img src="img/magento/wishlist.jpg" alt="" /></div>
                                </figure>
                                <p align="justify">Let's review the module configuration:</p>
                                <div align="justify">
                                    <table class="options-table">
                                        <tr>
                                            <td class="col-1"><strong>Enable</strong></td>
                                            <td>-</td>
                                            <td>this option allows you to enable or disable the wishlist.</td>
                                        </tr>
                                        <tr>
                                            <td class="col-1"><strong>Show spinner</strong></td>
                                            <td>-</td>
                                            <td>this option allows you to show the spinner during adding an item to the wishlist.</td>
                                        </tr>
                                        <tr>
                                            <td class="col-1"><strong>Show Success Message</strong></td>
                                            <td>-</td>
                                            <td>this option allows you to display the message which is shown after adding the product to the wishlist.</td>
                                        </tr>
                                        <tr>
                                            <td class="col-1"><strong>Success Message Text</strong></td>
                                            <td>-</td>
                                            <td>here you can enter the text you want to be in the pop-up window.</td>
                                        </tr>
                                    </table>
                                </div>
