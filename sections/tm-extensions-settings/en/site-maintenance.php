<h2 class="item1">Site Maintenance</h2>
<p align="justify">
    The <strong>Site Maintenance</strong> module for Magento 2 allows you to display the “Site under maintenance” page and let your customers know that now there is some technical work going on and that the website will soon become available.</p>
<div align="justify">To set up the module, navigate to <strong>Stores > Configuration</strong> and in the section on the right select <strong>Templatemonster > Site maintenance</strong>. <br>
  You’ll find three sections there:
</div>
<ul class="marked-list">
<li>Page configuration;</li>
    <li>Timer section;</li>
    <li>Form settings.</li>
</ul>

