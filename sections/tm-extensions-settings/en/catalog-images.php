<h3 class="item1">Catalog Images Grid</h3>
<h4>Catalog page settings</h4>
To configure the module, navigate to <strong>Stores -> Configuration</strong> tab and select <strong>Templatemonster -> Catalog images grid</strong> element on the left. <br>
This section contains two subsections:
<ul class="marked-list">
    <li>General</li>
    <li>Category page</li>
</ul>

<h4>General</h4>
General section contains only one setting: Enable, it completely enables/disables the module.
<figure class="img-polaroid"><img src="img/magento/catalog-images-3.jpg" alt="" /></figure>
<strong>Category page</strong> section contains settings for the category page.
<figure class="img-polaroid"><img src="img/magento/catalog-images-4.jpg" alt="" /></figure>
<figure class="img-polaroid"><img src="img/magento/catalog-images-5.jpg" alt="" /></figure>
Let’s review the settings of this section:
<table  class="options-table">
    <tr>
        <td class="col-1"><strong>Use Image </strong></td>
        <td>-</td>
        <td>select image type that you want to display on the category page. There are three options to choose: <strong>Thumbnail image</strong>, <strong>Category image</strong>, <strong>Custom font icon</strong>. If you select Custom font icon,  you’ll see Font Icon size instead of Image Width. Here you can specify the icon size in pixels.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>Category Images </strong></td>
        <td>-</td>
        <td>enables displaying images for categories.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>Subcategory Images </strong></td>
        <td>-</td>
        <td>enables displaying images for subcategories.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>Image Width </strong></td>
        <td>-</td>
        <td>image width.</td>
    </tr>       
    <tr>
        <td class="col-1"><strong>Columns count </strong></td>
        <td>-</td>
        <td>number of columns to display.</td>
    </tr>
    <tr>
        <td class="col-2"><strong>Count of categories to show </strong></td>
        <td>-</td>
        <td>number of categories to display.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>Count of subcategories to show </strong></td>
        <td>-</td>
        <td>number of subcategories to display.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>Show "View More" button</strong></td>
        <td>-</td>
        <td>if there are more categories, than specified in the previous option, you’ll see the <strong>View More</strong> button on the website. This option disables the display of this button.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>Show Before Products Grid </strong></td>
        <td>-</td>
        <td>displays the block above the product listing on the category page.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>Show After Products Grid </strong></td>
        <td>-</td>
        <td>displays the block below the product listing on the category page.</td>
    </tr>
</table>

<h4>Widget settings</h4>
In addition to the static display of the block on the product listing page, you can also display the list of categories on the page of your choice with the help of a widget.
<p>To add a widget, navigate to <strong>Content -> Widgets</strong> menu and press <strong>Add Widget</strong>. In the appeared window, in the <strong>Type</strong> field, select <strong>Catalog Image grid</strong>. In the <strong>Design Themes</strong> select the theme, that you want to apply this widget to.</p>
<figure class="img-polaroid"><img src="img/magento/catalog-images-6.jpg" alt="" /></figure>
In the <strong>Layout Updates</strong> section, select a page and a <strong>Template</strong> for widget display.
<figure class="img-polaroid"><img src="img/magento/catalog-images-7.jpg" alt="" /></figure>

<p>Navigate to <strong>Widget options</strong> tab and you will see available options. Part of them is identical with the category page options.</p>
<figure class="img-polaroid"><img src="img/magento/catalog-images-8.jpg" alt="" /></figure>
Let’s review the options that differ
<table  class="options-table">
    <tr>
        <td class="col-1"><strong>Category </strong></td>
        <td>-</td>
        <td>select the category, the subcategories of which you want to display in form of a grid with images.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>Container css class </strong></td>
        <td>-</td>
        <td>specify the css class for a container that will be used for wrapping a grid with images.</td>
    </tr>           
</table>


<h5>Product categories settings</h5>
In order to configure the module to the fullest extent, configure images, thumbnails, specify the icon class, if needed. Navigate to Products - > Categories, select any category and you’ll see four options.
<figure class="img-polaroid"><img src="img/magento/catalog-images-9.jpg" alt="" /></figure>
<table  class="options-table">
    <tr>
        <td class="col-1"><strong>Category image </strong></td>
        <td>-</td>
        <td>is the standard functionality of the engine. After you upload an image, it will be displayed on the category page. You can also display it in the module.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>Thumbnail </strong></td>
        <td>-</td>
        <td>a setting for uploading the image that will be used for working with the module.</td>
    </tr>  
    <tr>
        <td class="col-1"><strong>Activate Image Grid </strong></td>
        <td>-</td>
        <td>this option includes displaying a grid of the selected category, the settings of which are set up in <strong>Stores -> Configuration</strong>.</td>
    </tr> 
    <tr>
        <td class="col-1"><strong>Css class for Font Icon </strong></td>
        <td>-</td>
        <td>css-icon class (if you want to use iconic fonts instead of images).</td>
    </tr>          
</table>