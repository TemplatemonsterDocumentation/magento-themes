<p align="justify">You will be asked to input two pieces of information into the module settings: 1] Your <span class="col-1"><strong>Template ID </strong></span> and 2] Your <strong>Order ID</strong>. Your  template ID and your order ID  can be found in the template order email or your TemplateMonster account. </p>
<p align="justify">Once you have specified the data, you can click the <strong>Check Updates</strong> button. If there any updates for your current theme are available, you can  update your theme/ using the <strong>Download </strong>button.</p>
<p align="justify">Press the &quot;<strong>Backup</strong>&quot; to perform a full backup. Doing this will ensure that you will be able to   restore an older version of your site.
  </p>
