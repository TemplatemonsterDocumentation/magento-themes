<h4>Social Settings</h4>
<p class="indent">This section is responsible for social icons' display and configuration in the newsletter pop-up. You need to enable Social Icons option.</p>
<figure class="img-polaroid"><img src="img/magento/new-23.png" alt="" /></figure>
<p>Main settings subsections allow you to set colors and social icons' appearance for the following services:</p>
<ul class="marked-list">
	<li>Facebook</li>
	<li>Twitter</li>
	<li>LinkedIn</li>
	<li>Google+</li>
	<li>Youtube</li>
	<li>Vimeo</li>
	<li>Pinterest</li>
	<li>Instagram</li>
	<li>Foursquare</li>
	<li>Tumblr</li>
	<li>RSS</li>
</ul>

<ol class="index-list">
	<li>
		<p>You can enable or disable certain icon appearance by using the  <strong>Enabled ……. icon</strong> option. This option activating will make additional sections appear to let you configure each icon individually.</p>
		<figure class="img-polaroid"><img src="img/magento/new-15.jpg" alt="" /></figure>
	</li>
	<li>
		<p>This section  includes  <strong>Use default icon colors</strong> option. You can  change the icons color. </p>
		<figure class="img-polaroid"><img src="img/magento/new-16.jpg" alt="" /></figure>
		By default, the one is enabled, so that all the icons have their standard colours that are specific for each social network. However, if you'd like to define icons color on your own, you should disable this option.
	</li>
	<li>
		<p>Each social network section has a <strong>Link</strong> option available. It lets you to enter the link to any page (your profile) in any of available social network.</p>
		<figure class="img-polaroid"><img src="img/magento/new-13.jpg" alt="" /></figure>
	</li>
	<li>
		<p>After disabling the <strong>Use default icon colors</strong> option, you will see  some new fields  for each social network icon configuration:</p>
		<ul class="marked-list">
		  <li><strong>Icon background</strong></li>
			<li><strong>Icon color</strong></li>
			<li><strong>Icon background hover</strong></li>
			<li><strong>Icon hover color</strong></li>
		</ul>
		<figure class="img-polaroid"><img src="img/magento/new-25.png" alt="" /></figure>
	</li>
</ol>
<p>Each  of these options allow you to customize the icon appearance. The ones provide you the option to set the icon background, color for the initial state and on hover (after hovering the icon with the mouse cursor). <br>
Please make sure you save the configurations! </p>

<p>Just in case you'd like to return to the default settings, use the <strong>Reset</strong> button.</p>
