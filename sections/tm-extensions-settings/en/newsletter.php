<h4 align="justify">Social Settings</h4>
<p align="justify" class="indent">This section is responsible for social icons display and configuration in the newsletter pop-up. It consists of a few subsections that have similar options.</p>
<figure class="img-polaroid">
  <img src="img/magento/new-12.jpg" alt="" />
</figure>
<p align="justify">Main settings' subsection allows you to set colors and social icons' appearance for the following services:</p>

  <ul class="marked-list">
    <li>Facebook</li>
    <li>Twitter</li>
    <li>LinkedIn</li>
    <li>Google+</li>
    <li>Youtube</li>
    <li>Vimeo</li>
    <li>Pinterest</li>
    <li>Instagram</li>
    <li>Foursquare</li>
    <li>Tumblr</li>
    <li>RSS</li></ul>
<ol class="index-list">
  <li>
    <p align="justify">You can enable or disable certain icon appearance by using the respective <strong>Enabled ……. icon</strong> option. This option activation will make a respective sections appear to let you configure each icon individually.</p>
    <figure class="img-polaroid">
      <img src="img/magento/new-15.jpg" alt="" />
    </figure>
  </li>
  <li>
    <p align="justify">This section also includes the <strong>Use default icon colors</strong> option. It is used for changing icons color. </p>
    <figure class="img-polaroid">
     <img src="img/magento/new-16.jpg" alt="" />
    </figure>
    <div align="justify"> By default, it's enabled, so all the icons have their corporate [standard] color that is specific for each social network. However, if you'd like to define icons color on your own, you should disable this option. </div>
  </li>
  <li>
    <p align="justify">Each social network section has a <strong>Link</strong> option available. It lets you define the link to any page [your profile] in a respective social network.</p>
    <figure class="img-polaroid">
      <img src="img/magento/new-13.jpg" alt="" />
    </figure>
  </li>
  <li>
    <p align="justify">After disabling the <strong>Use default icon colors</strong> option, you will see the new fields in subsections for each social network icon configuration:</p>
    
      <ul class="marked-list">
        <li>Icon background</li>
        <li>Icon color</li>
        <li>Icon background hover</li>
        <li>Icon hover color</li>
      </ul>
    
    <figure class="img-polaroid">
      <img src="img/magento/new-14.jpg" alt="" />
    </figure>
  </li>
</ol>
<p align="justify">All of these options allow you to customize the icon appearance. They provide you with the option to set the icon background and color for the initial state and on hover [after hovering the icon with the mouse cursor]. <br />
  Please be sure to save your configuration! </p>
<p align="justify">Just in case you'd like to return all the default settings, there is a <strong>Reset</strong> button available next to the <strong>Save</strong> one.</p>
