<h3>Newsletter Pop-up</h3>
<p class="indent">Emailing a newsletter has always been the important part of every marketing strategy. This method is used all over the world now, so it will definitely help you to get more orders and subscribers. That's why we are proud to present you the new Newsletter pop-up module. It displays the popup window with subscription form and social icons on your online-shop web-site.</p>

<h4>Global configuration</h4>
In order to configure the module, navigate to <strong>Stores > Settings > Configuration</strong> section.
<figure class="img-polaroid"><img src="img/magento/new-0.png" alt="" /></figure>
Choose the <strong>TEMPLATEMONSTER > Newsletter Pop-up</strong> in the left side menu to go to the module settings interface.<br>
The module has 2 sections, and each one contains a specific set of options:
<ul class="marked-list">
	<li>
		<strong>General</strong>
	</li>
	<li>
		<strong>Social Settings</strong>
	</li>
</ul>
 <figure class="img-polaroid"><img src="img/magento/new-2.jpg" alt="" /></figure>

<p><strong>General</strong> section includes main module settings, and <strong>Social settings</strong> section, that serves for pop-up window social icons display setup.</p>

<h4>General</h4>
As mentioned above, this section includes main module settings. The <strong>General</strong> section indicate the current version of the module. You can enable or disable module here, enter a newsletter title and its content, specify buttons color, define the width of the window, etc. Let’s review each option separately.
<figure class="img-polaroid"><img src="img/magento/new-4.png" alt="" /></figure>
<ol class="index-list">
	<li>
		In order to enable or disable module, you need to use <strong>Enabled</strong> option. It also provides the possibility to  disable  the module.
	</li>
	<li>		
		<p><strong>Show on startup</strong> option allows you to configure the  pop-up display.</p>
		<figure class="img-polaroid"><img src="img/magento/new-6.png" alt="" /></figure>
		
	</li>
	<li>		
		<p><strong>Show on footer</strong> option allows you to display the pop-up when navigating to site footer using a scrollbar. </p>
		<figure class="img-polaroid"><img src="img/magento/new-9.png" alt="" /></figure>
	</li>
	<li><strong>Title.</strong>  Set newsletter main title here.</li>
    <p>
      <figure class="img-polaroid"><img src="img/magento/new-20.png" alt="" /></figure>
    </p>
    <li><strong>Content</strong> option. Here you can create/edit the content is displayed in the Newsletter Popup.
      <p><figure class="img-polaroid"><img src="img/magento/new-21.png" alt="" /></figure></p>
    </li>
    <li> <strong>Submit Button text</strong>
&amp; <strong>Cancel button text</strong> options. Enter the text you want to display in Submit  and Cancel button individually.

  <p><figure class="img-polaroid"><img src="img/magento/new-22.png" alt="" /></figure></p>
  </li>
    <li><strong>Pop-Up width</strong> option allows you to define the newsletter pop-up window width. The size value can be set in pixels only.	
