<h3>MegaMenu</h3>
<p class="indent">This module replaces standard menu with the advanced one, that allows you to configure subcategories display up to the 2nd nesting level, add static blocks to the dropdown menu, assign labels, display products, etc.</p>
<strong>MegaMenu</strong> is included to templates by default, but you can enable or disable it in the <strong>Stores > Configuration > TemplateMonster > Mega Menu</strong> section.
<figure class="img-polaroid"><img src="img/magento/megamenu-1.jpg" alt="" /></figure>

<ol class="index-list">
	<li>You can configure appearance of each dropdown menu on the category configuration page that can be found in the <strong>Products > Categories -> Megamenu</strong> section.
		<a class="pretty_image" href="img/magento/Megamenu.png"><figure class="img-polaroid"><img src="img/magento/Megamenu.png" alt="" /></figure></a>
			
	</li>
	<li>
		In order to add static block to the dropdown menu, first you need to create it in the <strong>CMS > Static Blocks</strong> section and then it will be available for display in the dropdown menu. 
		<figure class="img-polaroid"><img src="img/magento/megamenu-static.jpg" alt="" /></figure>
	</li>
	<li>
		This module allows to add a label to any products category. You can do this on the categories menu page under <strong>Products > Categories -> Megamenu</strong>. There is a 'custom class' option available, so that you can create styles for this label using css rules.
		<figure class="img-polaroid"><img src="img/magento/megamenu-badge.jpg" alt="" /></figure>
	</li>		
	<li>
		You can define number of columns in the mega menu. In order to do this you should enable columns display and specify the number of columns needed. 
		<figure class="img-polaroid"><img src="img/magento/megamenu-column.jpg" alt="" /></figure>
	</li>		
	<li>
		In order to display products, enable the <strong>Turn on products show</strong> option, and choose a few items among the list. 
		<figure class="img-polaroid"><img src="img/magento/megamenu-product.jpg" alt="" /></figure>
	</li>		
	<li>
		Subcategory image can be added in this subcategory <strong>MegaMenu</strong> section. 
		<figure class="img-polaroid"><img src="img/magento/megamenu-image.jpg" alt="" /></figure>
		<p>However, to make the image appear, you also need to enable its display by choosing <strong>Turn on images for subcategories</strong>, in the parent category <strong>MegaMenu</strong> section.</p>
		<figure class="img-polaroid"><img src="img/magento/megamenu-image-1.jpg" alt="" /></figure>
	</li>
	<li>
		Megamenu layouts can be edited by using <strong>Custom template for megamenu item</strong> option. The source files of these templates are located in the following directory: <strong>app\design\frontend\TemplateMonster\theme764\TemplateMonster_Megamenu\templates\html\topmenu\</strong>.
		<figure class="img-polaroid"><img src="img/magento/megamenu-template.jpg" alt="" /></figure>
	</li>
</ol>