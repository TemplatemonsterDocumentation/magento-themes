<h3>Instagram module</h3>
<p>Instagram module allows you to display Instagram gallery on your store.<br>
The given module allows to display two types of galleries: user gallery and tag gallery.</p>						
<p>All configuration options are available in admin panel in <strong>System > Configuration > Templatemonster > Instagram</strong> section.</p>
<figure class="img-polaroid"><img src="img/magento/instagram.jpg" alt="" /></figure>			
<p>The module has the following options:</p>			
<table  class="options-table">
	<tr>
		<td class="col-1"><strong>Client ID</strong></td>
		<td>-</td>
		<td>Instagram app identifier. In order to receive the identifier,  you need to register your app using following <a href="http://instagram.com/developer/" target="_blank">link</a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Username</strong></td>
		<td>-</td>
		<td>Instagram username which gallery you want to display.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Sample tag</strong></td>
		<td>-</td>
		<td>tags used to display images.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Product tag</strong></td>
		<td>-</td>
		<td>is used to find the similar images, where the beginning of the tag consists of the chosen parameters set. E. g.: <strong>#storetitleproductname</strong>, <strong>#productsku</strong>, <strong>#storetitleproductsku</strong>, etc.</td>
	</tr>				
</table>			
		