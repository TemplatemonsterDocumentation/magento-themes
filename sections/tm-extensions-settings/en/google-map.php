<h3>Google Map</h3>
<p>The given module allows you to embed the Google map to your Magento website. By default, module provides you with two maps: the map on contact page and the map in the footer of other pages.					
<br>Both maps can be configured via Magento Admin Panel in <strong>System > Configuration > Templatemonster > Google Map</strong> section.</p>

<ol class="index-list">
	<li>
		<strong>Embed Map Settings</strong> block stands for the display of the map in the footer of all pages except the contact page.
	</li>
	<li>
		<strong>Contacts Map Settings</strong> block stands for the display of the map directly on the contact page.<figure class="img-polaroid"><img src="img/magento/googlemap-1.jpg" alt="" /></figure>
	</li>
	<li>
		Both sections have the same options for configuring the appearance of the map.<figure class="img-polaroid"><img src="img/magento/googlemap-2.jpg" alt="" /></figure>
	</li>
</ol>
<p>The module has the following options:</p>

<table class="options-table">
	<tr>
		<td class="col-1"><strong>API key</strong></td>
		<td>-</td>
		<td>Google Api Key is necessary for the correct work of the map. You can receive and activate it by following the <a href="https://code.google.com/apis/console/" target="_blank">link</a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Coordinates</strong></td>
		<td>-</td>
		<td>location data which will be shown in the centre of the displayed map. You can get this location data using <a href="http://maps.google.com/" target="_blank">Google Maps</a> website or using <a href="http://www.mapcoordinates.net/" target="_blank" rel="nofollow">this one</a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Sensor</strong></td>
		<td>-</td>
		<td>switching geolocation on and off.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Zoom</strong></td>
		<td>-</td>
		<td>enlargement value (should not exceed 8).</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Map Type</strong></td>
		<td>-</td>
		<td>type of the displayed map (roadmap, satellite, hybrid, etc.).</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Map width</strong></td>
		<td>-</td>
		<td>width of the map.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Map height</strong></td>
		<td>-</td>
		<td>height of the map.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Styles</strong></td>
		<td>-</td>
		<td>display style of the map. In order to change the appearance of the map, use the styles provided on the website <a href="https://snazzymaps.com/" target="_blank" rel="nofollow">snazzymaps</a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Disable UI</strong></td>
		<td>-</td>
		<td>hides the elements which control the map.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Scrollwheel</strong></td>
		<td>-</td>
		<td>disables the mouse scroll option.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Draggable</strong></td>
		<td>-</td>
		<td>enables you to move the map using the left button of your mouse or using fingers for touch (sensor) devices.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Add marker</strong></td>
		<td>-</td>
		<td>allows you to add the marker to the map.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Marker coordinates</strong></td>
		<td>-</td>
		<td>sets the marker coordinates.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Marker title</strong></td>
		<td>-</td>
		<td>text which will be displayed in the tool-tip while hovering over it.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Marker image</strong></td>
		<td>-</td>
		<td>path to the marker image, if you want to replace the default one. The image is saved in the folder <strong>skin/frontend/default/themeXXX/images/</strong> of your Magento installation.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Infowindow</strong></td>
		<td>-</td>
		<td>additional information which will be displayed if you click the marker.</td>
	</tr>
</table>
