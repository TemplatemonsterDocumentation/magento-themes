<h3>Special price countdown</h3>
<p class="indent">This module allows displaying the timer of discount expiration for special products.<br>
You can enable/disable the module via Magento Admin Panel, in <strong>System > Configuration > Templatemonster > Special Price Countdown</strong> section.</p>
<figure class="img-polaroid"><img src="img/magento/countdown.png" alt="countdown" /></figure>
<p>The module has three sections:</p>
<table class="options-table">
	<tr>
		<td class="col-1"><strong>1. Product Page View</strong></td>
		<td>-</td>
		<td>this section is responsible for the output of the counter on the product page. In the "Selector": You can specify the class of the block, after which the counter is displayed.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>2. Grid/List View</strong></td>
		<td>-</td>
		<td>this section is responsible for the output of the counter on the catalog product page;</td>
	</tr>
	<tr>
		<td class="col-1"><strong>3. Home(Sales) View</strong></td>
		<td>-</td>
		<td>this section is responsible for the output of the counter on the home page.</td>
	</tr>
</table>
<p class="indent">In order to edit the appearance of the timer, you need to edit the <strong>styles.css</strong> file located in the <i class="file-path">skin/frontend/tm_themes/themeXXX/css/tm/specialpricecountdown</i> folder.<p>