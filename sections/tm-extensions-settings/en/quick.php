<h3>Quick View</h3>
<p align="justify">Let your customers to preview  product information and add it to cart right from the pop-up window on a category page.</p>
<figure class="img-polaroid"><img src="img/magento/quick-10.png" alt="" /></figure>
<p align="justify">In order to configure this module, navigate to <strong>Stores &gt; Settings &gt; Configuration</strong> section. </p>

<p>Then in the panel on the left, tap <strong> TemplateMonster. </strong>Then choose <strong>Quick View </strong>to go to the module settings interface.</p>
 <figure class="img-polaroid"><img src="img/magento/quick-3.png" alt="" /></figure>


<p>There are 2 tabs for you to complete: <strong>General </strong>and <strong>Pop-Up Settings</strong>.</p>
