<h4>Blog Category</h4>
<p align="justify">Navigate to the <strong>TEMPLATEMONSTER</strong> section in the left sidebar. Find <strong>Blog</strong> and click <strong>Categories</strong>. Here you will see the list of categories. You can either delete or edit the category by clicking the <strong>Select</strong> button in the <strong>Action</strong> column.
        <br> To create a new category, you need to click the <strong>Add New Category</strong> button at the top. In the window, you will see several sections.</p>
