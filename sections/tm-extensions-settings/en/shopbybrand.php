<h3 class="item1">Shop by Brand</h3>
<p align="justify" class="indent">Help Customers quickly find out the brands and products they  consider, thus saving them much more time by using the <a href="https://www.templatemonster.com/?utm_expid=2187874-88.E4frEvLdRUiVnTFNAZQ_cw.0">Template Monster</a> <strong>Shop By Brand </strong> module. Sort products by <strong>brand</strong>, colour, style, size, material, pattern, and climate. The  extension is equipped with many interesting functions. </p>
<p align="justify" class="indent">
  <strong>SHOP  BY BRAND  FEATURES:</strong></p>
<ol class="marked-list">
    <li>
  Display brands' top link in <strong>top menu</strong>.</li>
    <li>Set <strong>number</strong> of brands shown in top menu.</li>
    <li><strong>Add</strong> and <strong>create</strong> unlimited brands with names, logos, etc. </li>
     <li><strong>Configurate </strong>brand attributes. </li> 
    <li><strong>Attach</strong> a range or a group of products to the <strong>specified brand.</strong></li> 
    <li>Take shoppers to a page where all in-store brands are <strong>listed</strong>.</li> 
    <li>Display<strong> short description </strong>on the brand listing page. </li> 
    <li>Create <strong>description pages</strong> for each in-store brands. </li> 
    <li>Show <strong>all  products of a brand block</strong> on each brand detailed page. </li> 
    <li><strong>Display </strong>brand name on the product page </li> 
    <li>Use a brand logo and brand banner on each <strong>brand  page</strong>. </li> 
    <li>Use a brand logo on  the brand <strong>listing page</strong> and <strong>product page</strong>. </li> 
    <li>Set a <strong>logo</strong> size. </li> 
    <li><strong>Choose  place</strong> on the product page   to put the logo in. </li> 
    <li>Use brand <strong>sidebar brand block</strong> with brand name and logo.</li>
</ol>
<p>In order to customize the module, click the <strong>Stores > Settings > Configuration</strong>.</p> 


