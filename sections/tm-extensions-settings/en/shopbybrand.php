<h2 class="item1">Shop by Brand
</h2>
<p>In order to customize the module, click the <strong>Stores > Settings > Configuration</strong>.</p> 



<p align="justify">Then choose the <strong>TEMPLATEMONSTER > Shop by Brand</strong> in the left side menu and you'll move to the module settings interface. </p>
<figure class="img-polaroid"><img src="img/magento/shop/tm.png" alt="" /></figure>
<p align="justify">The tab helps you configure the general information of the module.</p>
<div class="alert alert-info">
  <p> <i class="fa fa-info-circle" style="font-size:24px"></i> Check the tutorial on <a href="https://zemez.io/magento/support/how-to/magento-2-1-x-manage-shop-brand-module/"> How to Manage Shop By Brand Module</a>.</p>
</div>
<h3>Add a New Brand</h3>



If you want to add a new brand, click on the <strong>Add New Brand</strong> button at the top right corner, then fill out data about that new brand. 
<p><figure class="img-polaroid"><img src="img/magento/shop/addbr.png" alt="" /></figure></p>
<h5>There are 2 tabs  for you to complete:  <strong>General</strong> and <strong>Product Brand</strong>.</h5>
