<h3>Brand  Manager</h3>
<div align="justify">In the Admin panel, go to <strong>Template Monster &gt; Shop By Brand &gt; Brand Manage</strong>. This will bring you to the <strong>Brand  Manager</strong>.
</div>
<p align="justify">Hire you can <em>add</em>, <em>edit</em>, <em>manage</em> information regarding all in-store Brands, and <em>delete</em> Brands if you want.</p> 
<figure class="img-polaroid"><img src="img/magento/shop/brman.png" alt="" /></figure>


<p align="justify">All brands are gathered in a grid that displays <strong> ID</strong>, <strong>Name</strong> [e.g. Bogner, SiSi, Asos, etc.], <strong>Status</strong> [Enable/Disable]. In <strong>Action</strong> column you are able to choose the needed action, either you want to edit or delete one, several or all Brands at once. </p>
<p align="justify"><strong>NB: </strong>If you've ticked all or  several Brands to  edit you can   edit general information in a table only. If you want to edit a brand, just click on the Edit link in the corresponding row.</p>
<p>
<figure class="img-polaroid"><img src="img/magento/shop/all.png" alt="" /></figure>
</p>
<p>The Brands can easily be filtered. To set filter parameters click the <strong>Filters</strong> button.</p>
<p>
  <figure class="img-polaroid"><img src="img/magento/shop/filter.png" alt="" /></figure>
</p>
<h3>Add a New Brand</h3>



If you want to add a new brand, click on the <strong>Add New Brand</strong> button at the top right corner, then fill out data about that new brand. 
<p><figure class="img-polaroid"><img src="img/magento/shop/addbr.png" alt="" /></figure></p>
<h5>There are 2 tabs  for you to complete:  <strong>General</strong> and <strong>Product Brand</strong>.</h5>
<h5>GENERAL TAB</h5>
<div align="justify">
  <p>In this tab, you can <strong>Enable</strong> or <strong>Disable</strong> Brand, add general details for the brand such as <strong>Name</strong> and <strong>Page Title</strong>. You also can add <strong>URL key</strong>.</p>
  <p><figure class="img-polaroid"><img src="img/magento/shop/genbr.png" alt="" /></figure></p>
  <p>You can able to specify <strong>Brand Logo</strong>, <strong>Brand Page Banner</strong> [max. 1240 x 184] and <strong>Product Page Banner</strong>, you can  upload brand images from here. You are able to see  image size under the uploaded images thumbnail.</p>
  <p><figure class="img-polaroid"><img src="img/magento/shop/uploadime.png" alt=""  /></figure></p>
  <p>Use the <strong>Short Description</strong> [max. 250 characters; It's 1-2 sentences.] and <strong>Main Description</strong> fields to add brand description text which you want  to be shown on the frontend. </p>
  <p><figure class="img-polaroid"><img src="img/magento/shop/descript.png" alt="" /></figure>
  </p>
</div>
<div class="alert alert-info">
    <p align="justify"><strong>Note:</strong></p>
    <div align="justify">
      <p>You have to enable <strong>Banner</strong>, <strong>Logo</strong> and <strong>Description</strong> option from configuration setting of Shop By Brand, otherwise  <strong>Banner</strong>, <strong>Logo</strong> and <strong>Description</strong> won't be on the frontend. </p>
</div>
</div>
Besides, you can add  Meta Keywords and Meta Description for its <strong>Brand Detailed Page</strong>.
