<h4>Adding Slides</h4>
<p align="justify">You can add new slides only after saving the slider. Click <strong>Save and Continue Edit</strong> button in the top right corner. When the slider is saved, switch to <strong>Slider Items</strong> tab.</p>
<p>
  <figure class="img-polaroid"><img src="img/magento/slide-13.png" alt="" /></figure>
