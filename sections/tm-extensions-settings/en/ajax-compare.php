<h3 align="justify">Ajax Compare</h3>
                                <p align="justify">Products compare is one of the standard e-commerce features. We have created the special module for the convenient use of this feature, it is based on AJAX technology, and allows you to add products to compare without having to navigate to the separate page. </p>
                                <p align="justify">You can either enable or disable this module. In order to do this, navigate to the admin panel and choose the <strong>Stores > Configuration</strong> section in the left side menu.</p>
                                <figure class="img-polaroid">
                                    <div align=""><img src="img/magento/compare.jpg" alt="" /></div>
                                </figure>
                                <p align="justify">Then choose the <strong> TemplateMonster > Product Compare Ajax </strong> item on the new page on the left side.</p>
