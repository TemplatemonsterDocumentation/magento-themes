<h3>Product image zoom</h3>
<p>
	Для настройки Image zoom вам необходимо перейти на вкладку <strong>System -> Configuration</strong>.<br> В секции <strong>Product image zoom</strong> вы увидите довольно большое количество настроек. Данный модуль позволяет вам выбрать два типа отображения картинки на странице продукта: дефолтный и улучшенный (Unite gallery). Вы так же можете полностью отключить данный модуль.
</p>
<p>
	Дефолтный тип отображения никак не настраиваеться. Его можно просто включить или выключить. <br>
	Второй же тип отображения, он же <strong>Unite gallery</strong>, имеет очень много разнообразных настроек. 
</p>
