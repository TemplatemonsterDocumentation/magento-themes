<h3>Instagram module</h3>
<p>Instagram модуль позволяет отобразить Instagram галерею в вашем магазине.<br>Данный модуль позволяет отображать два типа галереи: галерея пользователя и галерея по тегу.</p>						
<p>Все опции управления доступны в админ панели, в секции <strong>System > Configuration > Templatemonster > Instagram</strong>.</p>
<figure class="img-polaroid"><img src="img/magento/instagram.jpg" alt="" /></figure>			
<p>Рассмотрим опции модуля:</p>			
<table  class="options-table">
	<tr>
		<td class="col-1"><strong>Client ID</strong></td>
		<td>-</td>
		<td>идентификатор Instagram приложения. Чтобы получить данный идентификатор, вы должны зарегистрировать ваше приложение по следующему <a href="http://instagram.com/developer/" target="_blank">адресу</a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Username</strong></td>
		<td>-</td>
		<td>имя пользователя в Instagram, чью галерею вы хотите отобразить.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Sample tag</strong></td>
		<td>-</td>
		<td>теги, по которым будут выводиться картинки.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Product tag</strong></td>
		<td>-</td>
		<td>используется для поиска похожих картинок, где начало тега состоит из набора выбранных параметров. К примеру, <strong>#storetitleproductname</strong>, <strong>#productsku</strong>, <strong>#storetitleproductsku</strong> и т.д.</td>
	</tr>				
</table>			
		