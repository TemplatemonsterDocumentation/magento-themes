<h3>Newsletter Pop-up</h3>
<p class="indent">Имейл рассылка всегда была важной частью маркетинга. Этот метод используется по всему миру и поможет вам получить больше заказов и подписчиков. Именно по этому мы презентуем вам модуль Newsletter popup. Модуль позволяет вам вывести попап с формой подписки и иконками соц. сетей, как только клиент зашёл на ваш сайт.</p>

<h4>Настройка</h4>
Для настройки модуля перейдите в секцию <strong>Stores -> Settings -> Configuration</strong>.
<figure class="img-polaroid"><img src="img/magento/new-0.png" alt="" /></figure>
В меню слева выберите пункт <strong>TEMPLATEMONSTER -> Newsletter Pop-up</strong> для перехода в интерфейс настроек модуля.<br>
Модуль содержит 2 секции, каждая из которых отвечает за определённый набор настроек:
<ul class="marked-list">
	<li>
		<strong>General</strong>
	</li>
	<li>
		<strong>Social Settings</strong>
	</li>
</ul>
 <figure class="img-polaroid"><img src="img/magento/new-2.jpg" alt="" /></figure>

<p>Секция <strong>General</strong> содержит основные настройки модуля, а секция <strong>Social settings</strong> позволяет настроить отображение социальных иконок в диалоге.</p>

<h4>Секция General</h4>
Как мы уже писали, данная секция содержит основные настройки модуля. Вы можете включить или отключить модуль, указать цвета кнопок, указать ширину окна и т.п.. Рассмотри каждую из опций более детально.
<figure class="img-polaroid"><img src="img/magento/new-4.jpg" alt="" /></figure>
<ol class="index-list">
	<li>
		Для включения или выключения модуля полностью вам понадобиться опция <strong>Enabled</strong>. С её помощью вы можете полностью отключить модуль.
	</li>
	<li>		
		<p>С помощью опций <strong>Show on startup</strong>, <strong>Show on footer</strong> и <strong>Show in footer</strong> вы можете настроить вывод диалога с формой подписки.</p>
		<figure class="img-polaroid"><img src="img/magento/new-6.jpg" alt="" /></figure>
		<figure class="img-polaroid"><img src="img/magento/new-8.jpg" alt="" /></figure>
	</li>
	<li>
		Опция <strong>Show on startup</strong> выводит всплывающее окно при открытии сайта. Как только у вас загрузилась страничка магазина, у вас появиться окно с предложением подписаться.		
	</li>
	<li>
		<p>Всплывающее окно так же можно вывести при доскроливании к футеру. То есть, если вы закроете окно, которое появилось при загрузке сайта и проскролите вниз к футеру, то у вас появится такое же окно с предложением подписаться. Для вывода второго окна подписки вам необходимо включить опцию <strong>Show on footer</strong>.</p>
	</li>
	<li>		
		<p><strong>Show in footer</strong> позволяет вывести в футере ссылку при нажатии на которую у вас появится всплывающее окно с формой подписки.  <strong>Footer link text</strong> - с помощью этого поля вы можете изменить текст для ссылки, которая выводится с помощью опции Show in footer.</p>
		<figure class="img-polaroid"><img src="img/magento/new-9.jpg" alt="" /></figure>
	</li>
	<li>
		Используя <strong>Pop-Up width</strong> опцию, вы можете задать ширину окна подписки. Задавать размер можно только в пикселях.
		<figure class="img-polaroid"><img src="img/magento/new-7.jpg" alt="" /></figure>
	</li>
	<li>
		Всплывающее окно можно вывести не сразу, а через указанный промежуток времени. За использование данной возможности отвечает опция <strong>Pop-Up timeout</strong>. Вы можете задать отсрочку показа окна подписки и оно будет появляться не сразу, а лишь через указанное вами время, после того, как клиент зашёл к вам на сайт.
		<figure class="img-polaroid"><img src="img/magento/new-10.jpg" alt="" /></figure>
	</li>
	<li>
		Опции <strong>Button color</strong> и <strong>Button hover color</strong> дают вам возможность настроить цвет бекграунда для кнопки подписаться.
		<figure class="img-polaroid"><img src="img/magento/new-5.jpg" alt="" /></figure>
	</li>
	<li>
		Custom <strong>CSS-class</strong> может вам понадобиться в целях разработки. С помощью этой опции вы можете задать свой класс для всплывающего диалога и использовать его в целях стилизации или других действий.
		<figure class="img-polaroid"><img src="img/magento/new-11.jpg" alt="" /></figure>
	</li>
</ol>

<h4>Секция Social Settings</h4>
<p class="indent">Данная секция отвечает за вывод и настройку социальных иконок в диалоге подписки. Она имеет несколько подсекции, которые по опциям схожи между собой.</p>
<figure class="img-polaroid"><img src="img/magento/new-12.jpg" alt="" /></figure>
<p>В подсекции с основными настройками вы можете настроить цвета и вывод иконок социальных сетей для таких сервисов:</p>
<ul class="marked-list">
	<li>Facebook</li>
	<li>Twitter</li>
	<li>LinkedIn</li>
	<li>Google+</li>
	<li>Youtube</li>
	<li>Vimeo</li>
	<li>Pinterest</li>
	<li>Instagram</li>
	<li>Foursquare</li>
	<li>Tumblr</li>
	<li>RSS</li>
</ul>

<ol class="index-list">
	<li>
		<p>Включить или отключить отображение той или иной иконки вы можете с помощью соответствующей опции <strong>Enabled ……. icon</strong>. При включении данной опции снизу появятся соответствующие секции для настройки каждой отдельной иконки.</p>
		<figure class="img-polaroid"><img src="img/magento/new-15.jpg" alt="" /></figure>
	</li>
	<li>
		<p>Есть в данной секции и опция <strong>Use default icon colors</strong>. Она используется для изменения цвета иконок. </p>
		<figure class="img-polaroid"><img src="img/magento/new-16.jpg" alt="" /></figure>
		Изначально она включена и все иконки имеют свой корпоративный цвет. Но если вы хотите самостоятельно задать цвет иконкам, то вы должны отключить данную опцию.
	</li>
	<li>
		<p>В появившихся секциях для каждой соц. сети вы увидите опцию <strong>Link</strong>. С её помощью вы можете указать ссылку на любую страницу (вашу страницу) соответствующей соц. сети.</p>
		<figure class="img-polaroid"><img src="img/magento/new-13.jpg" alt="" /></figure>
	</li>
	<li>
		<p>При отключении опции <strong>Use default icon colors</strong> в подсекциях с настройкой каждой иконки появятся новые поля:</p>
		<ul class="marked-list">
			<li>Icon background</li>
			<li>Icon color</li>
			<li>Icon background hover</li>
			<li>Icon hover color</li>
		</ul>
		<figure class="img-polaroid"><img src="img/magento/new-14.jpg" alt="" /></figure>
	</li>
</ol>
<p>Все эти опции направлены на редактирование внешнего вида иконок. С их помощью вы можете задать цвет фона иконки, цвет самой иконки и цвета для иконки и фона, которые будут отображаться при наведении на иконку. <br>
Не забудьте сохранить ваши настройки! </p>

<p>Если вдруг вы хотите вернуть все значения по умолчанию, рядом с кнопкой сохранения изменений есть кнопка <strong>Reset</strong>.</p>
