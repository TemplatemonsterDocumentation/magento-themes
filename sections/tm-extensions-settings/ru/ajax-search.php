<h3>Настройка Аякс поиска</h3>
<p>Для настройки аякс поиска вам необходимо перейти на вкладку <strong>System -> Configuration</strong>.<br> В секции <strong>Live search</strong> вы можете включить или отключить поиск.</p>
<a class="pretty_image" href="img/magento/ajax-search.jpg"><figure class="img-polaroid"><img src="img/magento/ajax-search.jpg" alt="" /></figure></a><br>
Рассмотрим опции модуля:
<table class="options-table">
	<tr>
		<td class="col-1"><strong>Image width</strong></td>
		<td>-</td>
		<td>ширина картинки продукта в поле выдачи результатов поиска</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Image height</strong></td>
		<td>-</td>
		<td>высота картинки продукта в поле выдачи результатов поиска</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Description</strong></td>
		<td>-</td>
		<td>включить/отключить вывод описания к продукту в поле выдачи результатов поиска</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Placeholder Text</strong></td>
		<td>-</td>
		<td>дефолтный текст (плейсхолдер) в поле поиска</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Number Product Show</strong></td>
		<td>-</td>
		<td>количество отображаемых продуктов, которые будут отображаться в окне выдачи результатов поиска</td>
	</tr>
</table>