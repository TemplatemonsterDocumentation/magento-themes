<h3>Product list gallery</h3>
<p class="indent">Модуль позволяет выводить миниатюры изображений продукта рядом с главным изображением продукта. В стандартной теме миниатюры выводятся под главным изображением продукта. Но в наших темах вывод данных миниатюр реализован согласно PSD макету.</p>
<p>Включить/отключить данный модуль можно в админ панели Magento, в секции <strong>System > Configuration > Templatemonster > Product List Gallery</strong>.</p>
<figure class="img-polaroid"><img src="img/magento/productgallery.jpg" alt="" /></figure>
<p>Модуль содержит 4 секции настроек:<strong>List View</strong>, <strong>Grid View</strong>, <strong>Homepage List View</strong>, <strong>Homepage Grid View</strong>. <br>
В секциях <strong>Homepage List View</strong> и <strong>Homepage Grid View</strong> настраивается отображение галереи на главной странице магазина. Другие две секции настраивают отображение галереи на страницах категорий.</p>
<p>Все секции содержат одинаковые настройки:</p>			
<table class="options-table">
	<tr>
		<td class="col-1"><strong>Enable/Disable</strong></td>
		<td>-</td>
		<td>включить/отключить модуль.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Image width</strong></td>
		<td>-</td>
		<td>ширина главного изображения продукта. </td>
	</tr>
	<tr>
		<td class="col-1"><strong>Image height</strong></td>
		<td>-</td>
		<td>высота главного изображения продукта. </td>
	</tr>
	<tr>
		<td class="col-1"><strong>Thumbnail width</strong></td>
		<td>-</td>
		<td>ширина миниатюры.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Thumbnail height</strong></td>
		<td>-</td>
		<td>высота миниатюры.</td>
	</tr>
</table>
<p>Обратите внимание! Максимальная ширина картинки будет равна ширине колонки, в которой она находится. </p>		