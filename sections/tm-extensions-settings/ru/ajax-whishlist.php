<h3>Ajax Wishlist</h3>
<p>Список желаний является одной из стандартных возможностей e-commerce. Для удобства пользования данной возможностью мы создали модуль, который работает посредством аякса, и позволяет вам добавлять продукты в список желаний без перехода на другую страницу.</p>
<p>Для настройки модуля вам необходимо зайти в дамин панель Мадженты и в левом меню выбрать: <strong>Stores > Configuration</strong>.</p><figure class="img-polaroid"><img src="img/magento/compare.jpg" alt="" /></figure>
<p>На новой странице в левом меню выбирите пункт <strong> Templatemonster > Ajax Wishlist </strong></p>
 <figure class="img-polaroid"><img src="img/magento/wishlist.jpg" alt="" /></figure>


<p>Рассмотрим опции модуля:</p>			
<table class="options-table">
	<tr>
		<td class="col-1"><strong>Enable</strong></td>
		<td>-</td>
		<td>включить или выключить модуль.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Show spinner</strong></td>
		<td>-</td>
		<td>позволяет включить отображение спиннера при процессе добавления товара в список желаний.</td>
	</tr>					
	<tr>
		<td class="col-1"><strong>Show Success Message </strong></td>
		<td>-</td>
		<td>данная опция включает отображение попап окна после добавления продукта в список желаний.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Success Message Text</strong></td>
		<td>-</td>
		<td>в данном поле вы можете указать текст, который будет отображаться в попапе после добавления товара в список желаний.</td>
	</tr>		
</table>