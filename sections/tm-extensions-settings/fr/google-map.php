<h3>Carte Google</h3>
<p>Le module donné vous permet d'intégrer la carte Google à votre site Magento. Par défaut, le module vous propose deux cartes: la carte à la page de contact et la carte dans le pied d'autres pages. <br>							
Les deux cartes peuvent être configurées via le panneau d'administration de Magento dans la section <strong>System > Configuration > Templatemonster > Google Map</strong>.</p>

<ol class="index-list">
	<li>
		Le bloc de <strong>Paramètres de la Carte Intégrée</strong> signifie l'affichage de la carte dans le pied de toutes les pages sauf la page de contact.
	</li>
	<li>
		Le bloc de <strong>Paramètres de la Carte de Contacts</strong> signifie l'affichage de la carte directement sur la page de contact.<figure class="img-polaroid"><img src="img/magento/googlemap-1.jpg" alt="" /></figure>
	</li>
	<li>
		Ces deux sections ont les mêmes options pour configurer l'apparence de la carte.<figure class="img-polaroid"><img src="img/magento/googlemap-2.jpg" alt="" /></figure>
	</li>
</ol>
<p>Le module dispose des options suivantes:</p>

<table class="options-table">
	<tr>
		<td class="col-1"><strong>API key</strong></td>
		<td>-</td>
		<td>Google Api Key est nécessaire pour le travail correct de la carte. Vous pouvez le recevoir et l'activer en suivant <a href="https://code.google.com/apis/console/" target="_blank">le lien</a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Coordinates</strong></td>
		<td>-</td>
		<td>les données de localisation qui seront affichées dans le centre de la carte affichée. Vous pouvez obtenir ces données de localisation à l'aide du site <a href="http://maps.google.com/" target="_blank">Google Maps</a> ou en utilisant <a href="http://www.mapcoordinates.net/" target="_blank" rel="nofollow">celui-ci</a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Sensor</strong></td>
		<td>-</td>
		<td>la mise en marche et l'arrêt de la géolocalisation.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Zoom</strong></td>
		<td>-</td>
		<td>la valeur de l'élargissement (ne doit pas dépasser 8).</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Map Type</strong></td>
		<td>-</td>
		<td>le type de la carte affichée (feuille de route, satellite, hybride, etc.).</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Map width</strong></td>
		<td>-</td>
		<td>la largeur de la carte.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Map height</strong></td>
		<td>-</td>
		<td>la hauteur de la carte.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Styles</strong></td>
		<td>-</td>
		<td>le format d'affichage de la carte. Pour modifier l'apparence de la carte, utilisez les styles offerts sur le site Web de <a href="https://snazzymaps.com/" target="_blank" rel="nofollow">snazzymaps</a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Disable UI</strong></td>
		<td>-</td>
		<td>cache les éléments qui contrôlent la carte.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Scrollwheel</strong></td>
		<td>-</td>
		<td>désactive l'option de défilement de la souris.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Draggable</strong></td>
		<td>-</td>
		<td>vous permet de déplacer la carte en utilisant le bouton gauche de votre souris ou en utilisant les doigts pour toucher des dispositifs (de détection).</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Add marker</strong></td>
		<td>-</td>
		<td>vous permet d'ajouter le marqueur à la carte.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Marker coordinates</strong></td>
		<td>-</td>
		<td>définit les coordonnées de marqueur.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Marker title</strong></td>
		<td>-</td>
		<td>le texte qui sera affiché dans l'infobulle lorsque vous déplacez la souris au-dessus.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Marker image</strong></td>
		<td>-</td>
		<td>le chemin d'accès à l'image de marqueur, si vous voulez remplacer celle par défaut. L'image est enregistrée dans le dossier <strong>skin/frontend/default/themeXXX/images/</strong> de votre installation de Magento.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Infowindow</strong></td>
		<td>-</td>
		<td>l'information supplémentaire qui sera affiché si vous cliquez sur le marqueur.</td>
	</tr>
</table>
