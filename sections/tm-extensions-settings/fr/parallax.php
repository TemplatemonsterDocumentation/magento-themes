 <h2 class="item1">Parallax and Video Background</h2>
<p align="justify" class="indent">This simple and functional module allows you to add a background image or video to some blocks on your site with multi-layered parallax effect. By means of this module you can add video layers, text layers, layers with images. Parallax can be configured according to your taste — you can alter layer motion direction, its speed and position. </p>
<h2 class="item1">Extension settings</h2>
<p>To activate the module, navigate to <strong>Stores > Configuration</strong>.</p>
<figure class="img-polaroid"><img src="img/magento/stores.png" alt="" /></figure>  
<p align="justify">Then choose the <strong>TEMPLATEMONSTER > Parallax</strong> in the left side menu and you’ll find an option to enable the module. </p>

<figure class="img-polaroid"><img src="img/magento/activate.png" alt="parallax" /></figure>

<p>To add and configure parallax block, navigate to <strong>TEMPLATE MONSTER > Parallax Block</strong>.</p>
<figure class="img-polaroid"><img src="img/magento/parallax-3.jpg" alt="parallax" /></figure>
<p>You'll see the <strong> "Add new item"</strong>   button (1) with the help of which you can add a new parallax block and a list of  existing blocks (2).</p>
<figure class="img-polaroid"><img src="img/magento/parallax-4.jpg" alt="parallax" /></figure>
<p>While creating or editing a parallax block, two tabs become available. They are <strong>General Settings</strong> & <strong>Parallax Block Item</strong>.</p>
