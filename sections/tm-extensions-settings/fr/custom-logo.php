<h3>Custom Logo</h3>
<p class="indent">This module allows changing your logo.<br>
You can enable/disable the module via Magento Admin Panel, in <strong>System > Configuration > Templatemonster > Custom Logo</strong> section.</p>
<figure class="img-polaroid"><img src="img/magento/custom-logo.png" alt="" /></figure>
<p>The module has the following settings:</p>
<table class="options-table">
	<tr>
		<td class="col-1"><strong>Logo status</strong></td>
		<td>-</td>
		<td>enable/disable the module;</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Logo Type</strong></td>
		<td>-</td>
		<td>you can change the logo type: text or image;</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Upload your logo</strong></td>
		<td>-</td>
		<td>choose a file to upload as your logo image;</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Alternate text for logo</strong></td>
		<td>-</td>
		<td>you can input the alternate text for the logo image;</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Logo Text</strong></td>
		<td>-</td>
		<td>input the main logo text;</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Logo Slogan</strong></td>
		<td>-</td>
		<td>input a slogan for the logo;</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Upload your logo icon</strong></td>
		<td>-</td>
		<td>choose an image for the logo icon (jpg, gif, png);</td>
	</tr>
</table>