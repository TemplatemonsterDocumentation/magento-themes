<h3>Module Instagram</h3>
<p>Le module Instagram vous permet d'afficher la galerie d'Instagram sur votre boutique.<br>
Le module donné vous permet d'afficher deux types de galeries: la galerie d'utilisateur et la galerie de balises.</p>						
<p>Toutes les options de configuration sont disponibles via le panneau d'administration de Magento dans la section <strong>System > Configuration > Templatemonster > Instagram</strong>.</p>
<figure class="img-polaroid"><img src="img/magento/instagram.jpg" alt="" /></figure>			
<p>Le module dispose des options suivantes:</p>			
<table  class="options-table">
	<tr>
		<td class="col-1"><strong>Client ID</strong></td>
		<td>-</td>
		<td>l'identificateur d'APP Instagram. Afin de recevoir de l'identificateur,  vous devez inscrire votre application en utilisant le lien suivant<a href="http://instagram.com/developer/" target="_blank"></a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Username</strong></td>
		<td>-</td>
		<td>le nom d'utilisateur d'Instagram dont la galerie que vous souhaitez afficher.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Sample tag</strong></td>
		<td>-</td>
		<td>les balises utilisées utilisées pour afficher des images.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Product tag</strong></td>
		<td>-</td>
		<td>est utilisé pour trouver des images similaires, où le début de la balise comprend les paramètres choisis fixés. Par exemple: <strong>#storetitleproductname</strong>, <strong>#productsku</strong>, <strong>#storetitleproductsku</strong>, etc.</td>
	</tr>				
</table>			
		