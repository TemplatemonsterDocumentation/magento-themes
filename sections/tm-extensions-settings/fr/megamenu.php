<h3>MegaMenu</h3>
<p class="indent">Ce module remplace le menu standard et vous permet de configurer les sous-catégories afficher jusqu'au 2ème niveau d'imbrication, d'ajouter des blocs statiques dans le menu déroulant, d'attribuer des étiquettes, d'afficher des produits etc.</p>
<strong>MegaMenu</strong> est inclus dans les modèles par défaut, mais vous pouvez l'activer ou désactiver dans <strong>Boutiques > Configuration > TemplateMonster > Mega Menu (Stores > Configuration > TemplateMonster > Mega Menu)</strong>.
<figure class="img-polaroid"><img src="img/magento/megamenu-1.jpg" alt="" /></figure>

<ol class="index-list">
	<li>Vous pouvez configurer l'apparence de chaque menu déroulant sur la page de configuration de la catégorie qui peut être trouvé dans <strong>Produits > Catégories -> Megamenu (Products > Categories -> Megamenu)</strong>.
		<a class="pretty_image" href="img/magento/Megamenu.png"><figure class="img-polaroid"><img src="img/magento/Megamenu.png" alt="" /></figure></a>
			
	</li>
	<li>
		Pour ajouter le bloc statique dans le menu déroulant, vous devez d'abord le créer dans <strong>CMS > Blocs statiques (CMS > Static Blocks)</strong>, il sera disponible pour l'affichage dans le menu déroulant. 
		<figure class="img-polaroid"><img src="img/magento/megamenu-static.jpg" alt="" /></figure>
	</li>
	<li>
		Ce module permet d'ajouter une étiquette à la catégorie de produits. Vous pouvez le faire sur la page de menu de catégories sous <strong>Produits > Catégories -> Megamenu (Products > Categories -> Megamenu)</strong>. Il y a une option 'classe personnalisée', de sorte que vous pouvez créer des styles pour cette étiquette en utilisant des règles css.
		<figure class="img-polaroid"><img src="img/magento/megamenu-badge.jpg" alt="" /></figure>
	</li>		
	<li>
		Vous pouvez définir le nombre de colonnes dans le menu mega. Pour le faire, vous devez activer l'affichage des colonnes et spécifier le nombre de colonnes. 
		<figure class="img-polaroid"><img src="img/magento/megamenu-column.jpg" alt="" /></figure>
	</li>		
	<li>
		Pour afficher les produits, activez une option <strong>Activer l'affichage de produits (Turn on products show)</strong>, et choisissez quelques articles dans la liste. 
		<figure class="img-polaroid"><img src="img/magento/megamenu-product.jpg" alt="" /></figure>
	</li>		
	<li>
		Image de sous-catégorie peut être ajouté dans cette section de sous-catégorie <strong>MegaMenu (MegaMenu)</strong>. 
		<figure class="img-polaroid"><img src="img/magento/megamenu-image.jpg" alt="" /></figure>
		<p>Pour faire apparaître l'image, vous devez également activer son affichage en choisissant <strong>Activer les images pour les sous-catégories (Turn on images for subcategories)</strong>, dans la section <strong>MegaMenu (MegaMenu)</strong> de la catégorie parent.</p>
		<figure class="img-polaroid"><img src="img/magento/megamenu-image-1.jpg" alt="" /></figure>
	</li>
	<li>
		Mises en page de menu Mega peuvent être éditées en utilisant une option <strong>Modèle personnalisé pour élément de menu mega (Custom template for megamenu item)</strong>. Les fichiers sources de ces modèles se trouvent dans le répertoire suivant: <strong>app\design\frontend\TemplateMonster\themeXXX\TemplateMonster_Megamenu\templates\html\topmenu\</strong>.
		<figure class="img-polaroid"><img src="img/magento/megamenu-template.jpg" alt="" /></figure>
	</li>
</ol>