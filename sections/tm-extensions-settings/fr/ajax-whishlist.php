<h3>Ajax Wishlist</h3>
<p>La liste de voeux est l'une des caractéristiques standards e-commerce. Nous avons créé le module spécial pour l'utilisation pratique de cette fonction, il est basé sur la technologie AJAX et vous permet d'ajouter des produits à la liste sans avoir à naviguer à la page séparée.</p>
<p>Pour configurer ce module, vous devez aller dans le panneau d'administration Magento et choisissez <strong> Boutiques > Configuration (Stores > Configuration)</strong> dans le menu de gauche.</p>
<figure class="img-polaroid"><img src="img/magento/compare.jpg" alt="" /></figure>
<p>Ensuite choisissez <strong>TemplateMonster > Ajax Wishlist (TemplateMonster > Ajax Wishlist) </strong> sur la nouvelle page sur le côté gauche.</p>
 <figure class="img-polaroid"><img src="img/magento/wishlist.jpg" alt="" /></figure>


<p>Configuration du module:</p>			
<table class="options-table">
	<tr>
		<td class="col-1"><strong>Activer (Enable)</strong></td>
		<td>-</td>
		<td>cette option vous permet d'activer ou de désactiver le module.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Afficher spinner (Show spinner)</strong></td>
		<td>-</td>
		<td>cette option permet d'activer l'affichage de spinner lors de l'ajout de produit à votre liste de voeux.</td>
	</tr>					
	<tr>
		<td class="col-1"><strong>Afficher le message de Succès (Show Success Message)</strong></td>
		<td>-</td>
		<td>cette option permet d'activer l'affichage des messages pop-up après avoir ajouté un article à votre liste de voeux.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Texte de message de Succès (Success Message Text)</strong></td>
		<td>-</td>
		<td>ici vous pouvez définir le texte de message à apparaître dans la fenêtre pop-up après avoir ajouté un article à votre liste de voeux.</td>
	</tr>		
</table>