<h3>Ajax Catalog</h3>
<p>Sélection rapide et facile parmi la variété de produits contribue à prendre une décision positive sur les achats dans votre boutique en ligne. Le module Ajax Catalog qui offre cette possibilité  est le module le plus populaire parmi les boutiques en ligne basées sur différentes plates-formes. Il vous permet de trouver et de filtrer les produits dont vous avez besoin en fonction des paramètres sélectionnés rapidement et facilement sans recharger la page.</p>						
<p>Pour configurer ce module, accédez à <strong>(Magasins -> Paramètres -> Configuration) (Stores -> Settings -> Configuration)</strong>.</p>
<figure class="img-polaroid"><img src="img/magento/compare.jpg" alt="" /></figure>			
<p>Ensuite, accédez à <strong>TemplateMonster -> Catalog Ajax (TemplateMonster -> Catalog Ajax)</strong> dans le menu de gauche pour accéder à l'interface de configuration du module.</p>
<figure class="img-polaroid"><img src="img/magento/ajaxcatalog.jpg" alt="" /></figure>		
<p>Les options de module:</p>			
<table  class="options-table">
	<tr>
		<td class="col-1"><strong>Pagination (Pagination) </strong></td>
		<td>-</td>
		<td>liste des pagination. Si votre liste comprend plus d'une page avec des produits, cette option permet de basculer entre les pages intérieures sans recharger la page principale.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Nombre de produits (Show number items)</strong></td>
		<td>-</td>
		<td>nombre de produits affichés sur la page. Cette option vous permet de modifier le nombre de produits affichés sur la page sans rechargement.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Trier par (desc/asc) (Sort by (desc/asc))</strong></td>
		<td>-</td>
		<td>ordre de produits. Vous pouvez trier les éléments sans recharger la page principale.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Ordre de liste (prix, position, nom) (List order (price, position, name))</strong></td>
		<td>-</td>
		<td>paramètres de tri. Il vous permet de définir le paramètre de tri sans recharger la page.</td>
	</tr>		
	<tr>
		<td class="col-1"><strong>Mode d'affichage (grille / liste) (View mode (grid/list))</strong></td>
		<td>-</td>
		<td> affichage de la mise en page de produits. Cette option vous permet de spécifier des mises en page de produits sans les recharger.</td>
	</tr>		
	<tr>
		<td class="col-1"><strong>Layer filter (Navigation à facettes)</strong></td>
		<td>-</td>
		<td>Filtrer par attributs. C'est une option principale de module. Il active filtre AJAX par des attributs et des paramètres situés dans la colonne des filtres standard.</td>
	</tr>		
</table>			
<p>Vous pouvez utiliser toutes les options ou quelques-uns d'entre eux selon vos besoins.</p>	