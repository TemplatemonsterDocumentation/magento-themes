<h3>Ajax Catalog</h3>
<p>Çevrimiçi maðazanýzdaki çeþitli ürünler arasýndan hýzlý ve kolay bir þekilde olumlu karar verilerek seçim yapýlabilmesine katkýda bulunur. Ajax Catalog modülü, bu özelliðiyle çeþitli platformlardaki çevrimiçi maðazalarýn en popüler modülü haline gelmiþtir. Sayfayý tekrar yüklemede, istenilen özelliklerdeki ürünlerin bulunmasý ve filtrelenmesini kolayca ve hýzlýca gerçekleþtirir.</p>						
<p>Bu modülün ayarlarý için <strong>Maðazalar -> Ayarlar -> Düzenleme</strong> bölümüne giriniz.</p>
<figure class="img-polaroid"><img src="img/magento/compare.jpg" alt="" /></figure>			
<p>Arýndan modül ayarlarý için sol menüden <strong>TemplateMonster -> Catalog Ajax</strong> kýsmýna geçiniz.</p>
<figure class="img-polaroid"><img src="img/magento/ajaxcatalog.jpg" alt="" /></figure>		
<p>Modül seçeneklerine göz atalým:</p>			
<table  class="options-table">
	<tr>
		<td class="col-1"><strong>Pagination </strong></td>
		<td>-</td>
		<td>liste sayfalamasý. Eðer listeniz birden fazla ürün sayfasý içeriyorsa, bu seçenekle anasayfayý yenilemeden sayfalar arasýnda geçiþ yapabilabilir.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Show number items</strong></td>
		<td>-</td>
		<td>sayfadaki ürün sayýsýnýn gösterilmesi. Bu seçenekle; sayfa yenilenmeden, tekli listede gösterilecek olan ürün sayýsýný deðiþtirebilirsiniz.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Sort by (desc/asc)</strong></td>
		<td>-</td>
		<td>ürün sýralamasý. Anasayfa yenilenmeden, ürünlerin sýralamasýný eskiden yeniye veya yeniden eskiye doðru düzenleyebilirsiniz.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>List order</strong></td>
		<td>-</td>
		<td>özellikleri düzenlemek. Sayfayý yenilemeden, fiyata göre, duruma göre, isme göre sýralama yapabilirsiniz.</td>
	</tr>		
	<tr>
		<td class="col-1"><strong>View mode (grid/list)</strong></td>
		<td>-</td>
		<td>ürün sayfasýnýn tasarýmý. Sayfayý yenilemeden, ürünlerin ne þekilde sýralanacaðýný düzenleyebilirsiniz. Liste veya katman þeklinde olabilir.</td>
	</tr>		
	<tr>
		<td class="col-1"><strong>Layer filter</strong></td>
		<td>-</td>
		<td>özelliðe göre filtrelemek. Bu ana modül seçeneðidir. Standart maðaza sütun filtresindeki AJAX filtresini özelliðe ve parametreye göre ayarlar.</td>
	</tr>		
</table>			
<p>Tüm seçenekleri ayný anda kullanabilirsiniz veya sadece istediklerinizi kullanabilirsiniz.</p>	