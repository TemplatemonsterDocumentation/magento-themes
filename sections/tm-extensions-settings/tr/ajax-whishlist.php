<h3>Ajax Wishlist</h3>
<p>Ürün İsteği standart e-mağaza özelliklerinden biridir. Bu özellik için pratik bir modül geliştirdik. AJAX teknolojisine dayalı ve ayrı bir sayfaya gitmeden istek listenize ürün eklemenizi sağlıyor.</p>
<p>Bu modülün ayarları için için Magento yönetici panelindeki sol menüden <strong>Mağazalar > Ayarlar</strong> kısmına giriniz.</p>
<figure class="img-polaroid"><img src="img/magento/compare.jpg" alt="" /></figure>
<p>Daha sonra sol taraftaki <strong> TemplateMonster > Ajax Wishlist </strong> bölümünü seçiniz.</p>
 <figure class="img-polaroid"><img src="img/magento/wishlist.jpg" alt="" /></figure>


<p>Modül ayarlarına göz atalım:</p>			
<table class="options-table">
	<tr>
		<td class="col-1"><strong>Enable</strong></td>
		<td>-</td>
		<td>bu ayarla modülü açabilir veya kapatabilirsiniz.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Show spinner</strong></td>
		<td>-</td>
		<td>bu seçenek sayesinde, istek listesine eklerken ürün dönerek eklenir.</td>
	</tr>					
	<tr>
		<td class="col-1"><strong>Show Success Message</strong></td>
		<td>-</td>
		<td>bu seçenek sayesinde, istek listesine ürün eklendiğinde bir pencere açılır ve işlemin başarılı olduğu mesajı gösterilir.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Success Message Text</strong></td>
		<td>-</td>
		<td>istek listesine ürün eklendiğinde açılacak olan mesaj kutusunda nasıl bir mesaj yazacağını bu kısımdan ayarlayabilirsiniz.</td>
	</tr>		
</table>