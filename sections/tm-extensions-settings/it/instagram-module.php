<h3>Modulo Instagram</h3>
<p>Il modulo Instagram ti permette di inserire una galleria Instagram nel tuo negozio.<br>
Il modulo ti permette di inserire due tipi di galleria: galleria utente e galleria tag.</p>						
<p>Tutte le configurazioni sono disponibili nel pannello di amministrazione (Admin Panel), nella sezione <strong>System > Configuration > Templatemonster > Instagram</strong>.</p>
<figure class="img-polaroid"><img src="img/magento/instagram.jpg" alt="" /></figure>			
<p>Il modulo ha le seguenti opzioni:</p>			
<table  class="options-table">
	<tr>
		<td class="col-1"><strong>Client ID</strong></td>
		<td>-</td>
		<td>Identificativo dell'app Instagram. Per ottenere l'identificativo è necessario registrare la propria app seguendo questo link <a href="http://instagram.com/developer/" target="_blank"></a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Username</strong></td>
		<td>-</td>
		<td>Nome utente Instagram di cui vuoi visualizzare la galleria.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Sample tag</strong></td>
		<td>-</td>
		<td>tag utilizzati per visualizzare le immagini.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Product tag</strong></td>
		<td>-</td>
		<td>è utilizzato per trovare immagini simili, dove l'inizio del tag indica i parametri selezionati, ad es. E. g.: <strong>#storetitleproductname</strong>, <strong>#productsku</strong>, <strong>#storetitleproductsku</strong>, ecc.</td>
	</tr>				
</table>			
		