<h3>MegaMenu</h3>
<p class="indent">Questo modulo sostituisce menu standard con quella avanzata, che consente di configurare sottocategorie visualizzare fino al livello di nidificazione 2°, aggiungere blocchi statici per il menu a discesa, assegnare etichette, prodotti di visualizzazione, etc.</p>
<strong>MegaMenu</strong> è incluso per i modelli di default, ma è possibile abilitare o disabilitare nel <strong>Negozi > Configurazione > TemplateMonster > Mega Menu (Stores > Configuration > TemplateMonster > Mega Menu)</strong> sezione.
<figure class="img-polaroid"><img src="img/magento/megamenu-1.jpg" alt="" /></figure>

<ol class="index-list">
	<li>È possibile configurare l'aspetto di ogni menu a tendina sulla pagina di configurazione categoria che può essere trovato nel <strong>Prodotti > Categorie -> Megamenu (Products > Categories -> Megamenu)</strong> sezione.
		<a class="pretty_image" href="img/magento/Megamenu.png"><figure class="img-polaroid"><img src="img/magento/Megamenu.png" alt="" /></figure></a>
			
	</li>
	<li>
		Per aggiungere blocco statico al menu a discesa, in primo luogo è necessario crearlo nel <strong>CMS > Blocchi Statici (CMS > Static Blocks)</strong> sezione e quindi sarà disponibile per la visualizzazione nel menu a discesa. 
		<figure class="img-polaroid"><img src="img/magento/megamenu-static.jpg" alt="" /></figure>
	</li>
	<li>
		Questo modulo permette di aggiungere un'etichetta a qualsiasi categoria di prodotti. È possibile farlo sulla pagina del menu categorie sotto <strong>Prodotti > Categorie -> Megamenu (Products > Categories -> Megamenu)</strong>. C'è una opzione 'classe personalizzata' disponibili, in modo che è possibile creare dei stili per questa etichetta utilizzando le regole CSS.
		<figure class="img-polaroid"><img src="img/magento/megamenu-badge.jpg" alt="" /></figure>
	</li>		
	<li>
		È possibile definire il numero di colonne nel menu mega. Per fare questo si dovrebbe abilitare la visualizzazione colonne e di specificare il numero di colonne necessarie. 
		<figure class="img-polaroid"><img src="img/magento/megamenu-column.jpg" alt="" /></figure>
	</li>		
	<li>
		Per visualizzare i prodotti, attivare la <strong>Accendere prodotti spettacolo (Turn on products show)</strong> opzione e scegliere alcuni elementi nella lista. 
		<figure class="img-polaroid"><img src="img/magento/megamenu-product.jpg" alt="" /></figure>
	</li>		
	<li>
		Sotto immagine può essere aggiunto in questa sottocategoria <strong>MegaMenu</strong> sezione. 
		<figure class="img-polaroid"><img src="img/magento/megamenu-image.jpg" alt="" /></figure>
		<p>Tuttavia, per far apparire l'immagine, è anche necessario attivare il suo display scegliendo <strong>Accendere le immagini per sottocategorie (Turn on images for subcategories)</strong>, nella categoria superiore <strong>MegaMenu</strong> sezione.</p>
		<figure class="img-polaroid"><img src="img/magento/megamenu-image-1.jpg" alt="" /></figure>
	</li>
	<li>
		Layout dei Mega Menu possono essere modificati utilizzando <strong>Modello personalizzato per voce di megamenu (Custom template for megamenu item)</strong> opzione. I file di origine di questi modelli si trovano nella seguente direzione: <strong>app\design\frontend\TemplateMonster\themeXXX\TemplateMonster_Megamenu\templates\html\topmenu\</strong>.
		<figure class="img-polaroid"><img src="img/magento/megamenu-template.jpg" alt="" /></figure>
	</li>
</ol>