<h3>Catalogo Ajax</h3>
<p>Scelta rapida e semplice tra le varietà dei prodotti contribuisce e fornisce una decisione positiva di acquisto nel vostro negozio on-line. Modulo Catalogo Ajax che offre questa possibilità è il modulo più popolare tra i negozi online basati su diverse piattaforme. Esso consente di trovare e filtrare i prodotti necessari in base ai parametri selezionati rapidamente e facilmente senza ricaricare la pagina.</p>

<p>Per configurare questo modulo, navigare su <strong>Negozi -> Impostazioni -> Configurazione (Stores -> Settings -> Configuration)</strong> sezione.</p>
<figure class="img-polaroid"><img src="img/magento/compare.jpg" alt="" /></figure>			
<p>Poi navigare <strong>TemplateMonster -> Catalogo Ajax (TemplateMonster -> Catalog Ajax)</strong> nel menu a sinistra per andare alle impostazioni del modulo interfaccia. Poi navigare su</p>
<figure class="img-polaroid"><img src="img/magento/ajaxcatalog.jpg" alt="" /></figure>		
<p>Rivediamo opzioni del modulo:</p>			
<table  class="options-table">
	<tr>
		<td class="col-1"><strong>Paginatura </strong></td>
		<td>-</td>
		<td>messa in vendita di impaginazione. Se il vostro elenco comprende più di una pagina con i prodotti, questa opzione consente di passare tra le pagine interne senza ricaricare la pagina principale.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Visualizza numero articoli </strong></td>
		<td>-</td>
		<td>numero di prodotti visualizzati nella pagina. Questa opzione consente di modificare il numero dei prodotti esposti sulla pagina singola lista senza ricaricare.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>"Ordina per (desc/asc)" ("Sort by (desc/asc)")</strong></td>
		<td>-</td>
		<td>prodotti ordinazione. È possibile ordinare gli elementi senza ricaricare la pagina principale.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>"Ordina lista (pezzo, posizione, nome)" ("List order (price, position, name)")</strong></td>
		<td>-</td>
		<td>l'ordinamento parametri. Esso consente di definire il parametro di ordinamento senza ricaricare la pagina.</td>
	</tr>		
	<tr>
		<td class="col-1"><strong>"Modalità di visualizzazione (griglia/lista)" ("View mode (grid/list)")</strong></td>
		<td>-</td>
		<td>prodotti visualizzazione del layout. Questa opzione consente di specificare il layout prodotti della messa in pagine senza ricaricare tema, con il tipo griglia/lista.</td>
	</tr>		
	<tr>
		<td class="col-1"><strong> Filtro layer (Layer filter)</strong></td>
		<td>-</td>
		<td>filtrare per attributi. Questa è l'opzione principale del modulo. Esso consente filtro AJAX per attributi e parametri si trovano nella colonna filtri negozio standard.</td>
	</tr>		
</table>			
<p>Sentitevi liberi di utilizzare tutte le opzioni una per volta o solo alcuni di essi, tocca a voi la scelta.</p>	