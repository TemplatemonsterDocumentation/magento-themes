<h3>Ajax Wishlist</h3>
<p>Prodotti lista dei desideri è una delle caratteristiche standard di e-commerce. Abbiamo creato il modulo speciale per il comodo utilizzo di questa funzionalità, si basa sulla tecnologia AJAX e consente di aggiungere prodotti alla lista dei desideri, senza dover accedere alla pagina separata.</p>
<p>Per configurare questo modulo, si dovrebbe passare a Magento pannello di amministrazione e scegliere la <strong>Negozi > Configurazione (Stores > Configuration)</strong> sezione nel menu a sinistra.</p>
<figure class="img-polaroid"><img src="img/magento/compare.jpg" alt="" /></figure>
<p>Quindi scegliere il <strong>TemplateMonster > Ajax Lista dei Desideri (TemplateMonster > Ajax Wishlist) </strong> elemento sulla nuova pagina sul lato sinistro.</p>
 <figure class="img-polaroid"><img src="img/magento/wishlist.jpg" alt="" /></figure>


<p>Rivediamo la configurazione del modulo:</p>			
<table class="options-table">
	<tr>
		<td class="col-1"><strong>Abilita (Enable)</strong></td>
		<td>-</td>
		<td>questa opzione consente di abilitare o disabilitare il modulo.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Visualizzazione filatore (Show spinner)</strong></td>
		<td>-</td>
		<td>questa opzione permette di abilitare la visualizzazione filatore mentre l'aggiunta di un elemento per la vostra lista dei desideri.</td>
	</tr>					
	<tr>
		<td class="col-1"><strong>Visualizzazione Messaggio Successo (Show Success Message)</strong></td>
		<td>-</td>
		<td>questa opzione permette di abilitare la visualizzazione dei messaggi pop-up dopo l'aggiunta di un elemento alla lista dei desideri.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Successo Messaggio Testo (Success Message Text)</strong></td>
		<td>-</td>
		<td>Qui è possibile definire un messaggio di testo da visualizzare nella finestra pop-up dopo l'aggiunta di un articolo alla lista dei desideri.</td>
	</tr>		
</table>