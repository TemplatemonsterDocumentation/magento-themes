<h3>Product List Gallery</h3>
<p class="indent">Questo modulo consente di visualizzare immagini in anteprima dei prodotti, insieme all'immagine principale. Nel tema predefinito, le anteprime vengono disposte sotto l'immagine principale del prodotto, mentre con il nostro tema, le anteprime sono visualizzate secondo un layout PSD.</p>
<p>Il modulo può essere abilitato/disabilitato nel pannello admin di Magento, in <strong>System > Configuration > Templatemonster > Product List Gallery</strong>.</p>
<figure class="img-polaroid"><img src="img/magento/productgallery.jpg" alt="" /></figure>
<p>Il module include 4 sezioni di impostazioni: <strong>List View</strong>, <strong>Grid View</strong>, <strong>Homepage List View</strong>, e <strong>Homepage Grid View</strong>. <br>
In <strong>Homepage List View</strong> e <strong>Homepage Grid View</strong> è possibile configurare la visualizzazione della galleria sulla pagina iniziale del negozio. Altre due sezioni permettono di configurare la visualizzazione della galleria nelle pagine delle categorie.</p>
<p>Tutte le sezioni includono le stesse impostazioni:</p>			
<table class="options-table">
	<tr>
		<td class="col-1"><strong>Enable/Disable</strong></td>
		<td>-</td>
		<td>per abilitare/disabilitare il modulo.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Image width</strong></td>
		<td>-</td>
		<td>per impostare la larghezza dell'immagine principale del prodotto. </td>
	</tr>
	<tr>
		<td class="col-1"><strong>Image height</strong></td>
		<td>-</td>
		<td>per impostare l'altezza dell'immagine principale del prodotto. </td>
	</tr>
	<tr>
		<td class="col-1"><strong>Thumbnail width</strong></td>
		<td>-</td>
		<td>per impostare la larghezza dell'anteprima del prodotto.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Thumbnail height</strong></td>
		<td>-</td>
		<td>per impostare l'altezza dell'anteprima del prodotto.</td>
	</tr>
</table>
<p>Nota! La larghezza massima dell'immagine corrisponde alla larghezza della colonna nella quale l'immagine viene posizionata.</p>
		