<h3>Google Map</h3>
<p>Questo modulo di permette di incorporare le mappe di Google suo tuo sito Magento. Di default, il modulo ti dà due mappe: la mappa sulla pagina dei contatti e la mappa a pié pagina sulle altre pagine. <br>Entrambe le mappe possono essere configurate attraverso il pannello di amministrazione (Admin Panel) di Magento, andando alla sezione <strong>System > Configuration > Templatemonster > Google Map</strong>.</p>

<ol class="index-list">
	<li>
		Il blocco impostazioni <strong>Embed Map Settings</strong> è per la visualizzazione della mappa a pié pagina di tutte le pagine, tranne la pagina dei contatti.
	</li>
	<li>
		 Il blocco impostazioni <strong>Contacts Map Settings</strong> è per la visualizzazione della mappa direttamente sulla pagina dei contatti.
		 <figure class="img-polaroid"><img src="img/magento/googlemap.jpg" alt="" /></figure>
	</li>
	<li>
		Entrambe le sezioni contengono le stesse opzioni per configurare l'aspetto della mappa.<figure class="img-polaroid"><img src="img/magento/googlemap-1.jpg" alt="" /></figure>
	</li>
</ol>
<p>Il modulo ha le seguenti opzioni:</p>

<table class="options-table">
	<tr>
		<td class="col-1"><strong>API key</strong></td>
		<td>-</td>
		<td>l'Api Key di Google è necessario per il corretto funzionamento della mappa. Puoi ottenerlo e attivarlo cliccando su questo <a href="https://code.google.com/apis/console/" target="_blank">link</a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Coordinates</strong></td>
		<td>-</td>
		<td>i dati delle località da mostrare al centro della mappa visualizzata sul sito. Puoi ottenere questi dati della località usando il sito <a href="http://maps.google.com/" target="_blank">Google Maps</a> o <a href="http://www.mapcoordinates.net/" target="_blank" rel="nofollow">anche questo</a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Sensor</strong></td>
		<td>-</td>
		<td>attivare e disattivare la geolocalizzazione.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Zoom</strong></td>
		<td>-</td>
		<td>valore di ingrandimento (non deve superare il valore 8).</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Map Type</strong></td>
		<td>-</td>
		<td>tipo di mappa da visualizzare (mappa stradale, satellite, ibrida, ecc.).</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Map width</strong></td>
		<td>-</td>
		<td>larghezza della mappa.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Map height</strong></td>
		<td>-</td>
		<td>altezza della mappa.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Styles</strong></td>
		<td>-</td>
		<td>stile di visualizzazione della mappa. Per cambiare l'aspetto della mappa, puoi utilizzare gli stili che trovi sul sito <a href="https://snazzymaps.com/" target="_blank" rel="nofollow">snazzymaps</a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Disable UI</strong></td>
		<td>-</td>
		<td>nasconde gli elementi che controllano la mappa.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Scrollwheel</strong></td>
		<td>-</td>
		<td>disabilita la funzione della rotella del mouse.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Draggable</strong></td>
		<td>-</td>
		<td>abilita la possibilità di spostare la mappa utilizzando il pulsante sinistro del mouse o utilizzando le dita, su dispositivi con touch screen.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Add marker</strong></td>
		<td>-</td>
		<td>permette di aggiungere dei puntatori alla mappa.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Marker coordinates</strong></td>
		<td>-</td>
		<td>imposta le coordinate del puntatore.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Marker title</strong></td>
		<td>-</td>
		<td>testo da visualizzare quando si punta il mouse sopra il puntatore.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Marker image</strong></td>
		<td>-</td>
		<td>percorso verso l'immagine del puntatore, nel caso tu voglia sostituire l'immagine predefinita. L'immagine viene salvata nella cartella <strong>skin/frontend/default/themeXXX/images/</strong> della tua cartella di installazione Magento.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Infowindow</strong></td>
		<td>-</td>
		<td>informazioni aggiuntive da visualizzare quando si clicca sul puntatore.</td>
	</tr>
</table>
