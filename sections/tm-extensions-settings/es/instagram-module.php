<h3>M&oacute;dulo de Instagram</h3>
<p>El m&oacute;dulo de Instagram le permite mostrar la galer&iacute;a Instagram en su tienda.<br>
Este m&oacute;dulo le permite mostrar dos tipos de galer&iacute;as: galer&iacute;a de usuario y una galer&iacute;a de etiquetas.</p>						
<p>Todas las opciones de configuraci&oacute;n est&aacute;n disponibles en el Panel de Administraci&oacute;n en la secci&oacute;n <strong>System > Configuration > Templatemonster > Instagram</strong>.</p>
<figure class="img-polaroid"><img src="img/magento/instagram.jpg" alt="" /></figure>			
<p>El m&oacute;dulo tiene las siguientes opciones:</p>			
<table  class="options-table">
	<tr>
		<td class="col-1"><strong>Client ID</strong></td>
		<td>-</td>
		<td>Identificador de app  de Instagram. Para recibir el identificador debe registrar su aplicaci&oacute;n usando el siguiente <a href="http://instagram.com/developer/" target="_blank">enlace</a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Username</strong></td>
		<td>-</td>
		<td>Nombre de usuario de Instagram de la galer&iacute;a que se desea mostrar.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Sample tag</strong></td>
		<td>-</td>
		<td>etiquetas que se utilizan para mostrar im&aacute;genes.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Product tag</strong></td>
		<td>-</td>
		<td>se utiliza para encontrar im&aacute;genes similares, donde el principio de la etiqueta tiene los par&aacute;metros indicados. Por ejemplo: <strong>#storetitleproductname</strong>, <strong>#productsku</strong>, <strong>#storetitleproductsku</strong>, etc.</td>
	</tr>				
</table>			
		