<h3>Ajax Catalog</h3>
<p>Elección rápida y sencilla entre la variedad de los productos contribuye a la toma de una decisión positiva sobre la compra en su tienda online. El módulo Ajax Catalog que ofrece esta oportunidad es el módulo más popular entre las diferentes tiendas en línea basado en plataformas. Se le permite encontrar y filtrar los productos que necesita de acuerdo a los parámetros seleccionados de forma rápida y sencilla sin necesidad de recargar la página.</p>						
<p>Para configurar este módulo, vaya a la sección <strong>(Tiendas -> Configuración -> Configuración)(Stores -> Settings -> Configuration)</strong>.</p>
<figure class="img-polaroid"><img src="img/magento/compare.jpg" alt="" /></figure>			
<p>A continuación, vaya a <strong>(TemplateMonster -> Catalog Ajax)(TemplateMonster -> Catalog Ajax)</strong> en el menú de la izquierda para acceder a la interfaz de configuración del módulo.</p>
<figure class="img-polaroid"><img src="img/magento/ajaxcatalog.jpg" alt="" /></figure>		
<p>Las opciones del módulos:</p>			
<table  class="options-table">
	<tr>
		<td class="col-1"><strong>Paginación (Pagination) </strong></td>
		<td>-</td>
		<td>lista de paginación. Si su lista incluye más de una página con los productos, esta opción permite la conmutación entre las páginas internas sin tener que recargar la página principal.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>(Número de productos) (Show number items)</strong></td>
		<td>-</td>
		<td>número de productos que se muestran en la página. Esta opción le permite cambiar el número de productos que se muestran en la página sin recargar.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Ordenar por (desc/asc) (Sort by (desc/asc))</strong></td>
		<td>-</td>
		<td>orden de productos. Puede ordenar los elementos sin tener que recargar la página principal.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Orden de la lista (precio, posición, nombre) (List order (price, position, name))</strong></td>
		<td>-</td>
		<td>parámetros de clasificación. Se le permite establecer el parámetro de clasificación sin necesidad de recargar la página.</td>
	</tr>		
	<tr>
		<td class="col-1"><strong>Modo de visualización (rejilla/lista) (View mode (grid/list))</strong></td>
		<td>-</td>
		<td>visualización del diseño del producto. Esta opción le permite especificar diseños de productos sin recarga.</td>
	</tr>		
	<tr>
		<td class="col-1"><strong>navegación facetada (Layer filter)</strong></td>
		<td>-</td>
		<td>filtrar por atributos. Es una opción principal del módulo. Se activa filtro AJAX por atributos y parámetros situados en la columna de filtro estándar.</td>
	</tr>		
</table>			
<p>Se pueden utilizar todas las opciones o algunas de ellas dependiendo de sus necesidades.</p>	