<h3>Instagram Feed</h3>
<figure class="img-polaroid"><img src="img/magento/instagram-front.png" alt="" /></figure>	
	<p>The module has the following options:</p>
	<p>This widget is used to display a set of images from the Instagram account on the homepage of your store. For this, you need to open your admin panel and navigate to <strong>“Store” > “Configuration” > “TemplateMonster” > “Instagram”</strong> tab. There you will see the following sections:</p>
<ul class="marked-list">
    <li>General</li>
    <li>Home page</li>
    <li>Category View</li>
    <li>Product View </li>
    <li>CMS Page</li>
    <li>Advanced</li>
</ul>
<p>Let’s take a look at each section separately:</p>	
