<h3>Google Map</h3>
<p>Este m&oacute;dulo le permite insertar un mapa de Google en su sitio web de Magento. Por defecto, el m&oacute;dulo viene con dos mapas: el mapa de la p&aacute;gina de contacto y el mapa del pie de p&aacute;gina para el resto de p&aacute;ginas. <br>Ambos mapas pueden configurarse en el Panel de Administraci&oacute;n de Magento en la secci&oacute;n <strong>System > Configuration > Templatemonster > Google Map</strong>.</p>

<ol class="index-list">
	<li>
		<strong>Embed Map Settings</strong> muestra el mapa en el pie de p&aacute;gina de todas las p&aacute;ginas excepto en la p&aacute;gina de contacto.
	</li>
	<li>
		<strong>Contacts Map Settings</strong> muestra el mapa directamente en la p&aacute;gina de contacto.<figure class="img-polaroid"><img src="img/magento/googlemap-1.jpg" alt="" /></figure>
	</li>
	<li>
		Ambas secciones tienen las mismas opciones de configuraci&oacute;n para la apariencia del mapa.<figure class="img-polaroid"><img src="img/magento/googlemap-2.jpg" alt="" /></figure>
	</li>
</ol>
<p>El m&oacute;dulo tiene las siguientes opciones:</p>

<table class="options-table">
	<tr>
		<td class="col-1"><strong>API key</strong></td>
		<td>-</td>
		<td>La clave de la API de Google es necesaria para el correcto funcionamiento del mapa. Puede obtenerla y activarla siguiendo <a href="https://code.google.com/apis/console/" target="_blank">el enlace</a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Coordinates</strong></td>
		<td>-</td>
		<td>datos de localizaci&oacute;n que se mostrar&aacute;n en el centro del mapa visualizado. Puede obtener estos datos desde la web de <a href="http://maps.google.com/" target="_blank">Google Maps</a> o usando <a href="http://www.mapcoordinates.net/" target="_blank" rel="nofollow">esta web</a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Sensor</strong></td>
		<td>-</td>
		<td>cambiar geolocalizaci&oacute;n, activado y desactivado.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Zoom</strong></td>
		<td>-</td>
		<td>valor de ampliaci&oacute;n (no debe ser mayor de 8).</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Map Type</strong></td>
		<td>-</td>
		<td>tipo de mapa que se mostrar&aacute; (calles, sat&eacute;lite, h&iacute;brido, etc.).</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Map width</strong></td>
		<td>-</td>
		<td>ancho del mapa.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Map height</strong></td>
		<td>-</td>
		<td>altura del mapa.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Styles</strong></td>
		<td>-</td>
		<td>estilo de visualizaci&oacute;n del mapa. Para cambiar el aspecto del mapa, use los estilos proporcionados en el sitio web <a href="https://snazzymaps.com/" target="_blank" rel="nofollow">snazzymaps</a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Disable UI</strong></td>
		<td>-</td>
		<td>oculta los elementos para controlar el mapa.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Scrollwheel</strong></td>
		<td>-</td>
		<td>desactiva la opci&oacute;n de desplazamiento con la rueda del rat&oacute;n.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Draggable</strong></td>
		<td>-</td>
		<td>permite mover el mapa con el bot&oacute;n izquierdo del rat&oacute;n o usando los dedos en dispositivos t&aacute;ctiles.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Add marker</strong></td>
		<td>-</td>
		<td>le permite a&ntilde;adir marcadores al mapa.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Marker coordinates</strong></td>
		<td>-</td>
		<td>establece las coordenadas del marcador.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Marker title</strong></td>
		<td>-</td>
		<td>texto que se mostrar&aacute; como tool-tip cuando se pasa por encima.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Marker image</strong></td>
		<td>-</td>
		<td>ruta de la imagen del marcador, si quiere cambiar la predeterminada. La imagen se guarda en la carpeta <strong>skin/frontend/default/themeXXX/images/</strong> de su instalaci&oacute;n de Magento.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Infowindow</strong></td>
		<td>-</td>
		<td>informaci&oacute;n adicional que se mostrar&aacute; si hace clic en el marcador.</td>
	</tr>
</table>
