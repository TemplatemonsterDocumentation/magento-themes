<h3>MegaMenu</h3>
<p class="indent">Este módulo reemplaza el menú estándar y le permite configurar subcategorías que se mostraran en el segundo nivel de anidamiento, agrega bloques estáticos en el menú desplegable para asignar etiquetas, productos de visualización, etc..</p>
<strong>MegaMenu</strong> está incluido en las plantillas por defecto, pero se puede activar o desactivar en la sección <strong>Tiendas > Configuración > Template Monster > Mega Menú (Stores > Configuration > TemplateMonster > Mega Menu)</strong>.
<figure class="img-polaroid"><img src="img/magento/megamenu-1.jpg" alt="" /></figure>

<ol class="index-list">
	<li>Puede configurar la apariencia de cada menú desplegable de la página de configuración de la categoría que se puede encontrar en la sección <strong>Productos > Categorías -> Megamenu (Products > Categories -> Megamenu)</strong>.
		<a class="pretty_image" href="img/magento/Megamenu.png"><figure class="img-polaroid"><img src="img/magento/Megamenu.png" alt="" /></figure></a>
			
	</li>
	<li>
		Para añadir bloque estático en el menú, primero debe crear en la sección <strong>CMS > Bloques estáticos (CMS > Static Blocks)</strong> que estará disponible para su visualización en el menú desplegable. 
		<figure class="img-polaroid"><img src="img/magento/megamenu-static.jpg" alt="" /></figure>
	</li>
	<li>
		Este módulo permite añadir una etiqueta a la categoría de productos. Usted puede hacer esto en la página de menú de categorías <strong>Productos > Categorias -> Megamenu (Products > Categories -> Megamenu)</strong>. Hay una opción 'clase personalizada', por lo que puede crear estilos para esa etiqueta utilizando las reglas CSS.
		<figure class="img-polaroid"><img src="img/magento/megamenu-badge.jpg" alt="" /></figure>
	</li>		
	<li>
		Puede establecer el número de columnas en el Megamenu. Para ello, debe activar la visualización de las columnas y especificar el número de columnas. 
		<figure class="img-polaroid"><img src="img/magento/megamenu-column.jpg" alt="" /></figure>
	</li>		
	<li>
		Para mostrar los productos, active una opción <strong>Permite la visualización de los productos (Turn on products show)</strong> y elegir un par de elementos de la lista. 
		<figure class="img-polaroid"><img src="img/magento/megamenu-product.jpg" alt="" /></figure>
	</li>		
	<li>
		Imagen de subcategoría se pueden añadir en esta sección de subcategoría <strong>MegaMenu</strong>. 
		<figure class="img-polaroid"><img src="img/magento/megamenu-image.jpg" alt="" /></figure>
		<p>Para visualizar la imagen, también debe activar la visualización seleccionando <strong>Activar imágenes para las subcategorías (Turn on images for subcategories)</strong>, en la sección de <strong>MegaMenu</strong> de la categoría padre.</p>
		<figure class="img-polaroid"><img src="img/magento/megamenu-image-1.jpg" alt="" /></figure>
	</li>
	<li>
		Diseños de Megamenu se pueden editar utilizando una opción <strong>Plantilla personalizada para el elemento de megamenu (Custom template for megamenu item)</strong>. Los archivos de origen de estos modelos se encuentran en el siguiente directorio: <strong>app\design\frontend\TemplateMonster\themeXXX\TemplateMonster_Megamenu\templates\html\topmenu\</strong>.
		<figure class="img-polaroid"><img src="img/magento/megamenu-template.jpg" alt="" /></figure>
	</li>
</ol>