<h3>MegaMenu</h3>
<p class="indent">Este módulo troca o menu normal com um mais avançado, isso te permite configurar subcategorias para exibir até 2 níveis de aninhamento, incluir blocos aos menus em cascata, incluir etiquetas, exibir produtos, etc.</p>
<strong>MegaMenu</strong> está incluso aos templates por padrão, mas você pode ativá-lo ou desativá-lo na seção <strong>Stores > Configuration > TemplateMonster > Mega Menu</strong>.
<figure class="img-polaroid"><img src="img/magento/megamenu-1.jpg" alt="" /></figure>

<ol class="index-list">
	<li>Você pode configurar a aparência de cada menu em cascata no menu de configurações de categoria que pode ser encontrado na seção <strong>Products > Categories -> Megamenu</strong>.
		<a class="pretty_image" href="img/magento/Megamenu.png"><figure class="img-polaroid"><img src="img/magento/Megamenu.png" alt="" /></figure></a>
			
	</li>
	<li>
		Para inlcuir um bloco estático ao menu em cascata, primeiro voc~e precisa criá-lo em <strong>CMS > Static Blocks</strong> e então torná-lo disponível no menu em cascata.
		<figure class="img-polaroid"><img src="img/magento/megamenu-static.jpg" alt="" /></figure>
	</li>
	<li>
		Este módulo permite incluir uma etiqueta a qualquer categoria de produtos. Você pode fazer isso na página de menu das categorias em <strong>Products > Categories -> Megamenu</strong>. Tem uma opção 'custom class', assim você pode criar estilos para esta etiqueta usando regras de CSS.
		<figure class="img-polaroid"><img src="img/magento/megamenu-badge.jpg" alt="" /></figure>
	</li>		
	<li>
		Você pode definir o número de colunas no mega menu. Para fazer isso você deve ativar a exibição de colunas e especificar o número de colunas desejadas.
		<figure class="img-polaroid"><img src="img/magento/megamenu-column.jpg" alt="" /></figure>
	</li>		
	<li>
		Para exibir produtos, ative a opção <strong>Turn on products show</strong>, e escolha alguns itens na lista. 
		<figure class="img-polaroid"><img src="img/magento/megamenu-product.jpg" alt="" /></figure>
	</li>		
	<li>
		A imagem de subcategoria pode ser incluída nesta seção de subcategoria do <strong>MegaMenu</strong>. 
		<figure class="img-polaroid"><img src="img/magento/megamenu-image.jpg" alt="" /></figure>
		<p>No entanto, para fazer a imagem aparecer, você precisa ativar sua exibição escolhendo a opção <strong>Turn on images for subcategories</strong>, no seção da categoria pai desta em <strong>MegaMenu</strong>.</p>
		<figure class="img-polaroid"><img src="img/magento/megamenu-image-1.jpg" alt="" /></figure>
	</li>
	<li>
		Os layouts do Megamenu podem ser personalizados através da opção <strong>Custom template for megamenu item</strong>. Os arquivos fonte destes templates ficam localizados no seguinte diretório: <strong>app\design\frontend\TemplateMonster\themeXXX\TemplateMonster_Megamenu\templates\html\topmenu\</strong>.
		<figure class="img-polaroid"><img src="img/magento/megamenu-template.jpg" alt="" /></figure>
	</li>
</ol>