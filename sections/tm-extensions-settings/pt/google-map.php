<h3>Google Map</h3>
<p>Esse módulo permite que você inclua um mapa do Google Maps no seu site Magento. Por padrão, este módulo provém dois mapas: o mapa na página de contatos e o mapa no rodapé de outras páginas. <br>Ambos podem ser configurados através do Painel Administrativo do Magento em <strong>Sistema (System) > Configurações (Configuration) > Templatemonster > Google Map</strong>.</p>

<ol class="index-list">
	<li>
		<strong>Embed Map Settings</strong>: este bloco exibe os mapas em todas as páginas, com exceção da página de contatos.
	</li>
	<li>
		<strong>Contacts Map Settings</strong> este bloco exibe o mapa diretamente na página de contato.<figure class="img-polaroid"><img src="img/magento/googlemap.jpg" alt="" /></figure>
	</li>
	<li>
		Ambas seções têm as as mesmas opções para mudar a aparência do mapa.<figure class="img-polaroid"><img src="img/magento/googlemap-1.jpg" alt="" /></figure>
	</li>
</ol>
<p>O módulo tem as seguintes opções:</p>

<table class="options-table">
	<tr>
		<td class="col-1"><strong>API key</strong></td>
		<td>-</td>
		<td>a chave da API do Google (Google Api Key) é necessária para o funcionamento do mapa. Você pode receber e ativá-la seguindo <a href="https://code.google.com/apis/console/" target="_blank">este link</a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Coordinates</strong></td>
		<td>-</td>
		<td>dados de localização que serão mostrados no centro do mapa. Você pode encontrar essas informações usando o <a href="http://maps.google.com/" target="_blank">Google Maps</a> ou através <a href="http://www.mapcoordinates.net/" target="_blank" rel="nofollow">deste aqui</a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Sensor</strong></td>
		<td>-</td>
		<td>liga e desliga a geolocalização.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Zoom</strong></td>
		<td>-</td>
		<td>um valor de aproximação (não deve ser maior que 8).</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Map Type</strong></td>
		<td>-</td>
		<td>tipo de mapa mostrado (ruas (roadmap), satélite (satellite), híbrido (hybrid), etc.).</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Map width</strong></td>
		<td>-</td>
		<td>largura do mapa.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Map height</strong></td>
		<td>-</td>
		<td>altura do mapa.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Styles</strong></td>
		<td>-</td>
		<td>estilo do mapa. Para mudar a aparência do mapa, utilize os estilos do site <a href="https://snazzymaps.com/" target="_blank" rel="nofollow">snazzymaps</a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Disable UI</strong></td>
		<td>-</td>
		<td>esconde os elementos que controlam o mapa.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Scrollwheel</strong></td>
		<td>-</td>
		<td>desabilita a navegação pelo mapa com o mouse.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Draggable</strong></td>
		<td>-</td>
		<td>habilita o movimento do mapa com o botão esquerdo do mouse (e arrastando) ou através do toque, em aparelhos com tela sensível a toque.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Add marker</strong></td>
		<td>-</td>
		<td>permite que você coloque um ponteiro no mapa.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Marker coordinates</strong></td>
		<td>-</td>
		<td>define coordenadas do ponteiro.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Marker title</strong></td>
		<td>-</td>
		<td>texto que será mostrado como descrição ao passar em cima do ponteiro no mapa.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Marker image</strong></td>
		<td>-</td>
		<td>caminho para a imagem do ponteiro, se desejar mudar a imagem padrão. A imagem é salva na pasta <strong>skin/frontend/default/themeXXX/images/</strong> de sua instalação do Magento.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Infowindow</strong></td>
		<td>-</td>
		<td>informação adicional que devem ser mostradas ao clicar-se no ponteiro.</td>
	</tr>
</table>
