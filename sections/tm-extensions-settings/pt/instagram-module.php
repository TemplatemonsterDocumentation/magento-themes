<h3>Módulo do Instagram</h3>
<p>O módulo do Instagram permite que você mostre uma galeria do Instagram na sua loja.<br>
Este módulo permite mostrar dois tipos de galeria: galeria de usuário e galeria de etiquetas (tags).</p>						
<p>Todas as opções de configuração estão disponíveis no painel administrativo em <strong>Sistema (System) > Configurações (Configuration) > Templatemonster > Instagram</strong>.</p>
<figure class="img-polaroid"><img src="img/magento/instagram.jpg" alt="" /></figure>			
<p>O módulo tem as seguintes opções:</p>			
<table  class="options-table">
	<tr>
		<td class="col-1"><strong>Client ID</strong></td>
		<td>-</td>
		<td>Identificador de app do Instagram. Para receber o seu, registre seu app no seguinte <a href="http://instagram.com/developer/" target="_blank">link</a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Username</strong></td>
		<td>-</td>
		<td>Usuário do Instagram do qual você quer mostrar a galeria.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Sample tag</strong></td>
		<td>-</td>
		<td>etiquetas (tags) usadas para mostrar imagens.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Product tag</strong></td>
		<td>-</td>
		<td>é usada para encontrar imagens similares, onde o início da etiqueta (tag) consiste do parâmetro escolhido. E. g.: <strong>#storetitleproductname</strong>, <strong>#productsku</strong>, <strong>#storetitleproductsku</strong>, etc.</td>
	</tr>				
</table>			
		