<h3>Ajax Wishlist</h3>
<p>Lista de desejos é uma funcionalidade comum de comércio eletrônico. Nós criamos um módulo para deixá-la mais conveniente, ele é baseado na tecnologia AJAX e permite incluir produtos à lista de desejos sem precisar navegar até uma página separada.</p>
<p>Para configurar este módulo, navegue para o painel administrativo do Magento e escolha <strong>Stores > Configuration</strong> no menu ao lado esquerdo.</p>
<figure class="img-polaroid"><img src="img/magento/compare.jpg" alt="" /></figure>
<p>Então escolha o item <strong> TemplateMonster > Ajax Wishlist </strong> na nova página no lado esquerdo.</p>
 <figure class="img-polaroid"><img src="img/magento/wishlist.jpg" alt="" /></figure>


<p>Vamos dar uma olhada na configuração do módulo:</p>			
<table class="options-table">
	<tr>
