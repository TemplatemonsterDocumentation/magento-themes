		<td>-</td>
		<td>esta opção permite ativar ou desativar o módulo.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Show spinner</strong></td>
		<td>-</td>
		<td>esta opção permite ativar a exibição do ícone animado enquanto inclui um item na lista de desejos.</td>
