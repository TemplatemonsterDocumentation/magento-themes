<h3>Ajax Catalog</h3>
<p>Seleção rápida e fácil em uma seleção variada de produtos ajuda a criar uma decisão positiva na compra quando se está em uma loja online. O módulo Ajax Catalog te dá essa essa possibilidade e é o módulo mais popular em lojas online de diversas plataformas. Ele permite a você encontrar e filtrar produtos de acordo com parâmetros selecionados de forma rápida e fácil sem precisar recarregar a página.</p>						
<p>Para configurar esse módulo, navegue para a seção <strong>Stores -> Settings -> Configuration</strong>.</p>
<figure class="img-polaroid"><img src="img/magento/compare.jpg" alt="" /></figure>			
<p>Então, navegue para <strong>TemplateMonster -> Catalog Ajax</strong> no menu do lado esquerdo para acessar a interface de configuração do módulo.</p>
<figure class="img-polaroid"><img src="img/magento/ajaxcatalog.jpg" alt="" /></figure>		
<p>Vamos dar uma olhada nas opções:</p>			
<table  class="options-table">
	<tr>
		<td class="col-1"><strong>Pagination </strong></td>
		<td>-</td>
		<td>paginação da listagem. Se sua listagem inclui mais de uma página com produtos, isso permite que você navegue entre as páginas internas sem recarregar a página principal.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Show number items</strong></td>
		<td>-</td>
		<td>número de produtos exibidos na página. Esta opção permite mudar o número de produtos mostrados em uma única página sem recarregar.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Sort by (desc/asc)</strong></td>
		<td>-</td>
		<td>ordem dos produtos (decrescente ou crescente). Você pode ordenar os itens sem recarregar a página principal.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>List order (price, position, name)</strong></td>
		<td>-</td>
		<td>parâmetros de ordenação. Permite definir um parâmetro de ordenação sem recarregar a página.</td>
	</tr>		
	<tr>
		<td class="col-1"><strong>View mode (grid/list)</strong></td>
		<td>-</td>
		<td>layout da exibição de produtos. Esta opção especifica o tipo de layout usado ao listar páginas sem recarregá-las.</td>
	</tr>		
	<tr>
		<td class="col-1"><strong>Layer filter</strong></td>
		<td>-</td>
		<td>filtrar por atributos. Esta é a principal opção do módulo. Ativa o filtro AJAX por atributos e parâmetros localizados na coluna de filtros comuns da loja.</td>
	</tr>		
</table>			
<p>Sinta-se a vontade para usar todas essas opções ao mesmo tempo ou apenas algumas delas, você decide.</p>	