<h3>MegaMenu</h3>
<p class="indent">Dieses Modul ersetzt das Standardmenü der Kategorie durch ein Menü, in dem man die Anzeige der Unterkategorien bis zu zwei Verschachtelungsebenen einstellen, statische Blöcke zum DropDown Menü oder Labels hinzufügen, Produkte usw. darstellen kann.</p>
<strong>MegaMenu</strong> gehört zu Vorlagen standardmäßig, aber Sie können es im Menü <strong>Stores > Configuration > TemplateMonster > Mega Menu</strong> aktivieren oder deaktivieren.
<figure class="img-polaroid"><img src="img/magento/megamenu-1.jpg" alt="" /></figure>

<ol class="index-list">
	<li>Die Anzeige des DropDown Menüs kann auf der Seite der Kategorie-Einstellungen eingestellt werden, die Sie im Bereich <strong>Products > Categories -> Megamenu</strong> finden.
		<a class="pretty_image" href="img/magento/Megamenu.png"><figure class="img-polaroid"><img src="img/magento/Megamenu.png" alt="" /></figure></a>
			
	</li>
	<li>
		Um einen statischen Block zum DropDown Menü hinzuzufügen, müssen Sie zuerst einen statischen Block im Bereich <strong>CMS > Static Blocks</strong> erstellen. Nur danach können Sie ihn im Menü darstellen. 
		<figure class="img-polaroid"><img src="img/magento/megamenu-static.jpg" alt="" /></figure>
	</li>
	<li>
		Das Modul bietet die Möglichkeit an, ein Label zu jeder Kategorie hinzuzufügen. Sie können dies im Kategoriemenü <strong>Products > Categories -> Megamenu</strong> erfüllen. Für dieses Label können Sie auch eine benutzerdefinierte Klasse hinzufügen, um in der Zukunft Stile dieses Elemenetes mittels der CSS Regeln festzulegen.
		<figure class="img-polaroid"><img src="img/magento/megamenu-badge.jpg" alt="" /></figure>
	</li>		
	<li>
		Im MegaMenu können Sie die Anzahl der Spalten festlegen. Dafür sollen Sie die Anzeige der Spalten aktivieren und ihre Anzahl festlegen. 
		<figure class="img-polaroid"><img src="img/magento/megamenu-column.jpg" alt="" /></figure>
	</li>		
	<li>
		Um Produkte anzuzeigen, sollen Sie die Option <strong>Turn on products show</strong> aktivieren und Produkte aus der Liste wählen. 
		<figure class="img-polaroid"><img src="img/magento/megamenu-product.jpg" alt="" /></figure>
	</li>		
	<li>
		Ein Bild für die Unterkategorie können Sie im Bereich <strong>MegaMenu</strong> dieser Unterkategorie hinzufügen . 
		<figure class="img-polaroid"><img src="img/magento/megamenu-image.jpg" alt="" /></figure>
		<p>Damit dieses Bild angezeigt werden kann, sollen Sie die Option <strong>Turn on images for subcategories</strong> aktivieren, die sich im Bereich <strong>MegaMenu</strong> der Hauptkategorie befindet.</p>
		<figure class="img-polaroid"><img src="img/magento/megamenu-image-1.jpg" alt="" /></figure>
	</li>
	<li>
		Die MegaMenü Layouts können Sie ändern, indem Sie die Option <strong>Custom template for megamenu item</strong> nutzen. Die Dateien dieser Vorlagen befinden sich im Ordner <strong>app\design\frontend\TemplateMonster\theme764\TemplateMonster_Megamenu\templates\html\topmenu\</strong> auf dem Server.
		<figure class="img-polaroid"><img src="img/magento/megamenu-template.jpg" alt="" /></figure>
	</li>
</ol>