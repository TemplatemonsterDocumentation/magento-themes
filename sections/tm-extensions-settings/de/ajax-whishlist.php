<h3>Ajax Wishlist</h3>
<p>Die Wunschliste ist eine der Standardoptionen der E-commerce Plattform. Zur Benutzerfreundlichkeit haben wir das Modul erstellt, das mittels AJAX funktioniert und Ihnen ermöglicht, Produkte zur Wunschliste hinzuzufügen, ohne eine neue Seite zu öffnen.</p>
<p>Um dieses Modul einzustellen, sollen Sie sich ins Control Panel einloggen und im Menü links den Bereich <strong>Stores > Configuration</strong> wählen.</p>
<figure class="img-polaroid"><img src="img/magento/compare.jpg" alt="" /></figure>
<p>Auf der neuen Seite im Menü links wählen Sie den Punkt <strong> TemplateMonster > Ajax Wishlist </strong></p>
 <figure class="img-polaroid"><img src="img/magento/wishlist.jpg" alt="" /></figure>


<p>Lassen Sie uns die Moduleinstellungen zeigen:</p>			
<table class="options-table">
	<tr>
		<td class="col-1"><strong>Enable</strong></td>
		<td>-</td>
		<td>diese Option ermöglicht es Ihnen das Modul entweder aktivieren oder deaktivieren.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Show spinner</strong></td>
		<td>-</td>
		<td>diese Option ermöglicht es Ihnen die Anzeige des Download-Icons zu aktivieren, während ein Produkt zur Wunschliste hinzugefügt wird.</td>
	</tr>					
	<tr>
		<td class="col-1"><strong>Show Success Message</strong></td>
		<td>-</td>
		<td>diese Option aktiviert die Anzeige des DropDown Fensters nach dem Hinzufügen eines Produktes zur Wunschliste.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Success Message Text</strong></td>
		<td>-</td>
		<td>in diesem Feld können Sie einen Text eingeben, der in dem DropDown Fenster angezeigt wird, nachdem ein Produkt zur Wunschliste hinzugefügt wurde.</td>
	</tr>		
</table>