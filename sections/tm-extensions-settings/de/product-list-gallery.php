<h3>Product list gallery</h3>
<p class="indent">Das jeweilige Modul ermöglicht es Produktvorschaubilder neben dem Hauptproduktbild anzuzeigen. Im Standard-Theme werden die Vorschaubilder unter dem Hauptproduktbild angezeigt. Aber in unseren Themes werden die Vorschaubilder gemäß des PSD-Layouts angezeigt.</p>
<p>Dieses Modul kann im Control Panel Magento, in der Sektion <strong>System > Configuration > Templatemonster > Product List Gallery</strong>sowohl aktiviert als auch deaktiviert werden.</p>
<figure class="img-polaroid"><img src="img/magento/productgallery.jpg" alt="" /></figure>
<p>Das Modul enthält 4 Sektionen von Einstellungen:<strong>List View</strong>, <strong>Grid View</strong>, <strong>Homepage List View</strong>, <strong>Homepage Grid View</strong>.<br>
In den Sektionen <strong>Homepage List View</strong> und <strong>Homepage Grid View</strong> wird die Darstellung von der Galerie auf der Hauptseite des Online-Shops eingestellt. Andere zwei Sektionen stellen die Darstellung von der Galerie auf der Kategorieseite ein.</p>
<p>Alle Sektionen enthalten gleiche Einstellungen:</p>			
<table class="options-table">
	<tr>
		<td class="col-1"><strong>Enable/Disable</strong></td>
		<td>-</td>
		<td>das Modul aktivieren/deaktivieren.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Image width</strong></td>
		<td>-</td>
		<td>die Breite des Hauptproduktbildes. </td>
	</tr>
	<tr>
		<td class="col-1"><strong>Image height</strong></td>
		<td>-</td>
		<td>die Höhe des Hauptproduktbildes. </td>
	</tr>
	<tr>
		<td class="col-1"><strong>Thumbnail width</strong></td>
		<td>-</td>
		<td>die Breite des Vorschaubildes.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Thumbnail height</strong></td>
		<td>-</td>
		<td>die Höhe des Vorschaubildes.</td>
	</tr>
</table>
<p>Achten Sie! Maximale Bildbreite wird mit der Spaltenbreite, in der sie sich befindet, gleich sein. </p>
		