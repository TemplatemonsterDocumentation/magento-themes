<h3>Live Search</h3>
<p>This module allows instant search of a product when entering from the keypad, followed by output in a pop-up window.</p>
<p>All configuration options are available in the admin panel in <strong>System > Configuration > Templatemonster > Live Search</strong> section.</p>
<figure class="img-polaroid"><img src="img/magento/live-search.png" alt="Live Search" /></figure>
<p>The module has the following settings:</p>
<table  class="options-table">
	<tr>
		<td class="col-1"><strong>Enable/Disable</strong></td>
		<td>-</td>
		<td>turn on/turn off the module;</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Image Width</strong></td>
		<td>-</td>
		<td>sets width of the product image;</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Image Height</strong></td>
		<td>-</td>
		<td>Sets height of the product image;</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Description</strong></td>
		<td>-</td>
		<td>shows/hides product description in the pop-up</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Placeholder Text</strong></td>
		<td>-</td>
		<td>you can enter the placeholder text.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Number Product Show</strong></td>
		<td>-</td>
		<td>sets max count items.</td>
	</tr>
</table>
<p>You can change the view of Live search. To do this, edit the <i class="file-path">skin/frontend/tm_themes/themeXXX/css/tm/livesearch/livesearch.css</i> file.</p>