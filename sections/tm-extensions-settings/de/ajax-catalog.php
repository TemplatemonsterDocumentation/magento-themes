<h3>Ajax Catalog</h3>
<p>Schnelle und einfache Auswahl unter mehreren Produkten motiviert Kunden zum Kauf in Ihrem Online-Shop. Das Modul Ajax Catalog ist sehr beliebt unter Online-Shops, die auf Basis von verschiedenen Plattformen erstellt sind. Es ermöglicht gewünschte Produkte schnell und einfach zu finden und entsprechend den ausgewählten Parametern, ohne die Seite neu zu laden, anzuzeigen. </p>						
<p>Um dieses Modul einzustellen, finden Sie den Bereich <strong>Stores -> Settings -> Configuration</strong>.</p>
<figure class="img-polaroid"><img src="img/magento/compare.jpg" alt="" /></figure>			
<p>Im Menü links wählen Sie den Punkt <strong>Templatemonster -> Catalog Ajax</strong> , um die Schnittstelle der Moduleinstellungen zu öffnen.</p>
<figure class="img-polaroid"><img src="img/magento/ajaxcatalog.jpg" alt="" /></figure>		
<p>Lassen Sie uns die Moduleinstellungen zeigen:</p>			
<table  class="options-table">
	<tr>
		<td class="col-1"><strong>Pagination </strong></td>
		<td>-</td>
		<td>Seitennummerierung innerhalb vom Listing. Wenn es im Listing mehr als eine Produktseite gibt, kann man eine neue Seite öffnen, ohne die Hauptseite neu zu laden.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Show number items</strong></td>
		<td>-</td>
		<td>die Anzahl der angezeigten Produkte auf einer Seite. Diese Option ermöglicht es Ihnen, die Anzahl der angezeigten Produkte auf der Listing-Seite zu ändern, ohne sie neu zu laden.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Sort by (desc/asc)</strong></td>
		<td>-</td>
		<td>Produktreihenfolge. Man kann Produkte sortieren, ohne die Seite neu zu laden.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>List order (price, position, name)</strong></td>
		<td>-</td>
		<td>Sortieroptionen. Wählen Sie eine Option aus, nach der Produkte sortiert werden, ohne die Seite neu zu laden.</td>
	</tr>		
	<tr>
		<td class="col-1"><strong>View mode (grid/list)</strong></td>
		<td>-</td>
		<td>ein Layout zur Produktanzeige. Man kann das Layout zur Produktanzeige ändern, ohne die Seite neu zu laden.</td>
	</tr>		
	<tr>
		<td class="col-1"><strong>Layer filter</strong></td>
		<td>-</td>
		<td>der Filter nach Attributen. Es ist die Hauptoption in diesem Modul. Sie aktiviert AJAX Produktauswahl nach Attributen und Optionen, die sich in der Standardspalte des Shopfilters befindet.</td>
	</tr>		
</table>			
<p>Sie können entweder alle Optionen gleichzeitig oder getrennt nutzen.</p>
		