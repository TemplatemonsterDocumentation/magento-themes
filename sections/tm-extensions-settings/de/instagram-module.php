<h3>Instagram module</h3>
<p>Instagram Modul ermöglicht die Instagram Galerie in Ihrem Shop anzuzeigen.<br>
Das jeweilige Modul ermöglicht zwei Typen von der Galerie anzuzeigen: Galerie und Galerie nach dem Tag.</p>						
<p>Alle Steuerungsoptionen sind im Control Panel in der Sektion <strong>System > Configuration > Templatemonster > Instagram</strong> verfügbar.</p>
<figure class="img-polaroid"><img src="img/magento/instagram.jpg" alt="" /></figure>			
<p>Lassen Sie uns die Moduloptionen anschauen:</p>			
<table  class="options-table">
	<tr>
		<td class="col-1"><strong>Client ID</strong></td>
		<td>-</td>
		<td>ID Ihrer Instagram Anwendung. Um diese ID zu erhalten, sollen Sie Ihre Anwendung unter <a href="http://instagram.com/developer/" target="_blank">registrieren</a>.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Username</strong></td>
		<td>-</td>
		<td>Benutzername in Instagram, dessen Galerie Sie anzeigen möchten.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Sample tag</strong></td>
		<td>-</td>
		<td>Tags, nach denen die Bilder angezeigt werden.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Product tag</strong></td>
		<td>-</td>
		<td>wird benutzt, um ähnliche Bilder zu finden (Starttag besteht aus einem Satz von ausgewählten Parametern). Zum Beispiel, <strong>#storetitleproductname</strong>, <strong>#productsku</strong>, <strong>#storetitleproductsku</strong> usw.</td>
	</tr>				
</table>			
		