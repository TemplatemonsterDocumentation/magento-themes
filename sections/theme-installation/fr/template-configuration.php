<h3>Configuration du Template</h3>
<p>Nous allons passer en revue les options de configuration du template en se basant sur l'exemple. Il est configuré de la même manière que notre template de Démo Live, sans affecter les données relatives aux produits.</p>
<h4>Activation du nouveau template</h4>
<ol>
	<li>Ouvrir le panneau d'administration de Magento et allez dans <b>System > Design</b> dans le menu du haut.</li>
	<li>Cliquez sur le bouton "<b>Add Design Change</b>".</li>
	<li>Dans le champ "<b>Custom Design</b>", choisissez le nom du thème (par exemple, theme###).</li>
	<li>Cliquez sur le bouton "<b>Save</b>" pour confirmer les changements.</li>
</ol>
