<h4>Ré-indexage des données</h4>
<ol>
	<li>Ouvrez le panneau d'administration de Magento et dans le menu du haut, sélectionnez <b>System > Index Management</b>.</li>
	<li>Cochez "<b>Select All</b>" dans le coin en haut à gauche.</li>
	<li>Dans le champ "<b>Actions</b>" sélectionnez "<b>Reindex Data</b>".</li>
	<li>Cliquez sur le bouton "<b>Submit</b>".</li>
	<li>Veuillez patienter, ceci pourrait prendre du temps.</li>
</ol>
