<h3>Configuration du Template</h3>
<p>Nous allons passer en revue les options de configuration du template en se basant sur l'exemple. Il est configuré de la même manière que notre template de Démo Live, sans affecter les données relatives aux produits.</p>
<h4>Activation du nouveau template</h4>
<ol class="index-list">
	<li>Ouvrir le panneau d'administration de Magento et allez dans <strong>System > Design</strong> dans le menu du haut.<figure class="img-polaroid"><img src="img/magento/Theme-set-1.jpg" alt="" /></figure></li>
	<li>Cliquez sur le bouton "<strong>Add Design Change</strong>".<figure class="img-polaroid"><img src="img/magento/Theme-set-2.jpg" alt="" /></figure></li>
	<li>Dans le champ "<strong>Custom Design</strong>", choisissez le nom du thème (par exemple, theme###). Cliquez sur le bouton "<strong>Save</strong>" pour confirmer les changements.<figure class="img-polaroid"><img src="img/magento/Theme-set-3.jpg" alt="" /></figure></li>	
</ol>

<h4>Ré-indexage des données</h4>
<ol class="index-list">
	<li>Ouvrez le panneau d'administration de Magento et dans le menu du haut, sélectionnez <strong>System > Index Management</strong>. <figure class="img-polaroid"><img src="img/magento/Theme-set-4.jpg" alt="" /></figure></li>
	<li>Cochez "<strong>Select All</strong>" dans le coin en haut à gauche. <figure class="img-polaroid"><img src="img/magento/Theme-set-5.jpg" alt="" /></figure></li>
	<li>Dans le champ "<strong>Actions</strong>" sélectionnez "<strong>Reindex Data</strong>".</li>
	<li>Cliquez sur le bouton "<strong>Submit</strong>".</li>
	<li>Veuillez patienter, ceci pourrait prendre du temps.</li>
</ol>

<h4>Désactiver le cache</h4>
<p>Pour que les changements apparaissent sur votre site sans retard, vous devez désactiver le cache. Ceci prévient les actions supplémentaires de nettoyage de cache après chaque changement que vous faites. Vous pouvez l'activer quand vous avez fini de modifier la boutique.</p>
<ol class="index-list">
	<li>Ouvrez le panneau d'administration de Magento et allez dans <strong>System > Cache Management</strong> dans le menu du haut. <figure class="img-polaroid"><img src="img/magento/Theme-set-6.jpg" alt="" /></figure></li>
	<li>Dans le coin en haut à gauche, cliquez sur "<strong>Select All</strong>". <figure class="img-polaroid"><img src="img/magento/Theme-set-7.jpg" alt="" /></figure></li>
	<li>Sur le côté droit, dans le champ "<strong>Actions</strong>", sélectionnez "<strong>Disable</strong>".</li>
	<li>Cliquez sur le bouton "<strong>Submit</strong>".</li>
</ol>