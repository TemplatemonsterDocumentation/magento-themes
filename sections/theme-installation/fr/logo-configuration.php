<h3>Configuration du logo</h3>
<p>Suivez les étapes suivantes pour paramétrer l'image de logo inclue dans le package du thème (vous pouvez la voir sur la page de Démo Live).</p>
<ol class="index-list"> 
	<li>Ouvrez le panneau d'administration de Magento et sélectionnez <b>System > Configuration</b> dans le menu du haut.</li>
	<li>Sélectionnez l'onglet "<b>Design</b>".</li>
	<li>Dans la section "<b>Header</b>", modifiez la valeur du champ "<b>Logo Image Src</b>". Changez le "<b>gif</b>" en "<b>png</b>".</li>
	<li>Cliquez sur le bouton <b>"Save Config"</b>.</li>
</ol>
<p>Pour installer le logo de votre société, vous devez uploader le fichier image du logo dans le dossier <b>/images</b> de votre thème Magento.</p>
