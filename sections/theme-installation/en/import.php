                              <h3 align="justify">Pages, static blocks and widgets import</h3>
                                <p align="justify">In order to make your template look exactly like our Live Demo, you need to import the store pages, static blocks and widgets. Please follow the instructions below in order to import store pages, static blocks and widgets.</p> 
                               <ul class="marked-list">
                               <h4 align="justify">Pages Import</h4>
                               <li>Navigate to <strong>Stores > Settings > Configuration</strong> section.</li>

                             <figure class="img-polaroid">
                                    <div align=""><img src="img/magento/compare.jpg" alt="" /></div>
                                </figure>
                                <li>Then choose the <strong>TEMPLATEMONSTER > Theme Options</strong> in the left side menu and you'll move to the module settings interface.</li>
                                <figure class="img-polaroid">
                                    <div align=""><img src="img/magento/color.jpg" alt="" /></div>
                                </figure>


<li>Click on the <strong>Store View: Default Config</strong>. Select a <strong>Main Website</strong> from the option dropdown.</li>
 <figure class="img-polaroid">
                                    <div align=""><img src="img/magento/default.jpg" alt="" /></div>
                                </figure>
<li>Confirm the scope switching in the pop-up window. Press &quot;OK&quot; to confirm.</li>
<p>
  <figure class="img-polaroid"><img src="img/magento/to_23.jpg" alt="" /></figure>
</p>
<li>Go to the <strong>Sample Data Installer</strong> tab > <strong>CMS pages</strong>.  </li>
 <figure class="img-polaroid">
                                    <div align=""><img src="img/magento/pages-import.jpg" alt="" /></div>
                                </figure>
                                <li>Select files you want to import. You  need to select <strong>Cms Page</strong>.</li>
                                <li>Set the <strong>Override</strong> option to "Yes" if you want to replace your store pages with new ones in case of duplicate key.</li>
                                <li>Press the <strong>Import</strong> button.</li>
                                <figure class="img-polaroid">
                                    <div align=""><img src="img/magento/pages-import-2.jpg" alt="" /></div>
                                </figure>
                                <li>You will see the confirmation message. The message will inform you about the total number of imported pages.</li>
                                 <figure class="img-polaroid">
                                    <div align=""><img src="img/magento/pages-import-3.jpg" alt="" /></div>
                                </figure>
</ul>
 <h4 align="justify">Static blocks Import</h4>
                                <p align="justify">When you are done with the pages import, you need to import the static blocks. The procedure is similar to the previous.</p>
                                
                               <li>Navigate to <strong>Stores > Settings > Configuration</strong> section.</li>

                             <figure class="img-polaroid">
                                    <div align=""><img src="img/magento/compare.jpg" alt="" /></div>
                                </figure>
                                <li>Then choose the <strong>TEMPLATEMONSTER > Theme Options</strong> in the left side menu and you'll move to the module settings interface.</li>
                                <figure class="img-polaroid">
                                    <div align=""><img src="img/magento/color.jpg" alt="" /></div>
                                </figure>


<li>Click on the <strong>Store View: Default Config</strong>. Select a <strong>Main Website</strong> from the option dropdown.</li>
 <figure class="img-polaroid">
                                    <div align=""><img src="img/magento/default.jpg" alt="" /></div>
                                </figure>
<li>Confirm the scope switching in the pop-up window. Press &quot;OK&quot; to confirm.</li>
<p>
  <figure class="img-polaroid"><img src="img/magento/to_23.jpg" alt="" /></figure>
</p>
<li>Go to the <strong>Sample Data Installer</strong> tab > <strong>CMS Blocks</strong>.  </li>
 
                                <li>Select files you want to import. You  need to select <strong>Cms Block</strong>.</li>
                                <li>Set the <strong>Override</strong> option to "Yes" if you want to replace your blocks with new ones in case of duplicate key.</li>
                                <li>Press the <strong>Import</strong> button.</li>
                                <figure class="img-polaroid">
                                    <div align=""><img src="img/magento/block-import-2.jpg" alt="" /></div>
                                </figure>
                                <li>You will see the confirmation message. The message will inform you about the total number of imported blocks.</li>

</ul>
<h4 align="justify">Widgets Import</h4>
                                <p align="justify">Now you need to import the widgets. The procedure is almost the same as the previous one.</p>
                                 <li>Navigate to the <strong>Stores > Settings > Configuration</strong> section.</li>

                             <figure class="img-polaroid">
                                    <div align=""><img src="img/magento/compare.jpg" alt="" /></div>
                                </figure>
                                <li>Then choose the <strong>TEMPLATEMONSTER > Theme Options</strong> in the left side menu and you'll move to the module settings interface.</li>
                                <figure class="img-polaroid">
                                    <div align=""><img src="img/magento/color.jpg" alt="" /></div>
                                </figure>


<li>Click on the <strong>Store View: Default Config</strong>. Select a <strong>Main Website</strong> from the option dropdown.</li>
 <figure class="img-polaroid">
                                    <div align=""><img src="img/magento/default.jpg" alt="" /></div>
                                </figure>
<li>Confirm the scope switching in the pop-up window. Press &quot;OK&quot; to confirm.</li>
<p>
  <figure class="img-polaroid"><img src="img/magento/to_23.jpg" alt="" /></figure>
</p>
<li>Go to the <strong>Sample Data Installer</strong> tab > <strong>Widgets</strong>.  </li>
 
                                <li>Select the layout (if you have any) you want to import widgets from. You  need to select the <strong>Cms Block</strong>.</li>
                                <li>Set the <strong>Override</strong> option to "Yes" if you want to replace your widgets with new ones in case of duplicate key.</li>
                                <li>Press the <strong>Import</strong> button.</li>
                                
                                <li>You will see the confirmation message. The message will inform you about the total number of imported widgets.</li>

</ul>

<p>Remember to <strong>save</strong> the settings.</p> 
