<h3>Logo configuration</h3>
<p align="justify">Please follow these steps when setting up logo image included into the theme package [you can view it on the Live Demo page].</p>
<div align="justify">
    Open the Magento admin panel and select the <strong>Store > Configuration</strong> in the sidebar menu.
                                </div>
                                <figure class="img-polaroid">
  <img src="img/magento/compare.jpg" alt="" /></figure>
                                <div align="justify">In the panel on the left expand the<strong> Templatemonster</strong> section. Then choose <strong>Theme Options</strong>. Also, you can edit a particular store view if you have it. (See <strong>Store View</strong> option on the tool bar.)
                                </div>
                               
                                <div align="justify">Choose the <strong>image</strong> Logo type. Press the <strong>Browse</strong> button. Select the image for your Logo and upload it. </div>
                                
                                <div align="justify">When complit, tap <strong>"Save Config"</strong>.</div>
<figure class="img-polaroid">
                                    <div align=""><img src="img/magento/Theme-set-9-3.jpg" alt="" /></div>
                                </figure>
                                <p>You can use the <strong>text</strong> Logo type.</p>
                                <p align="justify">The <strong>Theme Options </strong>module features will be  described  later in the <strong>TM Extensions Settings</strong> chapter.</p>

