<h3>Logo configuration.</h3>
<p>Please follow these steps when setting up logo image included into the theme package (you can view it on the Live Demo page).</p>
<ol class="index-list"> 
	<li>Open the Magento admin panel and select <strong>Stores > Configuration</strong> in the side menu.<figure class="img-polaroid"><img src="img/magento/Theme-set-9.jpg" alt="" /></figure></li>
	<li>Select the "<strong>General</strong>" tab.<figure class="img-polaroid"><img src="img/magento/Theme-set-10.jpg" alt="" /></figure>		</li>
	<li>In the "<strong>General</strong>" section, select the <strong>Design</strong> tab. Choose <strong>Header</strong>, and upload your logo.
	<li>Click <strong>"Save Config"</strong> button.</li>
</ol>
<p>In order to install your company logo, you need to upload the logo image file into the <strong>/images</strong> folder of your Magento theme.</p>	