<h3>Logo configuration.</h3>
<p>Please follow these steps when setting up logo image included into the theme package (you can view it on the Live Demo page).</p>
<ol class="index-list"> 
	<li>Open the Magento admin panel and select <b>System > Configuration</b> in the top menu .</li>
	<li>Select the "<b>Design</b>" tab.</li>
	<li>In the "<b>Header</b>" section, edit the "<b>Logo Image Src</b>" field value. Change it from "<b>gif</b>" to "<b>png</b>".</li>
	<li>Click <b>"Save Config"</b> button.</li>
</ol>
<p>In order to install your company logo, you need to upload the logo image file into the <b>/images</b> folder of your Magento theme.</p>
