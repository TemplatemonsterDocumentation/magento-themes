<h3>Logo configuration.</h3>
<p>Please follow these steps when setting up logo image included into the theme package [you can view it on the Live Demo page].</p>
<ol class="index-list"> 
	<li>Open the Magento admin panel and select <strong>Content > Configuration</strong> in the side menu.<figure class="img-polaroid"><img src="img/magento/Theme-set-9.jpg" alt="" /></figure></li>
	<li>
	  <div align="justify">In the list of stores you need click <strong>edit</strong> at <strong>Global-Main Website</strong>. Also, you can click edit for a particular store view if you have them.</div>
	</li>
	
  <figure class="img-polaroid">
    <img src="img/magento/Theme-set-1-1.jpg" alt="" />
  </figure>
 
	<li>Select the "<strong>Header</strong>" tab, and upload your logo.<figure class="img-polaroid"><img src="img/magento/Theme-set-10.jpg" alt="" /></figure></li>
	<li>Click <strong>"Save Config"</strong> button.</li>
</ol>
<p align="justify">In order to install your company logo, you need to upload the logo image file into the <strong>/images</strong> folder of your Magento theme.</p>	