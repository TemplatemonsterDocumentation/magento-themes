<h3>Pages configuration</h3>
<p>Navigate to the <strong>Content > Pages</strong> section in the admin panel of you shop. </p>
<figure class="img-polaroid"> <img src="img/magento/page-settings-6.jpg" alt="" />
                                        </figure>
<p>Here you can find the list of all the store pages. The imported pages: <strong>Home page</strong>, <strong>About Us</strong>, <strong>Privacy and Cookie Policy</strong>,    <strong>404 Not Found</strong>, <strong>Enable Cookies</strong> should already be available. You need to make pages visible on your site. If you want pages be available on each store view, you need open each page settings. In order to do this, press the edit button.</p>
                                       
                                    
                                        <figure class="img-polaroid"> <img src="img/magento/page-settings-2.jpg" alt="" />
                                        </figure>
                                   
                                  
                                        <div align="justify">Navigate to the <strong>Page in Websites</strong> tab. Select <strong>All Store Views</strong> option. 
