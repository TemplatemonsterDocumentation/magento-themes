<h3>Template Configuration</h3>
<p align="justify">We are going to review template configuration options based on the example. We will configure it the same way as our Live Demo template, without affecting products data.</p>

<h4>Activate the new theme</h4>
<ol class="index-list">
	<li>Open Magento admin panel and navigate to <strong>Content > Configuration</strong> menu.
		
</li>
	<li>
	  <div align="justify">In the list of stores you need click <strong>edit</strong> at <strong>Global-Main Website</strong>. Also, you can click edit for a particular store view if you have them.
	  </div>
	
</li>	
	<li>
	  <div align="justify">In the <strong>Default Theme</strong> click <strong>Applied Theme</strong> drop-down list select the required theme, then click <strong>Save Configuration</strong> button.
	  </div>
  </li>
</ol>
