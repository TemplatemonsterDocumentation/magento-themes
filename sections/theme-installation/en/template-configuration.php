<h3>Template configuration</h3>
<p>We are going to review template configuration options based on the example. It is configured the same way as our Live Demo template, without affecting products data.</p>
<h4>Activate the new template</h4>
<ol class="index-list">
	<li>Open Magento admin panel and navigate to <b>System > Design</b> in the top menu.</li>
	<li>Click the "<b>Add Design Change</b>" button.</li>
	<li>In the "<b>Custom Design</b>" field, select the theme name (for example, theme###).</li>
	<li>Click  "<b>Save</b>"  button to confirm the changes.</li>
</ol>
