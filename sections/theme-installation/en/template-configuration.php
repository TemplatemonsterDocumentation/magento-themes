<h3>Template configuration</h3>

We will configure it the same way as in our Live Demo template, without affecting your products data.
                                The theme configuration process will be shown based on the default Magento store with the sample data.  
<p>After the theme installation, no difference will appear on your site frontend.</p>
                                 <figure class="img-polaroid">
                                    <div align=""><img src="img/magento/Theme-set-18.jpg" alt="" /></div>
                                </figure>
                                <h4 align="justify">Activate the new theme</h4>
                                
                                <div align="justify">
                                    Open Magento admin panel and navigate to the <strong>Content > Configuration</strong> menu.
                                </div>
                                <figure class="img-polaroid">
                                    <div align=""><img src="img/magento/Theme-set-1.jpg" alt="" /></div>
                                </figure>
                                <div align="justify">In the list of stores click <strong>edit</strong> in the "Action" column against the <strong>Global-Main Website</strong>. 
                                </div>
                                <figure class="img-polaroid">
                                    <div align=""><img src="img/magento/Theme-set-1-1.jpg" alt="" /></div>
                                </figure>
                                <div align="justify">In the <strong>Default Theme</strong> section select the required theme (Theme Name) in the <strong>Applied Theme</strong> option drop-down list  and, then click <strong>Save Configuration</strong> button.
                                </div>
                                <figure class="img-polaroid">
                                    <div align=""><img src="img/magento/Theme-set-2.jpg" alt="" /></div>
                                </figure>
                                <h4 align="justify">Activate the color scheme</h4>
                                In order to activate the color scheme, navigate to the <strong>Stores > Settings > Configuration</strong> section.

                             <figure class="img-polaroid">
                                    <div align=""><img src="img/magento/compare.jpg" alt="" /></div>
                                </figure>
                                <p>Then choose the <strong>TEMPLATEMONSTER > Theme Options</strong> in the left side menu and you'll move to the module settings interface.
                                <figure class="img-polaroid">
                                   <img src="img/magento/color.jpg" alt="" /></figure>
                                <p>Click on the <strong>Store View: Default Config</strong>. Select a <strong>Main Website</strong> from the option dropdown.</p>
 <figure class="img-polaroid">
                                    <div align=""><img src="img/magento/default.jpg" alt="" /></div>
                                </figure>
<p>Confirm the scope switching in the pop-up window. Press &quot;OK&quot; to confirm.</p>
<p>
  <figure class="img-polaroid"><img src="img/magento/to_23.jpg" alt="" /></figure>
</p>
<p align="justify">After that, you will see a new tab. The <strong>Theme color settings</strong> tab under the <strong>General</strong> one.</p>
<p align="justify">Go to the <strong>Theme color settings</strong> tab. Enable the color scheme. Select enable from the<strong> Color scheme status</strong> dropdown.  </p>
<figure class="img-polaroid"><img src="img/magento/scheme.jpg" alt="" /></figure>
<p>Select the desired color scheme from the <strong>Color scheme</strong> dropdown. There are four different color schemes for you to choose. </p>
<figure class="img-polaroid"><img src="img/magento/scheme-1.jpg" alt="" /></figure>
<p>Remember to <strong>save</strong> the settings.</p> 
<p>
  <figure class="img-polaroid"><img src="img/magento/scheme-2.jpg" alt="" /></figure>
</p>
