<h4>Disable cache </h4>
<p>In order to make the changes appear on your website with no delay, you need to disable cache. It prevents additional cache clearing actions after every change you make. You can enable it when you are done with store editing.</p>
<ol class="index-list">
	<li>Open Magento admin panel and navigate to <b>System > Cache Management</b> in the top menu.</li>
	<li>In the top left corner click "<b>Select All</b>".</li>
	<li>On the right side, in the "<b>Actions</b>" field select "<b>Disable</b>".</li>
	<li>Click "<b>Submit</b>" button.</li>
</ol>
