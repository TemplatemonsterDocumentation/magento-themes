<h3>Template configuration</h3>
<p>We are going to review template configuration options based on the example. It is configured the same way as our Live Demo template, without affecting products data.</p>

<h4>Activate the new template</h4>
<ol class="index-list">
	<li>Open Magento admin panel and navigate to <strong>System > Design</strong> in the top menu.<figure class="img-polaroid"><img src="img/magento/Theme-set-1.jpg" alt="" /></figure></li>
	<li>Click the "<strong>Add Design Change</strong>" button.<figure class="img-polaroid"><img src="img/magento/Theme-set-2.jpg" alt="" /></figure></li>
	<li>In the "<strong>Custom Design</strong>" field, select the theme name (for example, theme###). Click  "<strong>Save</strong>"  button to confirm the changes. <figure class="img-polaroid"><img src="img/magento/Theme-set-3.jpg" alt="" /></figure></li>
</ol>

<h4>Reindex data</h4>
<ol class="index-list">
	<li>Open the Magento admin panel and in the top menu select <strong>System > Index Management</strong>.<figure class="img-polaroid"><img src="img/magento/Theme-set-4.jpg" alt="" /></figure></li>
	<li>Check the "<strong>Select All</strong>" box in the top left corner.<figure class="img-polaroid"><img src="img/magento/Theme-set-5.jpg" alt="" /></figure></li>
	<li>In the "<strong>Actions</strong>" field select "<strong>Reindex Data</strong>".</li>
	<li>Click "<strong>Submit</strong>" button.</li>
	<li>Please be patient, this may take a while.</li>
</ol>

<h4>Disable cache </h4>
<p>In order to make the changes appear on your website with no delay, you need to disable cache. It prevents additional cache clearing actions after every change you make. You can enable it when you are done with store editing.</p>
<ol class="index-list">
	<li>Open Magento admin panel and navigate to <strong>System > Cache Management</strong> in the top menu. <figure class="img-polaroid"><img src="img/magento/Theme-set-6.jpg" alt="" /></figure></li>
	<li>In the top left corner click "<strong>Select All</strong>". <figure class="img-polaroid"><img src="img/magento/Theme-set-7.jpg" alt="" /></figure></li>
	<li>On the right side, in the "<strong>Actions</strong>" field select "<strong>Disable</strong>".</li>
	<li>Click "<strong>Submit</strong>" button.</li>
</ol>	