<h4>Reindex data</h4>
<ol class="index-list">
	<li>Open the Magento admin panel and in the top menu select <b>System > Index Management</b>.</li>
	<li>Check the "<b>Select All</b>" box in the top left corner.</li>
	<li>In the "<b>Actions</b>" field select "<b>Reindex Data</b>".</li>
	<li>Click "<b>Submit</b>" button.</li>
	<li>Please be patient, this may take a while.</li>
</ol>
