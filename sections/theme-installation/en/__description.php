<h2 class="item1">Magento Theme Installation</h2>
<div class="alert alert-danger">This theme is not compatible with Magento 1.9 engine. Refer to Live-Demo Page to check  the theme Compatibility.</div>

  <div class="notice type-info">To avoid unexpected problems after installation. Make sure your Magento engine meets the theme requirements. </div>
  <p>You can find the theme requirements on the Theme Live-Demo page.</p>
  <p>Please strictly follow the steps described in this guide.</p>
  <ul class="marked-list">
    <li><a href="https://zemez.io/magento/support/knowledge-base/step-1-backup-magento-files/" target="_blank">Step 1. Backup Magento files</a></li>
    <li><a href="https://zemez.io/magento/support/knowledge-base/step-2-disable-cache/" target="_blank">Step 2. Disable the cache</a></li>
    <li><a href="https://zemez.io/magento/support/knowledge-base/step-3-upload-app-folder/" target="_blank">Step 3. Upload the app folder</a></li>
    <li><a href="https://zemez.io/magento/support/knowledge-base/step-4-unzip-app-folder/" target="_blank">Step 4. Unzip the app folder</a></li>
    <li><a href="https://zemez.io/magento/support/knowledge-base/step-5-run-console/" target="_blank">Step 5. Run the console</a></li>
  </ul>
  <div class="alert alert-danger">
    <div class="notice type-error">Attention! Please <strong>DO NOT</strong> import sample data if you have a live website as it will completely   replace your database content, and all your website data will be <strong>LOST</strong>.</div>
  </div>
  <p>The detailed Magento update guide you will find on the <a href="http://docs.magento.com/m2/ee/user_guide/system/system-upgrade.html?Highlight=System%20Upgrade" target="_blank" rel="noopener">Magento User Guide</a>.</p>
<div class="alert alert-warning">It is recommended to install the theme on a test server before you install it on a live store.</div>

