<h4>Catalog Image Grid widget type configuration</h4>
     
<p>To configure the <strong>Catalog Image Grid </strong>widget, navigate to <strong>Content > Widgets</strong>. Click the widget title to open the setting interface.</p>
                                <P>Navigate to <strong>Widgets Options</strong> tab. Go to the <strong>Categories</strong> option. You need to select your category you want to display within the widget.</P>
                                
                                <p>Save the settings.</P>
                                <p>*Use the <strong>Layout Configuration </strong>table to find out the  widget name. </P>
                                <div class="alert alert-warning">
                                <p>Note, the selected category should have at least 2 subcategories.</p>
                                <p>Subcategories of the root category should have the <strong>Thumbmail Image</strong>.</p>
                                <p>Go to <strong>Products > Categories</strong>. Select the subcategory you want to display within the widget.  Navigate to the <strong>Catalog Images Grid</strong> tab. Upload the thumbnail image. </p>
                                <p>You should enable the <strong>Catalog Images Grid</strong> for the root category.</p>
                                </div>
