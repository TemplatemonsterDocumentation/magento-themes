<h3>Şablon ayarları</h3>
<p>Buradaki örneğe göre şablon ayarlarını inceleyeceğiz. Tıpkı bizim Demo şablonumuzdaki gibi ayarlanmıştır ve ürün verisini herhangi şekilde etkilemeyecektir.</p>
<h4>Yeni şablonu etkinleştirmek</h4>
<ol>
	<li>Magento yönetici panelini açın ve üst menüden <b>System > Design</b> (Sistem/Tasarım) bölümüne girin.</li>
	<li>Buradan "<b>Tasarım Ekle</b>" ye basın.</li>
	<li>Şimdi "<b>Özel Tasarım</b>" alanından tema ismini seçin (mesela, theme###).</li>
	<li>Ardından "<b>Kaydet</b>" e basın ve değişiklikleri kaydedin.</li>
</ol>
