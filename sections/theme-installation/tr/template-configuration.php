<h3>Şablon ayarları</h3>
<p>Buradaki örneğe göre şablon ayarlarını inceleyeceğiz. Tıpkı bizim Demo şablonumuzdaki gibi ayarlanmıştır ve ürün verisini herhangi şekilde etkilemeyecektir.</p>

<h4>Yeni şablonu etkinleştirmek</h4>
<ol class="index-list">
	<li>Magento yönetici panelini açın ve üst menüden <strong>System > Design</strong> (Sistem/Tasarım) bölümüne girin.<figure class="img-polaroid"><img src="img/magento/Theme-set-1.jpg" alt="" /></figure></li>
	<li>Buradan "<strong>Tasarım Ekle</strong>" ye basın.<figure class="img-polaroid"><img src="img/magento/Theme-set-2.jpg" alt="" /></figure></li>
	<li>Şimdi "<strong>Özel Tasarım</strong>" alanından tema ismini seçin (mesela, theme###). Ardından "<strong>Kaydet</strong>" e basın ve değişiklikleri kaydedin. <figure class="img-polaroid"><img src="img/magento/Theme-set-3.jpg" alt="" /></figure></li>	
</ol>

<h4>Veri içeriğini güncelle</h4>
<ol class="index-list">
	<li>Magento yönetici panelini açın ve üst menüden <strong>System > Index Management</strong> (Sistem/İçerik Yöneticisi) bölümüne girin. <figure class="img-polaroid"><img src="img/magento/Theme-set-4.jpg" alt="" /></figure></li>
	<li>Sol üst tarafta bulunan "<strong>Hepsini Seç</strong>" kutusuna tıklayın. <figure class="img-polaroid"><img src="img/magento/Theme-set-5.jpg" alt="" /></figure></li>
	<li>Buradaki "<strong>Eylemler</strong>" bölümünden "<strong>Reindex Data</strong>" (Veri içeriğini güncelle) ye tıklayın.</li>
	<li>Ardından "<strong>Onayla</strong>" ya basın.</li>
	<li>Bu işlem bir kaç dakika sürebilir.</li>
</ol>

<h4>Kaşeyi etkisizleştir</h4>
<p>Değişikliklerin sitenizde anında gözükmesini isterseniz, kaşeyi etkisizleştirmeniz gerekir. Bu, her değişiklikten sonra kaşeyi temizlemek için kullanılan süreyi önler. Düzenlemeniz bittikten sonra yeniden aktifleştirebilirsiniz.</p>
<ol class="index-list">
	<li>Magento yönetici paneline girin ve üst menüden <strong>System > Cache Management</strong> (Sistem/Kaşe Yöneticisi) ne tıklayın.<figure class="img-polaroid"><img src="img/magento/Theme-set-6.jpg" alt="" /></figure></li>
	<li>Sol üst köşeden "<strong>Hepsini Seç</strong>" e tıklayın. <figure class="img-polaroid"><img src="img/magento/Theme-set-7.jpg" alt="" /></figure></li>
	<li>Sağ taraftaki "<strong>Eylemler</strong>" bölümünden "<strong>Etkisizleştir</strong>" e tıklayın. </li>
	<li>Son olarak "<strong>Onayla</strong>" düğmesine basın.</li>
</ol>