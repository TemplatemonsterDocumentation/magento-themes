<h4>Veri içeriğini güncelle</h4>
<ol>
	<li>Magento yönetici panelini açın ve üst menüden <b>System > Index Management</b> (Sistem/İçerik Yöneticisi) bölümüne girin.</li>
	<li>Sol üst tarafta bulunan "<b>Hepsini Seç</b>" kutusuna tıklayın.</li>
	<li>Buradaki "<b>Eylemler</b>" bölümünden "<b>Reindex Data</b>" (Veri içeriğini güncelle) ye tıklayın.</li>
	<li>Ardından "<b>Onayla</b>" ya basın.</li>
	<li>Bu işlem bir kaç dakika sürebilir.</li>
</ol>
