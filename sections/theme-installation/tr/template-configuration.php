<h4>Kaşeyi etkisizleştir</h4>
<p>Değişikliklerin sitenizde anında gözükmesini isterseniz, kaşeyi etkisizleştirmeniz gerekir. Bu, her değişiklikten sonra kaşeyi temizlemek için kullanılan süreyi önler. Düzenlemeniz bittikten sonra yeniden aktifleştirebilirsiniz.</p>
<ol>
	<li>Magento yönetici paneline girin ve üst menüden <b>System > Cache Management</b> (Sistem/Kaşe Yöneticisi) ne tıklayın.</li>
	<li>Sol üst köşeden "<b>Hepsini Seç</b>" e tıklayın.</li>
	<li>Sağ taraftaki "<b>Eylemler</b>" bölümünden "<b>Etkisizleştir</b>" e tıklayın.</li>
	<li>Son olarak "<b>Onayla</b>" düğmesine basın.</li>
</ol>
