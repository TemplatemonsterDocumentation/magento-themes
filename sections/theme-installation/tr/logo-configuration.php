<h3>Logo düzenlemesi</h3>
<p>Tema paketindeki logo görselini (demo sayfasýndan görebilirsiniz) düzenlerken lütfen þu adýmlarý izleyin.</p>
<ol class="index-list"> 
	<li>Magento yönetici panelini açýn ve yandaki menüden <strong>Maðazalar > Ayarlar</strong> kýsmýna girin.<figure class="img-polaroid"><img src="img/magento/Theme-set-9.jpg" alt="" /></figure></li>
	<li>Buradan "<strong>Genel</strong>" sekmesini seçin.<figure class="img-polaroid"><img src="img/magento/Theme-set-10.jpg" alt="" /></figure>		</li>
	<li>"<strong>Genel</strong>" bölümünden <strong>Tasarým</strong> sekmesine girin. <strong>Header</strong> kýsmýndan logonuzu yükleyin.
	<li>Kaydetmek için <strong>"Ayarlarý Kaydet"</strong> düðmesine basýn.</li>
</ol>
<p>Þirket logonuzu koymak için, logo görselinizi Magento temanýzdaki <strong>/images</strong> klasörüne yükleyin.</p>	