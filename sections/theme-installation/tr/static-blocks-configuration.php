<h3>Sabit blok ayarları</h3>
<p>Sayfa ayarlarınızı yaptıktan sonra sabit blokları düzenlemelisiniz. Prosedür benzer: kaynak dosyalardan kodu kopyalayıp, yönetici panelinden gerekli alanlara yapıştırmalısınız. Sabit bloklar için de benzer bir tablo kullanmalısınız.</p>
<ol class="index-list"> 
	<li>Üst menüden <b>CMS > Sabit Bloklar</b> ı seçin. (İçerik Yöneticisinden)</li>
	<li>Sağ köşeden <strong>"Yeni Blok ekle"</strong> düğmesine basın. <figure class="img-polaroid"><img src="img/magento/pages-setting-5.jpg" alt="" /></figure></li>
	<li>Blok ismini ve özelliğini girin. Bu detayları şablonun Demo sayfasından görüntüleyebilirsiniz. (Sayfa Ayarları bölümüne bakın).</li>
	<li>Blok kaynak dosyasını açın ve içerik alanına HTML kodu koypalayın.</li>
</ol>
<figure class="img-polaroid"><img src="img/magento/pages-setting-6.jpg" alt="" /></figure>

<p class="alert alert-info"><span>Dikkat: bazı şablonlarda sabit blok yoktur, bu yüzden tablo boş olabilir.</span></p>
<p class="alert alert-info"><span>Sabit blok koduna sahip olan kaynak dosyaları, şablon paketinizdeki "<b>sources\demo\static_blocks\</b>" dizininde yer almaktadır. </span></p>

