<h3>Configuraci&oacute;n del logotipo</h3>
<p>Por favor, siga estos pasos cuando configure la imagen del logotipo incluido en el paquete del tema (puede verlo en la p&aacute;gina de demostraci&oacute;n).</p>
<ol> 
	<li>Abra el panel de administraci&oacute;n de Magento y seleccione <b>System > Configuration</b> en el men&uacute; superior .</li>
	<li>Seleccione la pesta&ntilde;a "<b>Design</b>".</li>
	<li>En la secci&oacute;n "<b>Header</b>", modifique el valor del campo "<b>Logo Image Src</b>". Cambie desde "<b>gif</b>" hasta "<b>png</b>".</li>
	<li>Haga clic en el bot&oacute;n <b>"Save Config"</b>.</li>
</ol>
<p>Para instalar el logotipo de su empresa, usted necesita cargar el archivo de su logotipo a la carpeta <b>/images</b> del tema de Magento.</p>
