<h3>Configuración de la plantilla</h3>
<p>Vamos a revisar las opciones de configuración de la plantilla basadas en el ejemplo. Está configurado de la misma forma que nuestra plantilla de demostración, sin afectar a los datos de productos.</p>

<h4>Active la nueva plantilla</h4>
<ol class="index-list">
	<li>Abra el panel de administración de Magento y vaya a <strong>Stores > Configuration</strong> en el menú superior.
		<figure class="img-polaroid"><img src="img/magento/Theme-set-1.jpg" alt="" /></figure>
	</li>
	<li>En la menu <strong>General</strong> seleccione la pestaña <strong>Design</strong>. Haga clic en la pestaña <strong>Design Theme</strong> y en la lista desplegable <strong>Design Theme</strong> seleccione el tema requerido, a continuación, haga clic en el botón <strong>Save Config</strong> .
		<figure class="img-polaroid"><img src="img/magento/Theme-set-2.jpg" alt="" /></figure>
	</li>	
</ol>

<h4>Reindexar los datos</h4>
<ol class="index-list">
	<li>Abra el panel de administración de Magento y en el menú superior, seleccione <strong>System > Index Management</strong>.
		<figure class="img-polaroid"><img src="img/magento/Theme-set-3.jpg" alt="" /></figure>
	</li>
	<li>En la primera columna abra la lista desplegable y elija la opción "<strong>Select All</strong>". 
		<figure class="img-polaroid"><img src="img/magento/Theme-set-4.jpg" alt="" /></figure>
	</li>
	<li>Por encima elija el campo "<strong>Actions</strong>" seleccione la opción "<strong>Update on save</strong>". Se le permitirá indexar automáticamente los datos especificados después de guardar la configuración en el panel de administración.  
		<figure class="img-polaroid"><img src="img/magento/Theme-set-5.jpg" alt="" /></figure>
	</li>
	<li>Haga clic en el botón "<strong>Submit</strong>" .</li>
	<li>Por favor sea paciente, esto puede tomar un tiempo.</li>
</ol>

<h4>Desactivar caché</h4>
<p>Para hacer que los cambios aparezcan en su sitio web sin retraso deberá desactivar la caché. Puede activarlo cuando haya terminado con la edición de tienda.</p>
<ol class="index-list">
	<li>Abra el panel de administración de Magento y vaya a  <strong>System > Cache Management</strong> en el menú superior. 
		<figure class="img-polaroid"><img src="img/magento/Theme-set-6.jpg" alt="" /></figure>
	</li>
	<li>En la primera columna abra la lista desplegable y elija la opción "<strong>Select All</strong>" . 
		<figure class="img-polaroid"><img src="img/magento/Theme-set-7.jpg" alt="" /></figure>
	</li>
	<li>Por encima elija el campo "<strong>Refresh</strong>" seleccione la opción "<strong>Disable</strong>".
		<figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
	</li>
	<li>Haga clic en el botón "<strong>Submit</strong>" .</li>
</ol>