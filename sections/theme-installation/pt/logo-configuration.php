<h3>Configuração do Logo.</h3>
<p>Siga os passos abaixo quando configurar a imagem do logo incluída no pacote do tema (você pode vê-lo na página de demonstração).</p>
<ol class="index-list"> 
	<li>Abra o painel administrativo do Magento e escolha <strong>Stores > Configuration</strong> no menu do canto.<figure class="img-polaroid"><img src="img/magento/Theme-set-9.jpg" alt="" /></figure></li>
	<li>Escolha a aba "<strong>General</strong>" (Geral).<figure class="img-polaroid"><img src="img/magento/Theme-set-10.jpg" alt="" /></figure>		</li>
	<li>Na seção "<strong>General</strong>" (Geral), escolha a aba <strong>Design</strong>. Escolha <strong>Header</strong> (Cabeçalho), e envie seu logo.
	<li>Clique no botão <strong>"Save Config"</strong>.</li>
</ol>
<p>Para instalar o logo da sua empresa, você precisa enviar a imagem para a pasta <strong>/images</strong> no seu tema do Magento.</p>	