<h3>Configurar logotipo</h3>
<p>Siga os passos abaixo para definir sua imagem de logotipo, inclusa no pacote do tema. (você pode vê-lo na página de demonstração).</p>
<ol class="index-list"> 
	<li>Abra o painel do Magento e navegue para <strong>System > Configuration</strong>.<figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure></li>
	<li>Clique na aba "<strong>Design</strong>".<figure class="img-polaroid"><img src="img/magento/Theme-set-9.jpg" alt="" /></figure></li>
	<li>Na seção "<strong>Header</strong>", altere o campo "<strong>Logo Image Src</strong>". Mude de "<strong>gif</strong>" para "<strong>png</strong>".</li>
	<li>Clique em <strong>"Save Config"</strong>.</li>
</ol>
<p>Para instalar o logotipo de sua empresa, você precisa enviar a imagem do logo para a pasta <strong>/images</strong> do seu tema do Magento.</p>	