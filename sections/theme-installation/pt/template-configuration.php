<h3>Configurar template</h3>
<p>Vamos repassar as opções de configuração do template baseadas no exemplo. Está configurado da mesma maneira que nossa demonstração, sem afetar os dados dos produtos.</p>
<h4>Ativar novo template</h4>
<ol class="index-list">
	<li>Abra o painel administrativo do Magento e navegue para <b>System > Design</b> no menu superior.</li>
	<li>Clique em "<b>Add Design Change</b>".</li>
	<li>No campo "<b>Custom Design</b>", escolha o nome do tema (por exemplo, theme###).</li>
	<li>Clique em "<b>Save</b>"para confirmar as alterações.</li>
</ol>
