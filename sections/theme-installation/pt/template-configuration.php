<h4>Reindexar dados</h4>
<ol class="index-list">
	<li>Abra o painel do Magento e no menu superior escolha <b>System > Index Management</b>.</li>
	<li>Marque a caixa "<b>Select All</b>" no topo esquerdo.</li>
	<li>No campo "<b>Actions</b>" escolha "<b>Reindex Data</b>".</li>
	<li>Clique em "<b>Submit</b>".</li>
	<li>Aguarde, esse processo pode demorar.</li>
</ol>
