<h3>Настройка логотипа.</h3>
<p>Следуйте этой инструкции для установки логотипа, предоставляемого в комплекте с темой (его видно на странице Live Demo).</p>
<ol> 
	<li>Откройте панель управления Magento и выберите в верхнем меню  <b>System > Configuration</b>.</li>
	<li>Перейдите на вкладку "<b>Design</b>".</li>
	<li>В разделе "<b>Header</b>" отредактируйте значение поля "<b>Logo Image Src</b>". Измените его с "<b>gif</b>" на "<b>png</b>".</li>
	<li>Нажмите кнопку <b>"Save Config"</b>.</li>
</ol>
<p>Для установки логотипа вашей компании следует скопировать файл логотипа в папку <b>/images</b> приобретённой вами темы Magento.</p>
