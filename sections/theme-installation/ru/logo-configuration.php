<h3>Настройка логотипа.</h3>
<p>Следуйте этой инструкции для установки логотипа, предоставляемого в комплекте с темой (его видно на странице Live Demo).</p>
<ol class="index-list"> 
	<li>
		Откройте панель управления Magento и выберите в верхнем меню  <strong>System > Configuration</strong>. 
		<figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
	</li>
	<li>
		Перейдите на вкладку "<strong>Design</strong>". 
		<figure class="img-polaroid"><img src="img/magento/Theme-set-9.jpg" alt="" /></figure>		
	</li>
	<li>
		В разделе "<strong>Header</strong>" отредактируйте значение поля "<strong>Logo Image Src</strong>". Измените его с "<strong>gif</strong>" на "<strong>png</strong>".
	</li>
	<li>
		Нажмите кнопку <strong>"Save Config"</strong>.
	</li>
</ol>
<p>Для установки логотипа вашей компании следует скопировать файл логотипа в папку <strong>/images</strong> приобретённой вами темы Magento.</p>

