<h3>Настройка логотипа.</h3>
<p>Следуйте этой инструкции для установки логотипа, предоставляемого в комплекте с темой (его видно на странице Live Demo).</p>
<ol class="index-list"> 
	<li>
		Откройте панель управления Magento и выберите в боковом меню <strong>Stores > Configuration</strong>.
		<figure class="img-polaroid"><img src="img/magento/Theme-set-9.jpg" alt="" /></figure>
	</li>
	<li>
		В меню <strong>General</strong> выберите вкладку <strong>Design</strong>. Кликните на вкладку <strong>Header</strong>, загрузите лого и нажмите кнопку <strong>Save Config</strong>.
		<figure class="img-polaroid"><img src="img/magento/Theme-set-10.jpg" alt="" /></figure>	
	</li>	
</ol>

