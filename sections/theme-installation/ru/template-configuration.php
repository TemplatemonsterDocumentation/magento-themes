<h3>Настройка шаблона</h3>
<p>Мы рассмотрим настройку шаблона на конкретном примере. Настроим его таким образом, чтобы он повторил вид темы на странице предпросмотра (Live Demo) не затрагивая базу ваших товаров.</p>
<h4>Активируйте новую тему</h4>
<ol class="index-list">
	<li>Откройте панель управления Magento и выберите в верхнем меню <b>System > Design</b>.</li>
	<li>Нажмите кнопку "<b>Add Design Change</b>".</li>
	<li>В поле "<b>Custom Design</b>" выберите название шаблона (к примеру, theme###).</li>
	<li>Нажмите кнопку "<b>Save</b>" для сохранения внесенных изменений.</li>
</ol>
