<h4>Переиндексация данных</h4>
<ol class="index-list">
	<li>Откройте панель управления Magento и выберите в верхнем меню <b>System > Index Management</b>.</li>
	<li>Выберите поле "<b>Select All</b>" в верхнем левом углу.</li>
	<li>Справа в поле "<b>Actions</b>" выберите "<b>Reindex Data</b>".</li>
	<li>Нажмите кнопку "<b>Submit</b>".</li>
	<li>Сохраняйте терпение. Эта операция может потребовать некоторое время.</li>
</ol>
