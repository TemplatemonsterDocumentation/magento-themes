<h4>Deaktivieren von der Caching</h4>
<p>Um alle vorgenommenen Änderungen sofort in Ihrem Online-Shop zu sehen, sollen Sie die Caching deaktivieren. Wenn Sie die Caching deaktivieren, müssen Sie dann nach jeder vorgenommenen Änderung den Cache nicht aktualisieren. Wir aktivieren ihn wieder, nachdem den Online-Shop eingestellt haben.</p>
<ol class="index-list">
	<li>Öffnen Sie das Control Panel Magento und wählen Sie in dem oberen Menü <b>System > Cache Management</b>.</li>
	<li>Wählen Sie das Feld "<b>Select All</b>" in der oberen rechten Ecke.</li>
	<li>Rechts im Feld "<b>Actions</b>" wählen Sie "<b>Disable</b>".</li>
	<li>Drücken Sie den Button "<b>Submit</b>".</li>
</ol>
