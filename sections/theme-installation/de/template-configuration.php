<h4>Reindizieren von Daten</h4>
<ol class="index-list">
	<li>Öffnen Sie das Control Panel Magento und wählen Sie in dem oberen Menü <b>System > Index Management</b>.</li>
	<li>Wählen Sie das Feld "<b>Select All</b>" in der rechten oberen Ecke.</li>
	<li>Rechts im Feld "<b>Actions</b>" wählen Sie "<b>Reindex Data</b>".</li>
	<li>Drücken Sie den Button "<b>Submit</b>".</li>
	<li>Haben Sie noch etwas Geduld. Dieser Prozess kann einige Zeit dauern.</li>
</ol>
