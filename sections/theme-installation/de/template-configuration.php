<h3>Einstellung der Vorlage</h3>
<p>Jetzt schauen wir an, wie man die Vorlage einstellt. Wir stellen sie so ein, damit sie wie das Thema auf der Vorschauseite (Live Demo), ohne Ihre Produkte nicht zu verletzen, aussehen kann.</p>

<h4>Aktivieren Sie das neue Thema</h4>
<ol class="index-list">
	<li>Öffnen Sie das Control Panel Magento und wählen Sie in dem oberen Menü <strong>System > Design</strong>. <figure class="img-polaroid"><img src="img/magento/Theme-set-1.jpg" alt="" /></figure></li>
	<li>Drücken Sie den Button "<strong>Add Design Change</strong>". <figure class="img-polaroid"><img src="img/magento/Theme-set-2.jpg" alt="" /></figure></li>
	<li>Im feld "<strong>Custom Design</strong>" wählen Sie den Namen der Vorlage (zum Beispiel, theme###). Drücken Sie den Button "<strong>Save</strong>", um die vorgenommenen Änderungen zu speichern. <figure class="img-polaroid"><img src="img/magento/Theme-set-3.jpg" alt="" /></figure></li>	
</ol>

<h4>Reindizieren von Daten</h4>
<ol class="index-list">
	<li>Öffnen Sie das Control Panel Magento und wählen Sie in dem oberen Menü <strong>System > Index Management</strong>.</li>
	<li>Wählen Sie das Feld "<strong>Select All</strong>" in der rechten oberen Ecke.<figure class="img-polaroid"><img src="img/magento/Theme-set-4.jpg" alt="" /></figure></li>
	<li>Rechts im Feld "<strong>Actions</strong>" wählen Sie "<strong>Reindex Data</strong>". <figure class="img-polaroid"><img src="img/magento/Theme-set-5.jpg" alt="" /></figure></li>
	<li>Drücken Sie den Button "<strong>Submit</strong>".</li>
	<li>Haben Sie noch etwas Geduld. Dieser Prozess kann einige Zeit dauern.</li>
</ol>

<h4>Deaktivieren von der Caching</h4>
<p>Um alle vorgenommenen Änderungen sofort in Ihrem Online-Shop zu sehen, sollen Sie die Caching deaktivieren. Wenn Sie die Caching deaktivieren, müssen Sie dann nach jeder vorgenommenen Änderung den Cache nicht aktualisieren. Wir aktivieren ihn wieder, nachdem den Online-Shop eingestellt haben.</p>
<ol class="index-list">
	<li>Öffnen Sie das Control Panel Magento und wählen Sie in dem oberen Menü <strong>System > Cache Management</strong>. <figure class="img-polaroid"><img src="img/magento/Theme-set-6.jpg" alt="" /></figure></li>
	<li>Wählen Sie das Feld "<strong>Select All</strong>" in der oberen rechten Ecke. <figure class="img-polaroid"><img src="img/magento/Theme-set-7.jpg" alt="" /></figure></li>
	<li>Rechts im Feld "<strong>Actions</strong>" wählen Sie "<strong>Disable</strong>".</li>
	<li>Drücken Sie den Button "<strong>Submit</strong>".</li>
</ol>