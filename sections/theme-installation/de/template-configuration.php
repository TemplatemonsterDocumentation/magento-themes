<h3>Einstellung der Vorlage</h3>
<p>Jetzt schauen wir an, wie man die Vorlage einstellt. Wir stellen sie so ein, damit sie wie das Thema auf der Vorschauseite (Live Demo), ohne Ihre Produkte nicht zu verletzen, aussehen kann.</p>
<h4>Aktivieren Sie das neue Thema</h4>
<ol class="index-list">
	<li>Öffnen Sie das Control Panel Magento und wählen Sie in dem oberen Menü <b>System > Design</b>.</li>
	<li>Drücken Sie den Button "<b>Add Design Change</b>".</li>
	<li>Im feld "<b>Custom Design</b>" wählen Sie den Namen der Vorlage (zum Beispiel, theme###).</li>
	<li>Drücken Sie den Button "<b>Save</b>", um die vorgenommenen Änderungen zu speichern.</li>
</ol>
