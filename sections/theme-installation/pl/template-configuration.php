<h3>Konfiguracja szablonu</h3>
<p>Będziemy rozpatrywać opcje konfiguracyjne szablonu na podstawie przykładu. Skonfigurujemy go tak samo jak nasz szablon Live Demo, bez wpływu na dane produktów.</p>

<h4>Aktywacja nowego motywu</h4>
<ol class="index-list">
	<li>Otwórz panel administracyjny Magento i przejdź do <strong>Sklepy > Menu Konfiguracyjne</strong>.
		<figure class="img-polaroid"><img src="img/magento/Theme-set-1.jpg" alt="" /></figure>
	</li>
	<li>W menu <strong>Ogólne</strong> wybierz kartę <strong>Projektowanie</strong>. Kliknij kartę <strong>Projektowanie Motywu</strong> oraz na liście rozwijanej <strong>Projektowanie Motywu</strong> wybierz żądany motyw, a następnie kliknij przycisk <strong>Zapisz Konfiguracje</strong>.
		<figure class="img-polaroid"><img src="img/magento/Theme-set-2.jpg" alt="" /></figure>
	</li>	
</ol>

<h4>Reindeksowanie danych</h4>
<ol class="index-list">
	<li>Otwórz panel administracyjny Magento i przejdź do <strong>System > menu Indeksu Zarządzania</strong>.
		<figure class="img-polaroid"><img src="img/magento/Theme-set-3.jpg" alt="" /></figure>
	</li>
	<li>W pierwszej kolumnie otwórz listę rozwijaną i wybierz opcję "<strong>Wybierz Wszystko</strong>". 
		<figure class="img-polaroid"><img src="img/magento/Theme-set-4.jpg" alt="" /></figure>
	</li>
	<li>W górnym selektorze "<strong>Operacje</strong>" wybierz opcję "<strong>Aktualizuj podczas zapisania</strong>". To zezwoli na automatyczne reindeksowanie wyszczególnionych danych po zapisaniu dowolnych ustawień w panelu administracyjnym.  
		<figure class="img-polaroid"><img src="img/magento/Theme-set-5.jpg" alt="" /></figure>
	</li>
	<li>Kliknij przycisk "<strong>Potwierdź</strong>".</li>
	<li>Prosimy o cierpliwość, może to chwilę potrwać.</li>
</ol>

<h4>Wyłączenie pamięci cache</h4>
<p>W celu uwidocznienia bez opóźnień zmian dokonanych na twojej stronie internetowej, należy wyłączyć pamięć cache. Zapobiega to dodatkowemu działaniu wyczyszczenia pamięci po każdej dokonanej zmianie. Można ją włączyć, kiedy skończysz z edycją sklepu.</p>
<ol class="index-list">
	<li>Otwórz panel administracyjny Magento i przejdź do <strong>System > Menu Zarządzania Cache'm</strong>. 
		<figure class="img-polaroid"><img src="img/magento/Theme-set-6.jpg" alt="" /></figure>
	</li>
	<li>W pierwszej kolumnie otwórz listę rozwijaną i wybierz opcję "<strong>Wybierz Wszystko</strong>". 
		<figure class="img-polaroid"><img src="img/magento/Theme-set-7.jpg" alt="" /></figure>
	</li>
	<li>W górnym polu "<strong>Odśwież</strong>" wybierz opcję"<strong>Wyłącz</strong>".
		<figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
	</li>
	<li>Kliknij przycisk "<strong>Potwierdź</strong>".</li>
</ol>