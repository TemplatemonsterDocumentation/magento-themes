<h3>Configurazione del modello</h3>
<p>Stiamo andando a rivedere le opzioni di configurazione del modello in base all'esempio. Noi configurare allo stesso modo come il nostro modello Demo Live, senza alterare i dati prodotti.</p>

<h4>Attivare il nuovo tema</h4>
<ol class="index-list">
	<li>Aprire il pannello di amministrazione di Magento e navigare <strong>Negozi > Configurazione (Stores > Configuration)</strong> menu.
		<figure class="img-polaroid"><img src="img/magento/Theme-set-1.jpg" alt="" /></figure>
	</li>
	<li>In <strong>Generale (General)</strong> seleziona menu <strong>Design (Design)</strong> linguetta. Clicca su  <strong>Design Tema (Design Theme)</strong> linguetta e nella <strong>Design Tema (Design Theme)</strong> drop-down lista selezionare il tema richiesto, quindi fare clic su <strong>Salva Configurazione (Save Config)</strong> pulsante.
		<figure class="img-polaroid"><img src="img/magento/Theme-set-2.jpg" alt="" /></figure>
	</li>	
</ol>

<h4>Reindicizza dati</h4>
<ol class="index-list">
	<li>Aprire il pannello di amministrazione di Magento e navigare <strong>Sistema > Gestione Index (System > Index Management)</strong> menu.
		<figure class="img-polaroid"><img src="img/magento/Theme-set-3.jpg" alt="" /></figure>
	</li>
	<li>Nella prima colonna aprire l'elenco a discesa e scegliere la "<strong>Seleziona Tutto (Select All)</strong>" opzione. 
		<figure class="img-polaroid"><img src="img/magento/Theme-set-4.jpg" alt="" /></figure>
	</li>
	<li>In alto "<strong>Azioni (Actions)</strong>" selettore di scegliere "<strong>Aggiornamento al salvataggio (Update on save)</strong>" opzione. Esso permetterà automaticamente reindicizzare i dati specificati dopo aver salvato le impostazioni nel pannello di amministrazione.  
		<figure class="img-polaroid"><img src="img/magento/Theme-set-5.jpg" alt="" /></figure>
	</li>
	<li>Click "<strong>Invio (Submit)</strong>" pulsante.</li>
	<li>Si prega di essere paziente, l'operazione potrebbe richiedere un po.</li>
</ol>

<h4>Disabilitare la cache</h4>
<p>Al fine di apportare le modifiche appaiono sul vostro sito senza alcun ritardo, è necessario disabilitare la cache. Previene ulteriori azioni della cache di compensazione dopo ogni modifica apportata. È possibile attivare quando si è fatto con la modifica negozio.</p>
<ol class="index-list">
	<li>Aprire il pannello di amministrazione di Magento e navigare <strong>Sistema > Gestione Cache (System > Cache Management)</strong> menu. 
		<figure class="img-polaroid"><img src="img/magento/Theme-set-6.jpg" alt="" /></figure>
	</li>
	<li>Nella prima colonna aprire l'elenco a discesa e scegliere la "<strong>Seleziona Tutto (Select All)</strong>" opzione. 
		<figure class="img-polaroid"><img src="img/magento/Theme-set-7.jpg" alt="" /></figure>
	</li>
	<li>In alto "<strong>Ricaricare (Refresh)</strong>" campo di selezione "<strong>Disabilitare (Disable)</strong>" opzione.
		<figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
	</li>
	<li>Click "<strong>Invio (Submit)</strong>" pulsante.</li>
</ol>