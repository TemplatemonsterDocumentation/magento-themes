<h3>Konfiguracja bloga</h3>

<p>Dostępne są trzy układy bloga. Aby wybrać jeden z nich, postępuj zgodnie z poniższymi instrukcjami:</p>
<ol class="index-list"> 
    <li>
        W górnym menu nawigacyjnym przejdź do <strong>System &gt; Konfiguracja</strong>. 
        <figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
    </li>
    <li>
        Idź do zakładki "<strong>TM Store Setting</strong>". W sekcji "<strong>Układ bloga</strong>" wybierz blog, który ci się podoba.
        <figure class="img-polaroid"><img src="img/magento/blog-settings.jpg" alt="" /></figure>        
    </li>
    <li>
        Naciśnij <strong>"Zapisz konfigurację"</strong>.
    </li>
</ol>

<p>Możesz zmieniać układ, włączając lub wyłączając panel boczny w ustawieniach modułu. Ustawienia modułu można znaleźć w sekcji <strong>Ustawienia rozszerzeń -&gt; Blog</strong>.</p>