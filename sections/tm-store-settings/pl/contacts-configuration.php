<h3>Konfiguracja strony "Kontakt"</h3>
<p>Szablon zawiera dwa warianty projektu strony "Kontakt". Aby instalować wybraną stronę, postępuj zgodnie z poniższymi instrukcjami.</p>
<ol class="index-list"> 
	<li>
		Otwórz panel administracyjny Magento, w górnym menu nawigacyjnym wybierz <strong>System &gt; Configuration</strong>. 
		<figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
	</li>
	<li>
		Idź do zakładki "<strong>TM Store Settings</strong>". 
		<figure class="img-polaroid"><img src="img/magento/store-settings-1.jpgi" alt="" /></figure>		
	</li>
	<li>
		Aby zmienić stronę "Kontakt", w sekcji "<strong>Contact Form Layout </strong>" wybierz "<strong>Layout 1 </strong>" albo "<strong>Layout 2 </strong>".
	</li>
	<li>
		Naciśnij <strong>"Save Config" ("Zapisz konfigurację")</strong>.
	</li>
</ol>