<h3>Ustawienia stopki</h3>

<p>Możesz wybrać jeden z dwóch dostępnych układów stopki.</p>
Żeby jego wybrać albo zmienić, postępuj zgodnie z następującymi krokami:
<ol class="index-list"> 
    <li>
        W górnym menu nawigacyjnym przejdź do <strong>System &gt; Konfiguracja</strong>. 
        <figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
    </li>
    <li>
        Idź do zakładki "<strong>TM Store Setting</strong>". 
        <figure class="img-polaroid"><img src="img/magento/footer-settings.jpg" alt="" /></figure>      
    </li>
    <li>
        Aby zmienić stopkę, w sekcji "<strong>Układ stopki</strong>" wybierz "<strong>Układ 1</strong>" albo "<strong>Układ 2</strong>".
    </li>
    <li>
        Naciśnij <strong>"Zapisz konfigurację"</strong>.
    </li>
</ol>

<p>Drugi układ stopki zawiera widget <strong>Blog</strong>.<br>Po wybraniu drugiego układu zobaczysz puste miejsce w stopce. Aby wyświetlić blog, postępuj zgodnie z instrukcjami o dodawaniu bloków statycznych w sekcji <strong>Instalacja szablonu Magento -&gt; Konfiguracja bloków statycznych</strong> i w tabeli <strong><a href="http://livedemo00.template-help.com/magento_53638/template-settings" target="blank_">Ustawienia szablonu</a></strong>.</p>