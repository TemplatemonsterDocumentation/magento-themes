<h3>Konfiguracja strony domowej (głównej)</h3>

<p>Możesz wybrać jeden z dwóch dostępnych układów strony.</p>

Aby skonfigurować drugi układ strony głównej, trzeba dodać bloki statyczne z folderu <strong>sources/demo/static_blocks</strong> i zastosować je za pomocą widgetów. 
<p>Więcej informacji o tym, jak dodawać bloki statyczne i stosować je za pomocą widgetów, można znaleźć w sekcji <strong>Instalacja motywu Magento -&gt; Konfiguracja bloków statycznych</strong>. Tabela <strong><a href="http://livedemo00.template-help.com/magento_53638/template-settings" target="blank_">Ustawienia szablonu</a></strong> oraz poniższy obrazek także będą korzystne.

