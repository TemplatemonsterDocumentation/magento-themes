 <h3>Konfiguracja strony "Informacje o stronie"</h3>
<p>Można wybrać jeden z dwóch dostępnych wariantów projektu "Informacji o stronie". Aby instalować wybraną stronę, postępuj zgodnie z poniższymi instrukcjami.</p>
<ol class="index-list"> 
	<li>
		W górnym menu nawigacyjnym przejdź do <strong>CMS &gt; Pages</strong>. Tutaj można znaleźć wszystkie statyczne strony sklepu.
		<figure class="img-polaroid"><img src="img/magento/About-us-1.jpg" alt="" /></figure>
	</li>
	<li>
		Żeby wymienić domyślną stronę <strong>About us </strong> na alternatywną stronę - <strong>About us 2</strong>, trzeba zmienić <strong>URL Key (klucz adresu URL)</strong> na <strong>about </strong> w trybie edycji strony <strong>About us 2</strong>.
		<figure class="img-polaroid"><img src="img/magento/About-us-2.jpg" alt="" /></figure>	
		<div class="alert alert-warning">
			<p>Przed zastosowaniem alternatywnego projektu strony zmień <strong>URL Key (klucz adresu URL)</strong> z <strong>about (o)</strong> na jakikolwiek inny w trybie edycji domyślnej strony "Informacje o stronie".</p>	
		</div>		
	</li>
	
</ol>