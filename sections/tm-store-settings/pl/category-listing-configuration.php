<h3>Ustawienia listingu kategorii</h3>

<p>Szablon zawiera dwa układy listingu produktów na stronie kategorii.</p> Aby wybrać potrzebny:
<ol class="index-list"> 
    <li>
        W górnym menu nawigacyjnym przejdź do <strong>System &gt; Konfiguracja</strong>. 
        <figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
    </li>
    <li>
        Idź do zakładki "<strong>TM Store Setting</strong>". W sekcji "<strong>Układ listingu produktów</strong>" wybierz układ, który ci się podoba.
        <figure class="img-polaroid"><img src="img/magento/category-settings.jpg" alt="" /></figure>        
    </li>
    <li>
        Naciśnij <strong>"Zapisz konfigurację"</strong>.
    </li>
</ol>
<div class="alert alert-warning">
    <p>Ta opcja pozwala zmianiać układ do wyświetlania podglądu produktów w trybie siatki.  Przegląd listingu w trybie listy pozostaje bez zmian.</p>
</div>
<p>
    Aby dodać banner na stronę kategorii w górnym menu nawigacyjnym przejdź do <strong>Katalog &gt; Menedżer Kategorii</strong>. Wybierz potrzebną kategorię i w polu <strong>Obrazek</strong> wybierz obrazek bannera.
</p>
