 <h3> Konfiguration der "Über uns" Seite</h3>
<p>Sie können eine der beiden verfügbaren Design-Varianten für die Über uns-Seite wählen. Um die gewählte Seite zu installieren, folgen Sie den Anweisungen unten.</p>
<ol class="index-list"> 
	<li>
		Gehen Sie im oberen Navigationsmenü zu<strong> CMS > Seiten </strong>. Hier finden Sie alle statischen Seiten von dem Laden.
		<figure class="img-polaroid"><img src="img/magento/About-us-1.jpg" alt="" /></figure>
	</li>
	<li>
		Um die Standartseite <strong>About us</strong> zur alternativen Seite - <strong> About us 2</strong>zu ändern, sollten Sie <strong> den URL Schlüssel </strong> von <strong> About us </strong>im Bearbeitungsmodus zur <strong>About us 2</strong> Seite zu ändern .
		<figure class="img-polaroid"><img src="img/magento/About-us-2.jpg" alt="" /></figure>	
		<div class="alert alert-warning">
			<p>Vor der Anwendung des alternativen Seitendesigns, ändern Sie den <strong> URL Schlüssel </strong> von <strong> about </strong> zu einem beliebigen im Editiermodus der Standart Über uns Seite .</p>	
		</div>		
	</li>
	
</ol>