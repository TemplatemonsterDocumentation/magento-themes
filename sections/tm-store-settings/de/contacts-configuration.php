<h3>Kontaktseite Konfiguration</h3>
<p>Die Vorlage kommt mit zwei Varianten des Kontaktseitendesigns. Um die gewählte Seite zu installieren, folgen Sie den Anweisungen unten.</p>
<ol class="index-list"> 
	<li>
		Öffnen Sie die Magento Admin-Panel, im oberen Navigationsmenü wählen Sie <strong> System > Konfiguration</strong>. 
		<figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
	</li>
	<li>
		Gehen Sie zum"<strong>TM Store Settings</strong>" Tab. 
		<figure class="img-polaroid"><img src="img/magento/store-settings-1.jpg" alt="" /></figure>		
	</li>
	<li>
		Im "<strong>Contact Form Layout</strong>"  Bereich wählen Sie "<strong> Layout 1</strong>" oder "<strong> Layout 2</strong>" um die Kontaktseite zu ändern.
	</li>
	<li>
		Klicken Sie  <strong>"Save Config"</strong>.
	</li>
</ol>