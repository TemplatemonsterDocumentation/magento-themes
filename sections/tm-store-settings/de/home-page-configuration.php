<h3>Hauptseitenkonfiguration</h3>

<p>Sie können eine der beiden verfügbaren Seitenlayouts wählen.</p>

Um das zweite Hauptseitenlayout zu konfigurieren, sollten Sie die statischen Blocks vom <strong>sources/demo/static_blocks</strong> Ordner hinzufügen und unter Verwendung von Widgets anwenden. 
<p>Um herauszufinden, wie man statische Blöcke hinzufügt und sie für Widgets benutzt, schauen Sie sich den folgenden Abschnitt an<strong>Magento theme Installation -> Staticblocks Konfiguration</strong>. Die folgenden <strong><a href="http://livedemo00.template-help.com/magento_53638/template-settings" target="blank_">Vorlageneinstellungen</a></strong> Tabelle und das nachfolgende Bild wird ebenfalls nützlich sein.

