<h3>Footer-Einstellungen</h3>

<p>Sie können eine der beiden verfügbaren footer Layouts wählen.</p>
Um einen von beiden  zu wählen oder zu ändern folgen Sie den folgenden Schritten unten:
<ol class="index-list"> 
    <li>
        Im oberen Navigationsmenü wählen Sie <strong>System > Konfiguration</strong>. 
        <figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
    </li>
    <li>
        Wählen Sie  den "<strong>TM Store Settings</strong>" Tab. 
        <figure class="img-polaroid"><img src="img/magento/footer-settings.jpg" alt="" /></figure>      
    </li>
    <li>
        In der "<strong>Footer Layout</strong>" Sektion  wählen Sie "<strong>Layout 1</strong>" oder "<strong>Layout 2</strong>" um den Footer zu ändern .
    </li>
    <li>
        Klicken Sie <strong>"Save Config"</strong>.
    </li>
</ol>

<p>Der zweite footer Layout enthält ein <strong>Blog</strong> widget.<br>Bei der Auswahl des zweiten Layouts werden Sie einen leeren Bereich in der Fußzeile sehen. Um  den Blog anzuzeigen , befolgen Sie den Anweisungen um statische Blöcke in der <strong>Magento Vorlageninstallation -> Static blocks Konfiguration</strong> Sektion hinzuzufügen und in der <strong><a href="http://livedemo00.template-help.com/magento_53638/template-settings" target="blank_">Vorlageneinstellungen</a></strong> Tabelle.</p>