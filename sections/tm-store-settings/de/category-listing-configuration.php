<h3>Kategorienauflistung Einstellungen</h3>

<p>Die Vorlage enthält zwei Themenlayouts auf der Kategorieseite.</p>Um den gewünschten auszuwählen folgen Sie diesen Schritten:
<ol class="index-list"> 
    <li>
        Im oberen Navigationsmenü gehen Sie zu <strong>System > Konfiguration</strong>. 
        <figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
    </li>
    <li>
         Gehen Sie zum"<strong>TM Store Settings</strong>" Tab. In der "<strong>Produktauflistung Layout</strong>" Sektion wählen Sie das Layout, das Sie mögen. 
        <figure class="img-polaroid"><img src="img/magento/category-settings.jpg" alt="" /></figure>        
    </li>
    <li>
        Klicken Sie <strong>"Save Config"</strong>.
    </li>
</ol>
<div class="alert alert-warning">
    <p>Diese Option ermöglicht es, das Auflistungslayout nur auf der Produktvorschau in der Rasterdarstellung zu ändern. Listenansicht-Layout hat keine Änderungen.</p>
</div>
<p>
    Um Banner zur Kategorie-Seite hinzuzufügen gehen Sie im oberen Navigationsmenü  zu  <strong>Catalog > Manage Categories</strong>. Wählen Sie die gewünschte Kategorie und im <strong> Bild </strong>feld Banner-Bild .
</p>