<h3>Blog Konfiguration</h3>

<p>Es gibt drei Blog-Layouts zur Verfügung. Um einen davon auszuwählen, gehen Sie folgendermaßen vor:</p>
<ol class="index-list"> 
    <li>
        Im oberen Navigationsmenü gehen Sie zu <strong>System > Konfiguration</strong>. 
        <figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
    </li>
    <li>
        Gehen Sie zum"<strong>TM Store Settings</strong>" Tab. In der "<strong>Blog Layout</strong>" Sektion wählen Sie das Layout, das Sie mögen.
        <figure class="img-polaroid"><img src="img/magento/blog-settings.jpg" alt="" /></figure>        
    </li>
    <li>
        Klicken Sie <strong>"Save Config"</strong>.
    </li>
</ol>

<p>Sie können das Layout ändern, indem Sie die Seitenleiste in den Moduleinstellungen aus / einschalten . Moduleinstellungen finden Sie in <strong>Extensions settings -> Blog</strong>.</p>