<h3>Anasayfa Düzenlemesi</h3>

<p>Ýki farklý sayfa düzeninden herhangi birini kullanabilirsiniz.</p>

Ýkinci anasayfa düzenini ayarlamak için <strong>sources/demo/static_blocks</strong> klasöründen sabit kutucuk eklemeli ve bileþenleri kullanarak uygulama yapmalýsýnýz. 
<p>Bileþenleri kullanarak sabit kutucuk eklemek için <strong>Magento tema kurulumu -> Sabit kutucuk ayarlarý</strong> bölümüne bakýnýz. <strong><a href="http://livedemo00.template-help.com/magento_53638/template-settings" target="blank_">Þablon ayarlarý</a></strong> tablosu ve aþaðýdaki görsel de size yardýmcý olabilir.

