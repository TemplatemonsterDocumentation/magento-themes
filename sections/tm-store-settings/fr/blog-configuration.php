<h3>Configuration de blog</h3>

<p>Vous avez trois mises en page disponibles. Pour sélectionner l'un d'eux, suivez les instructions ci-dessous:</p>
<ol class="index-list"> 
    <li>
        Dans le menu de navigation en haut allez dans <strong>System > Configuration</strong>. 
        <figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
    </li>
    <li>
        Allez dans l'onglet "<strong>TM Store Settings</strong>". Dans la section "<strong>Blog Layout</strong>" sélectionnez la disposition que vous aimez.
        <figure class="img-polaroid"><img src="img/magento/blog-settings.jpg" alt="" /></figure>        
    </li>
    <li>
        Cliquez <strong>"Save Config"</strong>.
    </li>
</ol>

<p>Vous pouvez modifier la mise en page en choisissant off/on pour la barre latérale dans les paramètres du module. Vous pouvez les paramètres de module dans <strong>Extensions settings -> Blog</strong>.</p>