<h3>Paramètres de la liste des catégories</h3>

<p>Le modèle comprend deux mises en page pour les pages avec la liste des catégories.</p> Pour choisir une mise en page désirée:
<ol class="index-list"> 
    <li>
        Dans le menu de navigation en haut allez dans <strong>System > Configuration</strong>. 
        <figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
    </li>
    <li>
        Allez dans l'onglet "<strong>TM Store Settings</strong>". Dans la section "<strong>Product Listing Layout</strong>" sélectionnez la disposition que vous aimez.
        <figure class="img-polaroid"><img src="img/magento/category-settings.jpg" alt="" /></figure>        
    </li>
    <li>
        Cliquez <strong>"Save Config"</strong>.
    </li>
</ol>
<div class="alert alert-warning">
    <p>Cette option ne permet que modifier la liste de produits pour la mise en page de grille. Mise en page de liste n'a pas de changements.</p>
</div>
<p>
    Pour ajouter une bannière pour la page de catégorie, dans le menu de navigation en haut allez dans <strong>Catalog > Manage Categories</strong>. Sélectionnez la catégorie et dans le champ <strong>Image</strong> sélectionnez une image de la bannière.
</p>
