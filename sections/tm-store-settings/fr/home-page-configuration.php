<h3>Configuration de la page d'accueil</h3>

<p>Vous pouvez choisir l'une des deux mises en page disponibles.</p>

Pour configurer la deuxième mise en page de la page d'accueil vous devez ajouter des blocs statiques depuis le dossier <strong>sources/demo/static_blocks</strong> et les appliquer en utilisant les widgets. 
<p>Pour savoir comment ajouter des blocs statiques et les appliquer en utilisant les widgets, consultez la section suivante <strong>Magento theme installation -> Static blocks configuration</strong>. Le tableau suivant <strong><a href="http://livedemo00.template-help.com/magento_53638/template-settings" target="blank_">Template settings</a></strong> et l'image ci-dessous seront également utiles.

