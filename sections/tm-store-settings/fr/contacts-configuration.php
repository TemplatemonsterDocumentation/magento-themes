<h3>Configuration de la page Contact</h3>
<p>Le modèle est livré avec deux variantes de design de la page Contact. Pour installer la page que vous voulez, suivez les instructions ci-dessous.</p>
<ol class="index-list"> 
	<li>
		Ouvrez le panneau d'administration Magento, dans la barre de menu en haut veuillez choisir <strong>System > Configuration</strong>. 
		<figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
	</li>
	<li>
		Allez à l'onglet "<strong>TM Store Settings</strong>". 
		<figure class="img-polaroid"><img src="img/magento/store-settings-1.jpg" alt="" /></figure>		
	</li>
	<li>
		Dans la section  "<strong>Contact Form Layout</strong>" choisissez "<strong>Layout 1</strong>" ou "<strong>Layout 2</strong>" pour changer la page Contacts.
	</li>
	<li>
		Cliquez  <strong>"Save Config"</strong>.
	</li>
</ol>