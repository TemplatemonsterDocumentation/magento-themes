<h3>Paramètres de pied de page</h3>

<p>Vous pouvez choisir l'une des deux disposotion de pied de page disponibles.</p>
Pour les choisir ou changer suivez les étapes suivantes:
<ol class="index-list"> 
    <li>
        Dans le menu de navigation en haut allez dans <strong>System > Configuration</strong>. 
        <figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
    </li>
    <li>
        Allez dans l'onglet "<strong>TM Store Settings</strong>". 
        <figure class="img-polaroid"><img src="img/magento/footer-settings.jpg" alt="" /></figure>      
    </li>
    <li>
        Dans la section "<strong>Footer Layout</strong>" sélectionnez "<strong>Layout 1</strong>" ou "<strong>Layout 2</strong>" pour changer le pied de page.
    </li>
    <li>
        Cliquez <strong>"Save Config"</strong>.
    </li>
</ol>

<p>La deuxième disposition de bas de page contient un widget <strong>Blog</strong>.<br>Lors du choix de deuxième mise en page, vous verrez un espace vide dans le pied de page. Pour afficher blog, suivez les instructions sur l'ajout de blocs statiques dans la section <strong>Magento template installation -> Static blocks configuration</strong> et dans le tableau <strong><a href="http://livedemo00.template-help.com/magento_53638/template-settings" target="blank_">Template settings</a></strong>.</p>