 <h3>Configuration de la page About</h3>
<p>Vous pouvez choisir l'une des deux variantes de conception disponibles pour la page About. Pour installer la page que vous voulez, suivez les instructions ci-dessous.</p>
<ol class="index-list"> 
	<li>
		Dans la barre de menu en haut, allez dans <strong>CMS > Pages</strong>. Ici vous pouvez trouver toutes les pages statiques de la boutique.
		<figure class="img-polaroid"><img src="img/magento/About-us-1.jpg" alt="" /></figure>
	</li>
	<li>
		Afin de changer la page <strong>About us</strong> par défaut avec une autre page - <strong>About us 2</strong>, vous devez changer <strong>URL Key</strong> par <strong>about</strong> dans le mode d'édition de la page <strong>About us 2</strong>.
		<figure class="img-polaroid"><img src="img/magento/About-us-2.jpg" alt="" /></figure>	
		<div class="alert alert-warning">
			<p>Avant d'appliquer la conception alternative de page, changez <strong>URL Key</strong> depuis <strong>about</strong> par tout autre dans le mode d'édition de la page About par défaut.</p>	
		</div>		
	</li>
	
</ol>