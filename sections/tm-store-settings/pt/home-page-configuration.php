<h3>Configuração da página inicial/principal (Home/Main)</h3>

<p>Você pode escolher um de dois layouts de página disponíveis.</p>

Para configurar a segunda página principal do layout você precisa incluir blocos estáticos da pasta <strong>sources/demo/static_blocks</strong> e aplicá-los usando widgets. 
<p>Para descobrir como incluir blocos estáticos e aplicá-los usando widgets, dê uma olhada na seção <strong>Magento theme installation -> Static blocks configuration</strong>. A seguinte tabela de <strong><a href="http://livedemo00.template-help.com/magento_53638/template-settings" target="blank_">definições de Template</a></strong> e a imagem também serão úteis.

