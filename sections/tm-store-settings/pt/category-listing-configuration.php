<h3>Definições da listagem de categoriaCategory listing settings</h3>

<p>O template possui das oções de layout para a listagem de produtos na página da categoria.</p> Para escolher uma:
<ol class="index-list"> 
    <li>
        Na navegação superior vá para <strong>System > Configuration</strong>. 
        <figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
    </li>
    <li>
        Vá para a aba "<strong>TM Store Settings</strong>". Na seção "<strong>Product Listing Layout</strong>" escolha o layout desejado.
        <figure class="img-polaroid"><img src="img/magento/category-settings.jpg" alt="" /></figure>        
    </li>
    <li>
        Clique em <strong>"Save Config"</strong>.
    </li>
</ol>
<div class="alert alert-warning">
    <p>Esta opções permite mudar o layout da listagem apenas na visualização de produto na lista em grade. O layout da lista comum não muda.</p>
</div>
<p>
    Para incluir um banner em uma categoria, no menu superior vá para <strong>Catalog > Manage Categories</strong>. Escolha a categoria desejada e, no campo <strong>Image</strong>, escolha a imagem do banner.
</p>
