<h3>Configuração do Blog</h3>

<p>Exitem três layouts disponíveis para o blog. Para escolher um deles, siga as instruções abaixo:</p>
<ol class="index-list"> 
    <li>
        Na navegação superior vá para <strong>System > Configuration</strong>. 
        <figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
    </li>
    <li>
        Entre na aba "<strong>TM Store Settings</strong>". Na seção "<strong>Blog Layout</strong>" escolha o layout desejado.
        <figure class="img-polaroid"><img src="img/magento/blog-settings.jpg" alt="" /></figure>        
    </li>
    <li>
        Clique em <strong>"Save Config"</strong>.
    </li>
</ol>

<p>Você pode alterar o layout ligando e desligando a barra lateral nas definições de módulo. Essas definições podem ser encontradas em <strong>Extensions settings -> Blog</strong>.</p>