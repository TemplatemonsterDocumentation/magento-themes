<h3>Product list gallery</h3>
<p class="indent">Este módulo permite que você exiba as outras imagens do produto junto da imagem principal. No tema padrão, miniaturas são exibidas abaixo da imagem principal do produto, mas em nossos temas, as imagens são exibidas de acordo com o layout do PSD.</p>
<p>Esse módulo pode ser ativado/desativado no painel Administrativo do Magento, em <strong>System > Configuration > Templatemonster > Product List Gallery</strong>.</p>
<figure class="img-polaroid"><img src="img/magento/productgallery.jpg" alt="" /></figure>
<p>O módulo inclui 4 seções de configuração: <strong>List View</strong>, <strong>Grid View</strong>, <strong>Homepage List View</strong>, e <strong>Homepage Grid View</strong>.<br>
Em <strong>Homepage List View</strong> e <strong>Homepage Grid View</strong> você pode configurar a exibição da galeria na página principal da sua loja. As duas outras seções ajudam a configurar as galerias na página de categorias.</p>
<p>Todas as seções contém as mesmas configurações:</p>			
<table class="options-table">
	<tr>
		<td class="col-1"><strong>Enable/Disable</strong></td>
		<td>-</td>
		<td>você pode ativar/desativar o módulo.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Image width</strong></td>
		<td>-</td>
		<td>para definir a largura da imagem principal do produto. </td>
	</tr>
	<tr>
		<td class="col-1"><strong>Image height</strong></td>
		<td>-</td>
		<td>para definir a altura da imagem principal do produto. </td>
	</tr>
	<tr>
		<td class="col-1"><strong>Thumbnail width</strong></td>
		<td>-</td>
		<td>para definir a largura das imagens extras do produto.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Thumbnail height</strong></td>
		<td>-</td>
		<td>para definir a altura das imagens extras do produto.</td>
	</tr>
</table>
<p>Aliás! A largura máxima do imagem é igual ao tamanho da coluna onde ela foi colocada.</p>
		