 <h3>Sobre a configuração das páginas</h3>
<p>Você pode escolher uma das duas variações de design para a página "About us". Para instalar a página que deseja, siga as instruções abaixo.</p>
<ol class="index-list"> 
	<li>
		No menu de navegação do topo, vá para <strong>CMS > Pages</strong>. Aqui você pode encontrar todas as páginas estáticas da loja.
		<figure class="img-polaroid"><img src="img/magento/About-us-1.jpg" alt="" /></figure>
	</li>
	<li>
		Para mudar a página padrão de <strong>About us</strong> para página alternativa - <strong>About us 2</strong>, você precisa mudar o campo <strong>URL Key</strong> para <strong>about</strong> no modo de edição da página <strong>About us 2</strong>.
		<figure class="img-polaroid"><img src="img/magento/About-us-2.jpg" alt="" /></figure>	
		<div class="alert alert-warning">
			<p>Antes de aplicar o design alternativo da página, mude o campo <strong>URL Key</strong> da página <strong>about</strong> para qualquer outra no modo de edição da página About padrão.</p>	
		</div>		
	</li>
	
</ol>