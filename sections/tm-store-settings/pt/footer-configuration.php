<h3>Definições de Rodapé</h3>

<p>Você pode escolhere um dos dois layouts de rodapé disponíveis.</p>
Para alterar ou decidir por um deles siga os passos abaixo:
<ol class="index-list"> 
    <li>
        Na navegação superior vá para  <strong>System > Configuration</strong>. 
        <figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
    </li>
    <li>
        Entre na aba "<strong>TM Store Settings</strong>". 
        <figure class="img-polaroid"><img src="img/magento/footer-settings.jpg" alt="" /></figure>      
    </li>
    <li>
        Na seção "<strong>Footer Layout</strong>" decida entre "<strong>Layout 1</strong>" ou "<strong>Layout 2</strong>" para alterar o rodapé.
    </li>
    <li>
        Clique em <strong>"Save Config"</strong>.
    </li>
</ol>

<p>O segundo layout de rodapé contém um widget de <strong>Blog</strong>.<br>Quando escolher o segundo layout, você verá um espaço vazio no rodapé. Para mostrar o blog, siga as instruções de como incluir blocos estáticos em <strong>Magento template installation -> Static blocks configuration</strong> e também na seção <strong><a href="http://livedemo00.template-help.com/magento_53638/template-settings" target="blank_">Template settings</a></strong>.</p>