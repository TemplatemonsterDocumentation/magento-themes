<h3>Configuração da página de contatos</h3>
<p>O template conta com duas variações da página de contatos (Contact page). Para instalar a página desejada, siga as instruções abaixo.</p>
<ol class="index-list"> 
	<li>
		Abra o painel administrativo do Magento, na navegação do topo escolha <strong>System > Configuration</strong>. 
		<figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
	</li>
	<li>
		Vá para a aba "<strong>TM Store Settings</strong>". 
		<figure class="img-polaroid"><img src="img/magento/store-settings-1.jpg" alt="" /></figure>		
	</li>
	<li>
		Na seção "<strong>Contact Form Layout</strong>"  escolha entre "<strong>Layout 1</strong>" ou "<strong>Layout 2</strong>" para mudar a página de contatos.
	</li>
	<li>
		Clique em <strong>"Save Config"</strong>.
	</li>
</ol>