<h3>Настройка футера</h3>

<p>Мы предлагаем вам 3 макета футера для страницы.</p>
Чтобы выбрать или изменить его:
<ol class="index-list"> 
    <li>
        Откройте админ-панель Magento и выберите в верхнем меню  <strong>System > Configuration</strong>. 
        <figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
    </li>
    <li>
        Перейдите на вкладку "<strong>TM Store Settings</strong>". 
        <figure class="img-polaroid"><img src="img/magento/footer-settings.jpg" alt="" /></figure>      
    </li>
    <li>
        В разделе "<strong>Footer Layout</strong>" выберите "<strong>Layout 1</strong>" или "<strong>Layout 2</strong>" для изменения макета футера.
    </li>
    <li>
        Нажмите кнопку <strong>"Save Config"</strong>.
    </li>
</ol>

<p>Второй макет футера содержит виджет <strong>Blog</strong>.<br>При выборе второго макета в футере сайта образуется пустое пространство. Чтобы поместить в него виджет блога, вы должны следовать инструкциям по добавлению статических блоков раздела <strong>Установка темы Magento -> Настройка статических блоков</strong> и таблице <strong><a href="http://livedemo00.template-help.com/magento_53638/template-settings" target="blank_">Template settings</a></strong>.</p>