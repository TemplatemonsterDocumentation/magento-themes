<h3>Настройка футера</h3>

<p>Мы предлагаем вам 3 леяута футера для страницы.</p>
Чтобы изменить его:
<ol class="index-list"> 
	<li>
		Откройте панель управления Magento и выберите в верхнем меню  <strong>System > Configuration</strong>. 
		<figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
	</li>
	<li>
		Перейдите на вкладку "<strong>TM Store Settings</strong>". 
		<figure class="img-polaroid"><img src="img/magento/footer-settings.jpg" alt="" /></figure>		
	</li>
	<li>
		В разделе "<strong>Footer Layout</strong>" выберите "<strong>Layout X</strong>" для изменения футера.
	</li>
	<li>
		Нажмите кнопку <strong>"Save Config"</strong>.
	</li>
</ol>

<p>Второй леяут футера содержит виджет <strong>Blog</strong>.<br>При выборе второго леяута у вас в футере будет пустое место. Чтобы вывести туда блог, вы должны следовать инструкциям по добавлению статических блоков в разделе <strong>Установка темы Magento -> Настройка статических блоков</strong> и табличке <strong><a href="http://livedemo00.template-help.com/magento_53638/template-settings" target="blank_">Template settings</a></strong>.</p>