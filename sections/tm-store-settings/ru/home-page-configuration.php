<h3>Настройка домашней (главной) страницы</h3>

<p>Мы предлагаем вам 4 леяута главной страницы.</p>

Для настройки макета главной страницы вам необходимо добавить статические блоки с папки <strong>sources/demo/static_blocks</strong> и вывести их с помощью виджетов. 
<p>Информацию о том, как добавить статические блоки и вывести их в нужной позиции темы, вы можете прочесть в разделе <strong>Установка темы Magento -> Настройка статических блоков</strong>. В этом вам так же поможет таблица <strong><a href="http://livedemo00.template-help.com/magento_53638/template-settings" target="blank_">Template settings</a></strong> и рисунок, представленный ниже.

