<h3>Настройка домашней (главной) страницы</h3>

<p>Мы предлагаем вам два леяута главной страницы.</p>

Для настройки леяута главной страницы вам необходимо добавить статик блоки с папки <strong>sources -> demo -> static_blocks</strong> и вывести их виджетами. 
<p>Как добавить статик блоки и вывести их в нужном месте, вы можете прочесть в разделе <strong>Установка темы Magento -> Настройка статических блоков</strong>. В этом вам так же поможет табличка <strong><a href="http://livedemo00.template-help.com/magento_53638/template-settings" target="blank_">Template settings</a></strong> и рисунки, представленные ниже.

