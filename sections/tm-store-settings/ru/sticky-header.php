<h3>Залипающий хедер</h3>
<p>У вас есть возможность включить/выключить залипание хедера при скроле.<br>
Для включения/отключения залипающего хедера вам необходимо перейти в меню <strong>TM Store settings -> Settings</strong>. В секции <strong>Sticky Header</strong> вы можете включить/отключить залипание хедера. </p>
