<h3>Настройка блога</h3>

<p>Мы предлагаем вам 4 варианта леяута для блога. Чтобы выбрать один из них, следуйте инструкциям:</p>
<ol class="index-list"> 
	<li>
		Откройте панель управления Magento и выберите в верхнем меню  <strong>System > Configuration</strong>. 
		<figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
	</li>
	<li>
		Перейдите на вкладку "<strong>TM Store Settings</strong>". В разделе "<strong>Blog Layout</strong>" выберите тот леяут, который вам больше нравится.
		<figure class="img-polaroid"><img src="img/magento/blog-settings.jpg" alt="" /></figure>		
	</li>
	<li>
		Нажмите кнопку <strong>"Save Config"</strong>.
	</li>
</ol>

<p>Изменить леяут можно, включив или выключив сайдбар в настройках самого модуля. Настройки модуля вы найдёте в разделе <strong>Настройка сторонних расширений -> Блог</strong>.</p>