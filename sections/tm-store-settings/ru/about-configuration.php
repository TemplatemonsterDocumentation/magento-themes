<h3>Настройка страницы About</h3>
<p>В шаблоне вам на выбор предлагается два варианта дизайна страницы About. Чтобы установить страницу, которая вам понравилась, следуйте следующим инструкциям.</p>
<ol class="index-list"> 
	<li>
		В верхнем меню выберите <strong>CMS > Pages</strong>. Здесь находятся все статические страницы магазина.
		<figure class="img-polaroid"><img src="img/magento/About-us-1.jpg" alt="" /></figure>
	</li>
	<li>
		Чтобы изменить страницу <strong>About us</strong> используемую по умолчанию на страницу с альтернативным дизайном - <strong>About us 2</strong>, вам необходимо сменить <strong>URL Key</strong> на <strong>about</strong> зайдя в режим редактирования страницы <strong>About us 2</strong>.
		<figure class="img-polaroid"><img src="img/magento/About-us-2.jpg" alt="" /></figure>	
		<div class="alert alert-warning">
			<p>Переде тем, как применить альтернативный дизайн страницы, измените <strong>URL Key</strong> с <strong>about</strong> на любой другой для стандартной страницы About.</p>	
		</div>		
	</li>
	
</ol>