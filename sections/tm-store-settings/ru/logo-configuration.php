<h3>Настройка логотипа</h3>

<p>В данной теме реализован функционал загрузки логотипа через админ панель.<br>
Для замены логотипа вам необходимо перейти в меню <strong>TM Store settings -> Settings</strong>. В секции <strong>Custom logo</strong> вы можете включить модуль и загрузить свой логотип в форматах <strong>JPG, GIF, PNG</strong>. Вы так же можете указать альтерантивный текст для логотипа.</p>

<div class="alert alert-warning">
	<span>
		Обратите внимание, что в случае отключения кастомного лого он измениться на стандартный лого темы. 
	</span>
</div>

