<h3>Настройка логотипа</h3>

<p>В данной теме реализован функционал загрузки логотипа через админ панель.<br>
Для замены логотипа вам необходимо перейти в меню <strong>TM Store settings -> Settings</strong>. В секции <strong>Custom logo</strong> вы можете включить модуль и загрузить свой логотип в форматах <strong>JPG, GIF, PNG</strong>. Вы так же можете указать альтернативный текст для логотипа.</p>
<div class="alert alert-warning">
	<span>
		Обратите внимание, что в случае отключения кастомного лого он измениться на стандартный лого темы. 
	</span>
</div>
<p>В модуле реализована возможность изменения логотипа посредством текстового ввода.</p> Для этого в поле <strong>Logo Type</strong> выберите соответствующее значение. И в появившемся поле <strong>Logo Text</strong> введите текст для логотипа.
<div class="alert alert-warning">
	<span>
		Внимание! Поле ввода <strong>Logo Text</strong> поддерживает разметку HTML. Если вы хотите навешать стили, вы можете смело использовать HTML-структуру. Например: "&lt;span&gt;Tm&lt;/span&gt; Magento."
	</span>
</div>



