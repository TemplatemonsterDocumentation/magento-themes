<h3>Настройка листинга категорий</h3>

<p>В шаблоне реализовано 3 леяута листинга продуктов на странице категорий.</p> Чтобы выбрать нужный:
<ol class="index-list"> 
	<li>
		Откройте панель управления Magento и выберите в верхнем меню  <strong>System > Configuration</strong>. 
		<figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
	</li>
	<li>
		Перейдите на вкладку "<strong>TM Store Settings</strong>". В разделе "<strong>Product Listing Layout</strong>" выберите тот леяут, который вам больше нравится.
		<figure class="img-polaroid"><img src="img/magento/category-settings.jpg" alt="" /></figure>		
	</li>
	<li>
		Нажмите кнопку <strong>"Save Config"</strong>.
	</li>
</ol>
<div class="alert alert-warning">
    <p>Данная опция меняет леяут листинга только на привью продуктов в виде сетки. Леуят в виде списка остаётся неизменным.</p>
</div>
<p>
	Для добавления баннера на страницу категории выберите в меню <strong>Catalog > Manage Categories</strong>. Виберите нужную вам категорию и в поле <strong>Image</strong> вы можете выбрать нужный вам баннер.
</p>
