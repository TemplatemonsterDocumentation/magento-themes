<h3>Настройка страницы контактов</h3>
<p>В шаблоне вам на выбор предлагается два варианта дизайна страницы контактов. Чтобы установить страницу, которая вам понравилась, следуйте следующим инструкциям.</p>
<ol class="index-list"> 
	<li>
		Откройте панель управления Magento и выберите в верхнем меню  <strong>System > Configuration</strong>. 
		<figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
	</li>
	<li>
		Перейдите на вкладку "<strong>TM Store Settings</strong>". 
		<figure class="img-polaroid"><img src="img/magento/store-settings-1.jpg" alt="" /></figure>		
	</li>
	<li>
		В разделе "<strong>Contact Form Layout</strong>" выберите "<strong>Layout 1</strong>" или "<strong>Layout 2</strong>" для изменения страницы контактов.
	</li>
	<li>
		Нажмите кнопку <strong>"Save Config"</strong>.
	</li>
</ol>