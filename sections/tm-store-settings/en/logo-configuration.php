<h3>Logo configuration</h3>

<p>This theme includes functionality for logo uploading via admin panel.<br>
In order to change logo you should navigate to <strong>TM Store settings -> Settings</strong> menu. At <strong>Custom logo</strong> section you will be able to enable module and upload your logo as <strong>JPG, GIF, PNG</strong> image file. You can also specify alternative text for logo.</p>

<div class="alert alert-warning">
    <span>
        Please note, in case custom logo is disabled, theme default logo will be displayed. 
    </span>
</div>

