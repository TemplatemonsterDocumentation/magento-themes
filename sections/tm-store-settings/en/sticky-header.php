<h3>Sticky header</h3>
<p>You have the ability to enable/disable sticky header on scroll.<br>
To enable/disable sticky header, you need to navigate to the <strong>TM Store settings -> Settings</strong> tab. In <strong>Sticky Header</strong> section you can enable/disable sticky header. </p>
