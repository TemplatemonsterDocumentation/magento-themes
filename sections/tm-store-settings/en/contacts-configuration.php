<h3>Contact page configuration</h3>
<p>The template comes with two variations of Contact page design. In order to install the page you like, follow the instructions below.</p>
<ol class="index-list"> 
	<li>
		Open Magento administration panel, in the top navigation menu select <strong>System > Configuration</strong>. 
		<figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
	</li>
	<li>
		Go to "<strong>TM Store Settings</strong>" tab. 
		<figure class="img-polaroid"><img src="img/magento/store-settings-1.jpg" alt="" /></figure>		
	</li>
	<li>
		In the "<strong>Contact Form Layout</strong>"  section select "<strong>Layout 1</strong>" or "<strong>Layout 2</strong>" to change Contacts page.
	</li>
	<li>
		Click  <strong>"Save Config"</strong>.
	</li>
</ol>