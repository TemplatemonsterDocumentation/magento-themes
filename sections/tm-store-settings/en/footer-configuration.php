<h3>Footer settings</h3>

<p>You can choose one of the three available footer layouts</p>
In order to switch it:
<ol class="index-list"> 
    <li>
        In the top navigation menu go to <strong>System > Configuration</strong>. 
        <figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
    </li>
    <li>
        Go to "<strong>TM Store Settings</strong>" tab. 
        <figure class="img-polaroid"><img src="img/magento/footer-settings.jpg" alt="" /></figure>      
    </li>
    <li>
        In the "<strong>Footer Layout</strong>" section select "<strong>Layout 1</strong>" or "<strong>Layout 2</strong>" in order to change footer.
    </li>
    <li>
        Click <strong>"Save Config"</strong>.
    </li>
</ol>

<p>Second footer layout contains <strong>Blog</strong> widget.<br> When choosinbg second layout you will see empty space in the footer. In order to display blog, follow the instructions on adding static blocks in the <strong>Magento template installation -> Static blocks configuration</strong> section and in the <strong><a href="http://livedemo00.template-help.com/magento_53638/template-settings" target="blank_">Template settings</a></strong> table.</p>