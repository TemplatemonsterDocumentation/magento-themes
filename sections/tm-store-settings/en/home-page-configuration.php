<h3>Home (Main) Page Configuration</h3>

<p>You can select one of the 4 available page layouts.</p>

In order to configure the main page layout you need to add static blocks from the <strong>sources/demo/static_blocks</strong> folder and apply them using widgets. 
<p>To find out how to add static blocks and apply them using widgets, check the following section <strong>Magento theme installation -> Static blocks configuration</strong>. <strong><a href="http://livedemo00.template-help.com/magento_53638/template-settings" target="blank_">Template settings</a></strong> table and images below will also help you with this.

