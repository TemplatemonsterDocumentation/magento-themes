 <h3>About page configuration</h3>
<p>You can choose one of the two available design variations for About page. In order to install the page you like, follow the instructions below.</p>
<ol class="index-list"> 
	<li>
		In the top navigation menu go to <strong>CMS > Pages</strong>. Here you can find all the static pages of the store.
		<figure class="img-polaroid"><img src="img/magento/About-us-1.jpg" alt="" /></figure>
	</li>
	<li>
		In order to change default <strong>About us</strong> page with the alternative page - <strong>About us 2</strong>, you need to change <strong>URL Key</strong> to <strong>about</strong> in editing mode of the <strong>About us 2</strong> page.
		<figure class="img-polaroid"><img src="img/magento/About-us-2.jpg" alt="" /></figure>	
		<div class="alert alert-warning">
			<p>Before applying alternative page design, change <strong>URL Key</strong> from <strong>about</strong> to any other in the editing mode of default About page.</p>	
		</div>		
	</li>
	
</ol>