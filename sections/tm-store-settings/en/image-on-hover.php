<h3>Changing product image on hover.</h3>
<p>First, you need to add <strong>image_hover</strong> attribute.</p>
<ol class="index-list">
	<li>
		Navigate to <strong>Catalog -> Attributes -> Manage Attributes</strong> and hit <strong>Add New Attribute</strong> button.<a class="pretty_image" href="img/magento/image-on-hover-1.jpg"><figure class="img-polaroid"><img src="img/magento/image-on-hover-1.jpg" alt="" /></figure></a>
	</li>
	<li>
		In the <strong>Attribute Code</strong> field enter <strong>image_hover</strong> value.<br>
		Also, in the <strong>Catalog Input Type for Store Owner</strong> field choose the <strong>Media Image</strong> option.
		<a class="pretty_image" href="img/magento/image-on-hover-2.jpg"><figure class="img-polaroid"><img src="img/magento/image-on-hover-2.jpg" alt="" /></figure></a>
	</li>
	<li>
		Open the <strong>Manage Label / Options</strong> tab on the left and enter any value (for example: Hover image) in <strong>Admin</strong> field.
		<a class="pretty_image" href="img/magento/image-on-hover-3.jpg"><figure class="img-polaroid"><img src="img/magento/image-on-hover-3.jpg" alt="" /></figure></a>
	</li>
</ol>

<p>You can enable the image change on hover for each product individually. In order to do this, you need to open a product and navigate to <strong>Images</strong> tab. In <strong>Hover image</strong> column select a picture you want to use. </p>
<div class="alert alert-warning">
	<span>
		Note that this functionality does not work for products with Configurable Swatches.
	</span>
</div>
<a class="pretty_image" href="img/magento/image-on-hover.jpg"><figure class="img-polaroid"><img src="img/magento/image-on-hover.jpg" alt="" /></figure></a>
<p>
	After saving the changes you need to navigate to <strong>TM Store settings -> Settings</strong> tab. In <strong>Grid View</strong> and <strong>Homepage Grid View</strong> sections select the <strong>Hover images</strong> option in the <strong>Type of the product's image preview:</strong> field.
</p>