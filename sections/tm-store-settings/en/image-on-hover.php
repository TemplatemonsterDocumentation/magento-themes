<h3>Product image change on hover</h3>

<p>You can enable product image change on hover for each separate product. In order to do this, open any product and navigate to <strong>Images</strong> tab. Check the required image at <strong>Hover image</strong> column  then. </p>
<div class="alert alert-warning">
    <span>
        Please note, this functionality is not available for products with Configurable Swatches.
    </span>
</div>
