<h3>Configuración de Contact page</h3>
<p>La plantilla viene con dos variaciones de diseño de Contacts page. Para instalar la página que le gusta, siga las siguientes instrucciones.</p>
<ol class="index-list"> 
	<li>
		Abra la panel de administración de Magento, en el menú de navegación superior, seleccione <strong>System > Configuration</strong>. 
		<figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
	</li>
	<li>
		Vaya a la pestaña "<strong>TM Store Settings</strong>". 
		<figure class="img-polaroid"><img src="img/magento/store-settings-1.jpg" alt="" /></figure>		
	</li>
	<li>
		En "<strong>Contact Form Layout</strong>" seleccione la sección "<strong>Layout 1</strong>" o "<strong>Layout 2</strong>" para cambiar Contacts page.
	</li>
	<li>
		Haga clic en <strong>"Save Config"</strong>.
	</li>
</ol>