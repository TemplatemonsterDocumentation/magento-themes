<h3>Configuraci&oacute;n de la plantilla</h3>
<p>Vamos a revisar las opciones de configuraci&oacute;n de la plantilla basadas en el ejemplo. Est&aacute; configurado de la misma forma que nuestra plantilla de demostraci&oacute;n, sin afectar a los datos de productos.</p>
<h4>Active la nueva plantilla</h4>
<ol class="index-list">
	<li>Abra el panel de administraci&oacute;n de Magento y vaya a <strong>System > Design</strong> en el men&uacute; superior.<figure class="img-polaroid"><img src="img/magento/Theme-set-1.jpg" alt="" /></figure></li>
	<li>Haga clic en el bot&oacute;n "<strong>Add Design Change</strong>".<figure class="img-polaroid"><img src="img/magento/Theme-set-2.jpg" alt="" /></figure></li>
	<li>En el campo "<strong>Custom Design</strong>", seleccione el nombre del tema (por ejemplo, theme###). Haga clic en el bot&oacute;n "<strong>Save</strong>" para aplicar los cambios.<figure class="img-polaroid"><img src="img/magento/Theme-set-3.jpg" alt="" /></figure></li>	
</ol>

<h4>Reindexar los datos</h4>
<ol class="index-list">
	<li>Abra el panel de administraci&oacute;n de Magento y en el men&uacute; superior, seleccione <strong>System > Index Management</strong>.<figure class="img-polaroid"><img src="img/magento/Theme-set-4.jpg" alt="" /></figure></li>
	<li>Marque la casilla "<strong>Select All</strong>" en la esquina superior izquierda.<figure class="img-polaroid"><img src="img/magento/Theme-set-5.jpg" alt="" /></figure></li>
	<li>En el campo "<strong>Actions</strong>" seleccione "<strong>Reindex Data</strong>".</li>
	<li>Haga clic en el bot&oacute;n "<strong>Submit</strong>".</li>
	<li>Por favor, sea paciente, esto puede tomar un tiempo.</li>
</ol>

<h4>Desactivar cach&eacute;</h4>
<p>Para hacer que los cambios aparezcan en su sitio web sin retraso deber&aacute; desactivar la cach&eacute;. Puede activarlo cuando haya terminado con la edici&oacute;n de tienda.</p>
<ol class="index-list">
	<li>Abra el panel de administraci&oacute;n de Magento y vaya a <strong>System > Cache Management</strong> en el men&uacute; superior.<figure class="img-polaroid"><img src="img/magento/Theme-set-6.jpg" alt="" /></figure></li>
	<li>En la parte superior izquierda esquina haga clic en "<strong>Select All</strong>".<figure class="img-polaroid"><img src="img/magento/Theme-set-7.jpg" alt="" /></figure></li>
	<li>En el lado derecho, en el campo "<strong>Actions</strong>" seleccione "<strong>Disable</strong>".</li>
	<li>Haga clic en el bot&oacute;n "<strong>Submit</strong>".</li>
</ol>