<h3>Configuración de la página Principal</h3>

<p>Puede seleccionar uno de los dos diseños disponibles de página.</p>

Con el fin de configurar el segundo diseño de la página principal es necesario agregar bloques estáticos de la carpeta <strong>sources/demo/static_blocks</strong> y aplicar el tema utilizando widgets. 
<p>Para saber cómo agregar bloques estáticos y aplicarlos usando widgets, compruebe la siguiente sección <strong>Instalación de la plantilla de Magento -> Configuración de bloques estáticos</strong>. Seguimiento <strong><a href="http://livedemo00.template-help.com/magento_53638/template-settings" target="blank_">Ajustes de la plantilla</a></strong> tabla y la imagen de abajo también serán útiles.

