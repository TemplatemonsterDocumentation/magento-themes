 <h3>Configuración de la página About</h3>
<p>Usted puede elegir una de las dos variaciones de diseño disponibles para la página About. Para instalar la página que le gusta, siga las siguientes instrucciones.</p>
<ol class="index-list"> 
	<li>
		En el menú superior de la navegación <strong>CMS > Páginas</strong>. Aquí puede encontrar todas las páginas estáticas de la tienda.
		<figure class="img-polaroid"><img src="img/magento/About-us-1.jpg" alt="" /></figure>
	</li>
	<li>
		Con el fin de cambiar la página por defecto <strong>About us</strong> por la página alternativa - <strong>About us 2</strong>, necesita cambiar <strong>URL Key</strong> por <strong>about</strong> en el modo de edición de la página <strong>About us 2</strong>.
		<figure class="img-polaroid"><img src="img/magento/About-us-2.jpg" alt="" /></figure>	
		<div class="alert alert-warning">
			<p>Antes de aplicar el diseño alternativo de página, cambie <strong>URL Key</strong> por <strong>about</strong> a cualquier otro modo de edición de incumplimiento de la página About.</p>	
		</div>		
	</li>
	
</ol>