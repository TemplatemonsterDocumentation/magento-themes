<h3>Ajustes de Categoría del listado</h3>

<p>La plantilla incluye dos listas de diseños en la página de categoría.</p> Con el fin de seleccionar el requerido:
<ol class="index-list"> 
    <li>
        En el menú superior de navegación vaya a <strong>Sistema > Configuración</strong>. 
        <figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
    </li>
    <li>
        Vaya a la pestaña "<strong>TM Store Settings</strong>". En la sección "<strong>Product Listing Layout</strong>" de seleccionar el diseño que más le guste.
        <figure class="img-polaroid"><img src="img/magento/category-settings.jpg" alt="" /></figure>        
    </li>
    <li>
        Haga clic en <strong>"Guardar Configuraciones"</strong>.
    </li>
</ol>
<div class="alert alert-warning">
    <p>Esta opción permite cambiar listas de diseños único en la vista previa de productos en vista de cuadrícula. Diseño de vista de lista no tiene cambios.</p>
</div>
<p>
    Con el fin de añadir banner a la página de la categoría en el menú de navegación superior vaya a <strong>Catálogo > Administración de Categorías</strong>. Seleccione la categoría deseada y en el campo de <strong>Imagen</strong> elija la imagen del banner.
</p>
