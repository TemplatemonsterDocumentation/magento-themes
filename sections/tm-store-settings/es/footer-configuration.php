<h3>Configuración de pie de página</h3>

<p>Usted puede elegir uno de los dos diseños de pie de página disponibles.</p>
Con el fin de elegir o cambiar uno, siga los pasos a continuación:
<ol class="index-list"> 
    <li>
        En el menú superior de navegación vaya a  <strong>Sistema > Configuración</strong>. 
        <figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
    </li>
    <li>
        Vaya al botón "<strong>TM Store Settings</strong>" . 
        <figure class="img-polaroid"><img src="img/magento/footer-settings.jpg" alt="" /></figure>      
    </li>
    <li>
        En la sección "<strong>Layout de Pie de página</strong>" seleccione "<strong>Layout 1</strong>" o "<strong>Layout 2</strong>" para cambiar el pie de página.
    </li>
    <li>
        Haga clic en <strong>"Guardar Configuraciones"</strong>.
    </li>
</ol>

<p>Segunda disposición de Pie de página contiene widget <strong>Blog</strong> .<br>Al elegir el segundo diseño se verá el espacio vacío en el pie de página. Para poder visualizar blog, siga las instrucciones sobre cómo agregar bloques estáticos en la sección <strong>Instalación de la plantilla de Magento -> Configuración de bloques estáticos</strong> y en la tabla <strong><a href="http://livedemo00.template-help.com/magento_53638/template-settings" target="blank_">Ajustes de la plantilla</a></strong> .</p>