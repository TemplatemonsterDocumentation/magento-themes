<h3>Configuración del blog</h3>

<p>Hay tres diseños disponibles en el blog. Con el fin de seleccionar uno de ellos, siga las instrucciones a continuación:</p>
<ol class="index-list"> 
    <li>
        En el menú superior de navegación vaya a <strong>Sistema > Configuración</strong>. 
        <figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
    </li>
    <li>
        Vaya a la pestaña "<strong>TM Store Settings</strong>". En la sección "<strong>Blog Layout</strong>" seleccione el diseño que más le guste.
        <figure class="img-polaroid"><img src="img/magento/blog-settings.jpg" alt="" /></figure>        
    </li>
    <li>
        Haga clic en <strong>"Guardar Configuraciones"</strong>.
    </li>
</ol>

<p>Usted puede cambiar el diseño para la barra lateral torneada off/on en la configuración del módulo. Ajustes del módulo puede encontrarlos en <strong>Extensions settings -> Blog</strong>.</p>