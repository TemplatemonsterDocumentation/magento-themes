<h3>Product list gallery</h3>
<p class="indent">El módulo le permite mostrar las imágenes del producto en miniatura junto con su imagen principal. En el tema por defecto, se visualizan las miniaturas de la imagen principal abajo, pero en nuestros temas se muestran miniaturas de acuerdo con el diseño PSD.</p>
<p>Este módulo puede ser activado / desactivado en la panel de administración de Magento, en <strong>System > Configuration > Templatemonster > Product List Gallery  (Sistema > Configuración > Templatemonster > Product list gallery)</strong>.</p>
<figure class="img-polaroid"><img src="img/magento/productgallery.jpg" alt="" /></figure>
<p>Módulo incluye 4 secciones de configuración: <strong>List View</strong>, <strong>Grid View</strong>, <strong>Homepage List View</strong>, y <strong>Homepage Grid View</strong>. <br>
En <strong>Homepage List View</strong> y <strong>Homepage Grid View</strong> Puede configurar a mostrar la galería en la página principal de la tienda. Otras dos secciones ayudan a configurar la muestra de la galería en las páginas de categorías.</p>
<p>Todas las secciones incluyen los mismos ajustes:</p>			
<table class="options-table">
	<tr>
		<td class="col-1"><strong>Enable/Disable</strong></td>
		<td>-</td>
		<td>se puede activar / desactivar el módulo.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Image width</strong></td>
		<td>-</td>
		<td>se puede configurar el ancho de la imagen principal del producto. </td>
	</tr>
	<tr>
		<td class="col-1"><strong>Image height</strong></td>
		<td>-</td>
		<td>se puede configurar la altura de la imagen principal del producto. </td>
	</tr>
	<tr>
		<td class="col-1"><strong>Thumbnail width</strong></td>
		<td>-</td>
		<td>se puede ajustar la anchura de la imagen en miniatura del producto.</td>
	</tr>
	<tr>
		<td class="col-1"><strong>Thumbnail height</strong></td>
		<td>-</td>
		<td>se puede ajustar la altura de la imagen en miniatura del producto.</td>
	</tr>
</table>
<p>Nota Bene! El ancho máximo de la imagen es igual a la anchura de la columna donde se coloca.</p>
		