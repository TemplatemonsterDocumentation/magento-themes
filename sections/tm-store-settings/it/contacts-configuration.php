<h3>Configurazione della pagina Contatti</h3>
<p>Il modello viene fornito con due varianti di design della pagina Contatti. Per effettuare l'installazione della pagina che desideri, segui cortesemente le istruzioni riportate di seguito.</p>
<ol class="index-list"> 
	<li>
		Apri il pannello di amministrazione di Magento, nel menu di navigazione superiore seleziona <strong>System > Configuration</strong>. 
		<figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
	</li>
	<li>
		Vai a "<strong>TM Store Settings</strong>" tab. 
		<figure class="img-polaroid"><img src="img/magento/store-settings-1.jpg" alt="" /></figure>		
	</li>
	<li>
		Nel "<strong>Contact Form Layout</strong>"  seleziona la sezione "<strong>Layout 1</strong>" oppure "<strong>Layout 2</strong>" e cambia la pagina Contatti.
	</li>
	<li>
		Clicca su  <strong>"Save Config"</strong>.
	</li>
</ol>