<h3>Configurazioni Blog</h3>

<p>Ci sono tre impaginazioni di blog disponibili. Per selezionare uno di loro, seguire le istruzioni riportate di seguito:</p>
<ol class="index-list"> 
    <li>
        Nel menu di navigazione in alto vai a <strong>System > Configuration</strong>. 
        <figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
    </li>
    <li>
        Vai nella "<strong>TM Store Settings</strong>" tab. Nel "<strong>Blog Layout</strong>" seleziona la sezione impaginazione che ti piace.
        <figure class="img-polaroid"><img src="img/magento/blog-settings.jpg" alt="" /></figure>        
    </li>
    <li>
        Clicca <strong>"Save Config"</strong>.
    </li>
</ol>

