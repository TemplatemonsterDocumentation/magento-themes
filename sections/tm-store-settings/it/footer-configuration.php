<h3>Impostazioni Footer</h3>

<p>È possibile scegliere uno dei due impaginazioni disponibili nella sezione footer.</p>
In ordine c'è la possibiltà di scegliere o modificare gli elementi seguendo l'istruzioni di seguito:
<ol class="index-list"> 
    <li>
        Nel menu di navigazione in alto vai a <strong>System > Configuration</strong>. 
        <figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
    </li>
    <li>
        Vai a "<strong>TM Store Settings</strong>" tab. 
        <figure class="img-polaroid"><img src="img/magento/footer-settings.jpg" alt="" /></figure>      
    </li>
    <li>
        Nel "<strong>Footer Layout</strong>" seleziona sezione "<strong>Layout 1</strong>" oppure "<strong>Layout 2</strong>" per cambiare la sezione footer.
    </li>
    <li>
        Clicca <strong>"Save Config"</strong>.
    </li>
</ol>

<p>Secondo footer impaginazione contiene <strong>Blog</strong> widget.<br>Quando si sceglie la seconda impaginazione vedrai lo spazio vuoto nella sezione footer della pagina. Per visualizzare il blog, segui le istruzioni su come aggiungere blocchi statici nel <strong>Magento template installation -> Static blocks configuration</strong> e nella sezione <strong><a href="http://livedemo00.template-help.com/magento_53638/template-settings" target="blank_">Template settings</a></strong> tabella.</p>