<h3>Home (Main) Page Configurazione</h3>

<p>È possibile selezionare una delle due impaginazioni delle pagine disponibili.</p>

Per configurare la seconda impaginazione della pagina principale è necessario aggiungere blocchi statici dal <strong>sources/demo/static_blocks</strong> cartella e applicarli utilizzando i widgets.
<p>Per sapere come aggiungere blocchi statici e applicarli utilizzando i widgets, controllare la sezione che segue <strong>Magento theme installation -> Static blocks configuration</strong>. Il seguente <strong><a href="http://livedemo00.template-help.com/magento_53638/template-settings" target="blank_">Template settings</a></strong> tavola e l'immagine qui sotto ti saranno anche d'aiuto.

