<h3>Impostazioni categoria quotazione</h3>

<p>Il modello include due impaginazioni di quotazione del prodotto sulla pagina di categoria.</p> Per selezionare uno dei richiesti:
<ol class="index-list"> 
    <li>
        Nel menu di navigazione in alto vai a <strong>System > Configuration</strong>. 
        <figure class="img-polaroid"><img src="img/magento/Theme-set-8.jpg" alt="" /></figure>
    </li>
    <li>
        Vai a "<strong>TM Store Settings</strong>" tab. Nel "<strong>Product Listing Layout</strong>" seleziona la sezione impaginazione che ti piace.
        <figure class="img-polaroid"><img src="img/magento/category-settings.jpg" alt="" /></figure>        
    </li>
    <li>
        Clicca <strong>"Save Config"</strong>.
    </li>
</ol>
<div class="alert alert-warning">
    <p>Questa opzione consente di modificare l'impaginazione quotazione solo sul prodotto in anteprima visualizzando la griglia. L'impaginazione di visualizzazione dell'elenco non ha nessuna modifica.</p>
</div>
<p>
    Per poter aggiungere il banner nella pagina di categoria nel menu di navigazione in alto vai a <strong>Catalog > Manage Categories</strong>. Seleziona la categoria desiderata e nel <strong>Image</strong> scegli il campo dell'immagine del banner.
</p>
