		
<h3>Module de Devises</h3>
<ol class="index-list">
	<li>Ouvrez le panneau d'administration de Magento.</li>
	<li>Dans le menu du haut, sélectionnez <b>System > Configuration</b>.</li>
	<li>Puis cliquez sur General et sélectionnez l'onglet <b>Currency Setup</b>.</li>
	<li>Dans <b>Currency Options</b>, en maintenant appuyée la touche CTRL, sélectionnez les devises autorisées pouvant être utilisées pour afficher les prix dans votre boutique Magento. Une fois que c'est fait, cliquez sur le bouton <b>Save Config</b> pour enregistrer vos changements.</li>
	<li>Allez dans <b>System > Manage Currency Rates</b> dans le menu du haut.</li>
	<li>Définissez les valeurs les valeurs des devises out utilisez le Service d'Importation. Cliquez sur le bouton <b>Save Currency Rates</b> pour enregistrer vos changements.</li>
</ol>
		