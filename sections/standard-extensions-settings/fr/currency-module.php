		
<h3>Module de Devises</h3>
<ol class="index-list">
	<li>Ouvrez le panneau d'administration de Magento.</li>
	<li>Dans le menu du haut, sélectionnez <strong>System > Configuration</strong>.</li>
	<li>Puis cliquez sur General et sélectionnez l'onglet <strong>Currency Setup</strong>.</li>
	<li>Dans <strong>Currency Options</strong>, en maintenant appuyée la touche CTRL, sélectionnez les devises autorisées pouvant être utilisées pour afficher les prix dans votre boutique Magento. Une fois que c'est fait, cliquez sur le bouton <strong>Save Config</strong> pour enregistrer vos changements.<figure class="img-polaroid"><img src="img/magento/currency-1.jpg" alt="" /></figure></li>
	<li>Allez dans <strong>System > Manage Currency Rates</strong> dans le menu du haut.</li>
	<li>Définissez les valeurs les valeurs des devises out utilisez le Service d'Importation. Cliquez sur le bouton <strong>Save Currency Rates</strong> pour enregistrer vos changements.<figure class="img-polaroid"><img src="img/magento/currency-2.jpg" alt="" /></figure></li>
</ol>
		