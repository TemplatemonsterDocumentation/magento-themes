		
<h3>Währungsmodul</h3>
<ol>
	<li>Öffnen Sie das Control Panel Magento.</li>
	<li>Wählen Sie in dem oberen Menü <b>System > Configuration</b>.</li>
	<li>Öffnen Sie die Taskleiste General und wählen Sie <b>Currency Setup</b>.</li>
	<li>In <b>Currency Options</b>, indem Sie CTRL Button drücken, können Sie die Währungen wählen, die in Ihrem Online-Shop verfügbar sein müssen. Wenn Sie alle notwendigen Währungen wählen, drücken Sie den Button <b>Save Config</b>, um die Änderungen zu speichern.</li>
	<li>Gehen Sie in <b>System > Manage Currency Rates</b> in dem oberen Menü über.</li>
	<li>Legen Sie den notwendigen Währungskurs fest oder stellen Sie den Import ein. Drücken Sie den Button <b>Save Currency Rates</b>, um die Änderungen zu speichern.</li>
</ol>
		