		
<h3>Währungsmodul</h3>
<ol class="index-list">
	<li>Öffnen Sie das Control Panel Magento.</li>
	<li>Wählen Sie in dem oberen Menü <strong>System > Configuration</strong>.</li>
	<li>Öffnen Sie die Taskleiste General und wählen Sie <strong>Currency Setup</strong>.</li>
	<li>In <strong>Currency Options</strong>, indem Sie CTRL Button drücken, können Sie die Währungen wählen, die in Ihrem Online-Shop verfügbar sein müssen. Wenn Sie alle notwendigen Währungen wählen, drücken Sie den Button <strong>Save Config</strong>, um die Änderungen zu speichern. <figure class="img-polaroid"><img src="img/magento/currency-1.jpg" alt="" /></figure></li>
	<li>Gehen Sie in <strong>System > Manage Currency Rates</strong> in dem oberen Menü über.</li>
	<li>Legen Sie den notwendigen Währungskurs fest oder stellen Sie den Import ein. Drücken Sie den Button <strong>Save Currency Rates</strong>, um die Änderungen zu speichern. <figure class="img-polaroid"><img src="img/magento/currency-2.jpg" alt="" /></figure></li>
</ol>
		