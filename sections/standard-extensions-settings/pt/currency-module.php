		
<h3>Módulo de moeda</h3>
<ol class="index-list"s>
	<li>Abra o admin do Magento.</li>
	<li>No menu, escolha <b>System > Configuration</b>.</li>
	<li>Então clique em General e depois na aba <b>Currency Setup</b>.</li>
	<li>Em <b>Currency Options</b>, segurando a tecla CTRL escolha as moedas permitidas que podem ser usadas para mostrar os preços na sua loja do Magento. Quando terminar, clique em <b>Save Config</b> para guardar suas alterações.</li>
	<li>Navegue até <b>System > Manage Currency Rates</b> no menu superior.</li>
	<li>Defina o valor da proporção das moedas ou utilize o Import Service. Clique em <b>Save Currency Rates</b> para alterá-las.</li>
</ol>
		