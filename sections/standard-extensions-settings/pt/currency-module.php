		
<h3>Módulo de moeda</h3>
<ol class="index-list"s>
	<li>Abra o admin do Magento.</li>
	<li>No menu, escolha <strong>System > Configuration</strong>.</li>
	<li>Então clique em General e depois na aba <strong>Currency Setup</strong>.</li>
	<li>Em <strong>Currency Options</strong>, segurando a tecla CTRL escolha as moedas permitidas que podem ser usadas para mostrar os preços na sua loja do Magento. Quando terminar, clique em <strong>Save Config</strong> para guardar suas alterações.<figure class="img-polaroid"><img src="img/magento/currency-1.jpg" alt="" /></figure></li>
	<li>Navegue até <strong>System > Manage Currency Rates</strong> no menu superior.</li>
	<li>Defina o valor da proporção das moedas ou utilize o Import Service. Clique em <strong>Save Currency Rates</strong> para alterá-las.<figure class="img-polaroid"><img src="img/magento/currency-2.jpg" alt="" /></figure></li>
</ol>
		