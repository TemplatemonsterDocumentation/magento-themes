		
<h3>Para kuru modülü</h3>
<ol class="index-list">
	<li>Magento yönetici panelini açın.</li>
	<li>Üst menüden <b>Sistem > Ayarlar</b> bölümüne girin.</li>
	<li>Ardından Genel bölümüne girin ve <b>Kur Ayarları</b> sekmedine tıklayın.</li>
	<li>Şimdi <b>Kur Ayarları</b> bölümündesiniz. CTRL tuşuna basılı tutarak, Magento mağazanızda kullanabileceğiniz para birimlerinden birini seçin. Ardından <b>Ayarları Kaydet</b> e tıklayın.</li>
	<li>Üst bölümden <b>Sistem > Kur Oranları</b> bölümüne girin.</li>
	<li>Gerekli para birimi oranını girin veya Aktarma Hizmet'ini kullanın. Ardından <b>Kur Oranlarını Kaydet</b> e tıklayın.</li>
</ol>
		