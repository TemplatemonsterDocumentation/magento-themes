		
<h3>Para kuru modülü</h3>
<ol class="index-list">
	<li>Magento yönetici panelini açın.</li>
	<li>Üst menüden <strong>Sistem > Ayarlar</strong> bölümüne girin.</li>
	<li>Ardından Genel bölümüne girin ve <strong>Kur Ayarları</strong> sekmedine tıklayın.</li>
	<li>Şimdi <strong>Kur Ayarları</strong> bölümündesiniz. CTRL tuşuna basılı tutarak, Magento mağazanızda kullanabileceğiniz para birimlerinden birini seçin. Ardından <strong>Ayarları Kaydet</strong> e tıklayın.<figure class="img-polaroid"><img src="img/magento/currency-1.jpg" alt="" /></figure></li>
	<li>Üst bölümden <strong>Sistem > Kur Oranları</strong> bölümüne girin.</li>
	<li>Gerekli para birimi oranını girin veya Aktarma Hizmet'ini kullanın. Ardından <strong>Kur Oranlarını Kaydet</strong> e tıklayın.<figure class="img-polaroid"><img src="img/magento/currency-2.jpg" alt="" /></figure></li>
</ol>
		