		
<h3>Modulo Valute</h3>
<ol class="index-list">
	<li>Apri il pannello di amministrazione Magento.</li>
	<li>Dal menù in alto, seleziona <strong>System > Configuration</strong>.</li>
	<li>Poi clicca su General e seleziona la scheda <strong>Currency Setup</strong>.</li>
	<li>In <strong>Currency Options</strong>, tenendo premuto il pulsante CTRL, seleziona le valute da utilizzare per visualizzare i prezzi nel tuo negozio Magento. Fatto ciò, clicca su <strong>Save Config</strong> per salvare le modifiche.<figure class="img-polaroid"><img src="img/magento/currency-1.jpg" alt="" /></figure></li>
	<li>Vai su <strong>System > Manage Currency Rates</strong> dal menù in alto.</li>
	<li>Imposta i valori richiesti di tasso di cambio valute, oppure usa l'Import Service. Clicca su <strong>Save Currency Rates</strong> per salvare le modifiche.<figure class="img-polaroid"><img src="img/magento/currency-2.jpg" alt="" /></figure></li>
</ol>
		