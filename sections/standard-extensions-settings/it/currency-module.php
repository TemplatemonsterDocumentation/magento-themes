		
<h3>Modulo Valute</h3>
<ol>
	<li>Apri il pannello di amministrazione Magento.</li>
	<li>Dal menù in alto, seleziona <b>System > Configuration</b>.</li>
	<li>Poi clicca su General e seleziona la scheda <b>Currency Setup</b>.</li>
	<li>In <b>Currency Options</b>, tenendo premuto il pulsante CTRL, seleziona le valute da utilizzare per visualizzare i prezzi nel tuo negozio Magento. Fatto ciò, clicca su <b>Save Config</b> per salvare le modifiche.</li>
	<li>Vai su <b>System > Manage Currency Rates</b> dal menù in alto.</li>
	<li>Imposta i valori richiesti di tasso di cambio valute, oppure usa l'Import Service. Clicca su <b>Save Currency Rates</b> per salvare le modifiche.</li>
</ol>
		