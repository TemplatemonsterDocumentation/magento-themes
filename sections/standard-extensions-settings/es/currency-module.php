		
<h3>M&oacute;dulo de divisas</h3>
<ol class="index-list">
	<li>Abra el panel de administraci&oacute;n de Magento.</li>
	<li>En el men&uacute; superior, seleccione <strong>System > Configuration</strong>.</li>
	<li>A continuaci&oacute;n, haga clic en General y seleccione la pesta&ntilde;a <strong>Currency Setup</strong>.</li>
	<li>En las <strong>Currency Options</strong>, Presione la tecla CTRL y seleccione las monedas permitidas que se pueden usar para mostrar los precios en su tienda de Magento. Cuando haya terminado, haga clic en el bot&oacute;n <strong>Save Config</strong> para guardar los cambios.<figure class="img-polaroid"><img src="img/magento/currency-1.jpg" alt="" /></figure></li>
	<li>Vaya a <strong>System > Manage Currency Rates</strong> en el men&uacute; superior.</li>
	<li>Establezca los valores de tasa de divisas requeridos o utilice el servicio de importaci&oacute;n. Haga clic en el bot&oacute;n <strong>Save Currency Rates</strong> para guardar los cambios.<figure class="img-polaroid"><img src="img/magento/currency-2.jpg" alt="" /></figure></li>
</ol>
		