		
<h3>M&oacute;dulo de divisas</h3>
<ol class="index-list">
	<li>Abra el panel de administraci&oacute;n de Magento.</li>
	<li>En el men&uacute; superior, seleccione <b>System > Configuration</b>.</li>
	<li>A continuaci&oacute;n, haga clic en General y seleccione la pesta&ntilde;a <b>Currency Setup</b>.</li>
	<li>En las <b>Currency Options</b>, Presione la tecla CTRL y seleccione las monedas permitidas que se pueden usar para mostrar los precios en su tienda de Magento. Cuando haya terminado, haga clic en el bot&oacute;n <b>Save Config</b> para guardar los cambios.</li>
	<li>Vaya a <b>System > Manage Currency Rates</b> en el men&uacute; superior.</li>
	<li>Establezca los valores de tasa de divisas requeridos o utilice el servicio de importaci&oacute;n. Haga clic en el bot&oacute;n <b>Save Currency Rates</b> para guardar los cambios.</li>
</ol>
		