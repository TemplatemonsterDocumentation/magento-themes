		
<h3>Moduł waluty</h3>
<ol>
	<li>Otwórz panel administratora Magento.</li>
	<li>Z górnego menu wybierz <b>System > Configuration</b>.</li>
	<li>Następnie kliknij pozycję Ogólne i wybierz zakładkę <b>Currency Setup</b>.</li>
	<li>W zakładce <b>Currency Options</b>, przytrzymując klawisz Ctrl, zaznacz dozwolone waluty, które mogą być używane do wyświetlania ceny w sklepie. Kiedy skończysz, kliknij przycisk <b>Save Config</b>, aby zapisać zmiany.</li>
	<li>Przejdź do <b>System > Manage Currency Rates</b> w górnym menu.</li>
	<li>Ustaw żądane wartości kursu walutowego lub skorzystaj z Usługi Import. Kliknij przycisk <b>Save Currency Rates</b>, aby zapisać Twoje zmiany.</li>
</ol>
		