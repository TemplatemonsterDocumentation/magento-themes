		
<h3>Moduł waluty</h3>
<ol class="index-list">
	<li>Otwórz panel administratora Magento.</li>
	<li>Z górnego menu wybierz <strong>System > Configuration</strong>.</li>
	<li>Następnie kliknij pozycję Ogólne i wybierz zakładkę <strong>Currency Setup</strong>.</li>
	<li>W zakładce <strong>Currency Options</strong>, przytrzymując klawisz Ctrl, zaznacz dozwolone waluty, które mogą być używane do wyświetlania ceny w sklepie. Kiedy skończysz, kliknij przycisk <strong>Save Config</strong>, aby zapisać zmiany.<figure class="img-polaroid"><img src="img/magento/currency-1.jpg" alt="" /></figure></li>
	<li>Przejdź do <strong>System > Manage Currency Rates</strong> w górnym menu.</li>
	<li>Ustaw żądane wartości kursu walutowego lub skorzystaj z Usługi Import. Kliknij przycisk <strong>Save Currency Rates</strong>, aby zapisać Twoje zmiany.<figure class="img-polaroid"><img src="img/magento/currency-2.jpg" alt="" /></figure></li>
</ol>
		