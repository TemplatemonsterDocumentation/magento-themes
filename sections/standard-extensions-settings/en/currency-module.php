		
<h3>Currency module</h3>
<ol class="index-list">
	<li>Open the Magento admin panel.</li>
	<li>From the top menu select <strong>System > Configuration</strong>.</li>
	<li>Then click General and select the <strong>Currency Setup</strong> tab.</li>
	<li>In the <strong>Currency Options</strong>, holding the CTRL key select the allowed currencies that can be used to display prices in your Magento store. When you are done, click the <strong>Save Config</strong> button to save your changes. <figure class="img-polaroid"><img src="img/magento/currency-1.jpg" alt="" /></figure></li>
	<li>Navigate to <strong>System > Manage Currency Rates</strong> in the top menu.</li>
	<li>Set the required currency rate values or use the Import Service. Click the <strong>Save Currency Rates</strong> button to save your changes. <figure class="img-polaroid"><img src="img/magento/currency-2.jpg" alt="" /></figure></li>
</ol>
		