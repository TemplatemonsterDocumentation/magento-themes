		
<h3>Currency module</h3>
<ol>
	<li>Open the Magento admin panel.</li>
	<li>From the top menu select <b>System > Configuration</b>.</li>
	<li>Then click General and select the <b>Currency Setup</b> tab.</li>
	<li>In the <b>Currency Options</b>, holding the CTRL key select the allowed currencies that can be used to display prices in your Magento store. When you are done, click the <b>Save Config</b> button to save your changes.</li>
	<li>Navigate to <b>System > Manage Currency Rates</b> in the top menu.</li>
	<li>Set the required currency rate values or use the Import Service. Click the <b>Save Currency Rates</b> button to save your changes.</li>
</ol>
		